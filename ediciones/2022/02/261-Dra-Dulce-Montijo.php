          <p class="texto-preg">¿Cuál es la importancia de la valoración y atención oportuna en odontología?</p>
          <p>“Sin duda, una evaluación temprana siempre será importante para prevenir las enfermedades bucales más comunes como la enfermedad periodontal y la caries. Es por ello importante inculcar la prevención desde una edad muy temprana y de esta forma crear conciencia del papel fundamental que tiene nuestra salud bucal en nuestra salud en general”.</p>

          <p class="texto-preg">¿Cuál es la relación entre salud y estética dental?</p>
          <p>“La estética dental y la salud dental son dos conceptos complementarios, nosotros cuando acudimos al odontólogo a realizarnos un tratamiento estético estamos mejorando nuestra salud dental y, por lo tanto, nuestra salud en general. La Organización Mundial de la Salud señala que 'la salud es un estado de completo bienestar físico, mental y social, no solamente la ausencia de afecciones o enfermedades', es importante destacar que la estética dental parte de un buen mantenimiento de la salud bucodental”.</p>

          <p class="texto-preg">¿Algún avance o innovación reciente en su campo que pudiera destacar?</p>
          <p>“En mi consulta me encuentro relacionada con lo que es el DSD, Digital Smile Design por sus siglas en inglés, es una herramienta que permite el diseño y planificación de manera digital de la estética ideal del paciente, con su ayuda mejoramos la comunicación y los objetivos deseados por el paciente y mejora la planificación del caso con el laboratorio, aumentando la previsibilidad y resultados del tratamiento.”</p>

          <p class="texto-preg">De acuerdo con su experiencia ¿cuál es la periodicidad ideal para visitar al odontólogo?</p>
          <p>“Lo ideal es visitar a su odontólogo de cabecera cada 6 meses, para realizar una limpieza general y valorar que los órganos dentarios y tratamientos restaurativos (en caso de presentarlos) se encuentren en buen estado, además detectar alguna enfermedad a tiempo y, mejor aún, poder evitarla. En pacientes inmunosuprimidos o con enfermedades crónicodegenerativas les recomendaría acudir cada 3 a 4 meses para un mayor control de su salud bucodental”.</p>

          <p class="texto-preg">¿Algún mensaje o recomendación adicional que quiera enviar a los lectores?</p>
