      <div class="texto">
        <p>En la actualidad, el bombardeo de información que se genera con las redes sociales, lejos de despejar dudas
          en los pacientes potenciales, los llena de vacilación al momento de decidirse por un tratamiento dental u
          ortodóncico. Esto es porque crea expectativas en torno a un tratamiento en particular, y al llegar con el
          especialista se percatan de que lo que investigaron o leyeron no es lo más adecuado para su caso particular.
        </p>
        <p>Por ello, uno de los datos más importantes que se deben tomar en cuenta al momento de realizar una historia
          clínica es qué busca el paciente, ya que de ahí parte el ortodoncista para dar las opciones de plan de
          tratamiento, tomando en consideración las indicaciones clínicas para cada tipo de diagnóstico en específico.
        </p>
        <p>Hay que destacar que los tratamientos de ortodoncia ya no son exclusivos de niños y adolescentes; la alta
          demanda de tratamiento en la población adulta ha hecho aparecer diferentes tipos de brackets para cubrir las
          necesidades de estética y duración. Además, para realizar un tratamiento de ortodoncia se cuenta con varias
          técnicas, estrategias y herramientas diferentes diseñados para cada tipo de diagnóstico.</p>
        <p>Podemos dividir los tipos de brackets en cinco grandes grupos: metálicos convencionales, autoligables,
          estéticos, linguales e invisibles. Los revisaremos a continuación.</p>

        <p><span style="color: #40a091;">Brackets metálicos.</span> Son los más antiguos, son prácticos y tienen muy
          buena funcionalidad adaptándose a muchas técnicas y aditamentos complementarios. Se ligan al arco por medio de
          ligaduras metálicas y elásticas. Su material hace que tengan baja fricción comparado con otros tipos de
          brackets, por lo que el movimiento no es tan lento. Una de sus ventajas es el bajo costo y sigue siendo en la
          actualidad el sistema de ortodoncia fija más usado en niños y adolescentes.</p>

        <div class="foto-izq">
          <figure>
            <img src="/ediciones/2022/02/imgs/261-Dra-Cinthia-Borbon-TIPOS-ORTODONCIA-2.jpg" alt="">
          </figure>
        </div>

        <p><span style="color: #40a091;">Brackets linguales.</span> Van por la cara interna de los dientes, por lo cual
          sería el tratamiento más estético al estar ocultos. Sus desventajas son varias: la principal es el roce de la
          lengua con los brackets, lo que podría ser molesto durante todo el tratamiento; presentan menos adaptación a
          los tejidos orales comparado a otras técnicas; implican un mayor tiempo operatorio en el consultorio, es
          decir, citas más largas; y, la dificultad en la higiene por el complicado acceso al momento del cepillado.</p>

        <p><span style="color: #40a091;">Brackets de autoligado.</span> Existen tanto en versión metálica como estética.
          Su gran ventaja es la menor fricción por lo que se acorta el tiempo de tratamiento, resuelven mejor los
          apiñamientos (dientes empalmados), sobre todo en pacientes que no se hacen extracciones dentales o cuando
          existe algún compromiso periodontal. Por ello, son más utilizados en pacientes adultos.</p>

        <p><span style="color: #40a091;">Brackets estéticos.</span> Su ventaja es la estética y su bajo perfil por el
          material del que están hechos. Los más conocidos son los de zafiro y los cerámicos. Llevan el movimiento con
          un poco más de fricción, así que en algunos casos podría verse afectada la duración del tratamiento. Otra de
          sus desventajas es que suelen tener un costo más elevado.</p>

        <div class="foto-der">
          <figure>
            <img src="/ediciones/2022/02/imgs/261-Dra-Cinthia-Borbon-TIPOS-ORTODONCIA-3.jpg" alt="">
