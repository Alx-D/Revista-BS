        <p><span style="color: #40a091;">Invisibles.</span> Se realiza por medio de una serie de alineadores
          personalizados al paciente, creando una secuencia a partir de un software, donde se ve el inicio y la
          evolución hasta el final del tratamiento. Es una opción estética, cómoda por ser removible, permite fácil
          higiene y es predecible ya que se pueden ver los resultados antes de iniciar. Su desventaja sería la
          efectividad condicionada a su correcto uso e higiene por parte del paciente; tiene un mayor costo y podría no
          ser indicado para todos los tipos de maloclusiones.</p>
        <p>Otro tipo de aparatos que siguen siendo muy utilizados en pacientes en crecimiento y desarrollo son los de
          ortopedia maxilar, pueden ser fijos o removibles y nos ayudan a redirigir el crecimiento óseo y malposiciones
          dentales.</p>

        <p class="descanso">Acelerar el tratamiento</p>
        <p>Todos los tipos de tratamientos anteriores se pueden combinar con alguna técnica de ortodoncia acelerada como
          son las corticotomías (cortes óseos), láser terapéutico o algún dispositivo de estimulación celular para
          disminuir tiempos de tratamiento.</p>
        <p>El especialista en ortodoncia es el indicado para decidir si el paciente es candidato a alguna de estas
          técnicas de ortodoncia acelerada. Muchas veces el ortodoncista se apoya en interconsultas con algún otro
          especialista por problemas en otras áreas, como un odontopediatra, periodoncista, rehabilitador, hasta algún
          maxilofacial para alguna cirugía ortognática.</p>
        <p>Una buena cita de diagnóstico con los estudios indicados nos ayuda a empatar criterios entre paciente y
          ortodoncista para llegar a la mejor opción para el tipo de problema y las necesidades de cada paciente. Lo
          mejor es solicitar una cita de valoración con un especialista en ortodoncia, para ser orientado y tomar la
          mejor decisión. &nbsp;<i class="fas fa-square"></i></p>
