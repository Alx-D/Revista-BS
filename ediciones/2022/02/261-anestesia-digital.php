<p>Así, la dispersión del anestésico es menos doloroso, depende de la densidad de la difusión, la cual está relacionada con la densidad del tejido, reduciendo el dolor no sólo por la resistencia que se siente en los tejidos, sino por su velocidad controlada, es por eso que contamos con este sistema de anestesia digital para minimizar las molestias causadas por los tratamientos odontológicos, así como reducir el estrés generado por los mismos, lo cual hace menos dolorosa la aplicación, respecto a la inyección tradicional.</p>

<p class="descanso">Palabras de encanto</p>
<p>Cuando es la primera vez que se le va a administrar anestesia local al niño, le explicamos lo que va a sentir durante y después de la anestesia. Esta información debe ser compatible con la edad del pequeño paciente y con un lenguaje adecuado a la comprensión de la sensación que sentirá con la anestesia, es importante contar la verdad al niño.</p>
<p>En cuestión de minutos, el paciente se encuentra completamente anestesiado de la zona que se tratará, sin haber sentido dolor o molestia como el que causan las agujas utilizadas en los métodos convencionales de anestesia. Es así como el paciente no se entera que fue inyectado y anestesiado, y el tratamiento se vuelve amigable para él.</p>
<p>Otro punto muy importante, es no asustar a los niños en casa haciéndoles creer que el dentista es malo, evitar comentarios de que el tratamiento que le realizarán va a ser doloroso.</p>
<p>Todos esos comentarios causan ansiedad en los niños y hacen que su tratamiento les parezca pesado, también hay que evitar frases como “si no te portas bien, te voy a llevar con el dentista”. Es momento de que hagamos equipo con los papás y crear una cultura de prevención, donde asistir al dentista, de una manera preventiva y periódica, sea parte cotidiana de un control de salud. &nbsp;<i class="fas fa-square"></i></p>
