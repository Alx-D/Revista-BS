      <div class="texto">
        <p>El miedo a la anestesia es una de las fobias más frecuentes en la visita al odontólogo. La jeringa de la anestesia convencional fue creada hace más de 150 años, y sólo hasta la década de los noventa se comenzaron a desarrollar sistemas de administración local de anestesia que incorporan tecnología informática para controlar la velocidad del flujo de solución anestésica a través de la aguja, y el primer sistema fue lanzado en 1997.</p>
<p>Cuando el infante es sometido a un tratamiento dental, en el cual se requiere anestesia, la aguja suele ser un gran problema, ya que con sólo verla asusta e inquieta a los menores, lo que suele ser un trauma para ellos.</p>
<p>Los especialistas, debemos procurar generar menos estrés a nuestros pacientes infantiles, y, con las nuevas técnicas ahora disponibles, tenemos la oportunidad de mejorar la experiencia en un tratamiento odontológico. La anestesia digital es la mayor aliada para consentir a los pequeños de la casa, ya que minimiza el miedo y la ansiedad.</p>

<p class="descanso">La varita</p>
<p>En la anestesia digital, técnica con la que contamos en nuestra clínica, la aguja es ultradelgada, prácticamente imperceptible para cualquier paciente.</p>
<p>Este sistema es computarizado y hace que el anestésico local sea aplicado por medio de un mircoprocesador y motor eléctrico que regula con precisión la velocidad de la aplicación de la anestesia a una presión y volumen constante.</p>

