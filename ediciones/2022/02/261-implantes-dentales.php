      <div class="texto">
        <p>Los implantes dentales se introdujeron en la odontología en la década de 1960, por lo cual no son un tratamiento nuevo, por el contrario, tienen bastantes años de investigación, que año con año los va mejorando.</p>
        <p>El implante dental se diseñó para sustituir la raíz de un diente natural, existen principalmente dos materiales: zirconia y titanio. Los implantes de óxido de zirconio son una opción para las personas que no desean metales dentro de su cuerpo.</p>
        <p>Los implantes de titanio, según estudios, tienen un comportamiento mejor en la boca, comparados con los de zirconia. Recordemos que en nuestra boca constantemente apretamos, rechinamos, bostezamos y salivamos, por lo que están sometidos a fuerzas de compresión, estiramiento y corrosión por la saliva.</p>

