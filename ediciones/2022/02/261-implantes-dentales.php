        <p><span>Resultado estético.</span> El implante queda sumergido en el hueso, una vez terminada la pequeña cirugía la incisión cicatriza en 72 horas y el implante lo dejamos en observación de tres a seis meses, esperando un proceso natural del cuerpo llamada oseointegración, en el que el hueso se forma alrededor del implante y solidifica. El paciente estará en revisiones periódicas checando que todo se encuentre bien hasta concluida la oseointegración.</p>

        <p><span>Colocación de la corona.</span> Después de la oseointegración, se tomará un molde de la posición del implante para la fabricación de la corona. Que posteriormente se atornillará al implante en su boca.</p>

        <p><span>Sensación.</span> Los dientes naturales están unidos al ligamento periodontal y éste a su vez al hueso, este ligamento sirve como un amortiguador natural cuando apretamos los dientes. En cambio, los implantes están en límpido contacto con el hueso, lo que los hace sentir más rígidos, además, no tienen nervio por lo que no sentirá lo frio ni lo caliente.</p>

        <p><span>Opción más duradera.</span> Los estudios señalan que el fracaso por caries en un puente es del 22 al 27%; fracturas de un diente 11 al 15% y tienen una vida media de 7 años. En cambio, los implantes tienen una vida media de más de 15 años, con tazas de éxito del 97%.</p>

        <p><span>Adaptable a su necesidad.</span> El número de implantes que se coloquen dependerá de la planeación y de la restauración final. Un ejemplo sería un paciente edéntulo total (sin ningún diente), se colocan seis implantes arriba en el maxilar y seis más en la mandíbula, sumando doce implantes.</p>

        <p><span>Fácil mantenimiento.</span> El cepillado dental mínimo tres veces al día es el mismo, no por ya no tener diente natural debe descuidar el cepillado, al contrario, un mantenimiento adecuado de su salud bucal es indispensable. Un complemento al cepillado son los irrigadores de agua a presión, que llegan a zonas de difícil acceso para las cerdas del cepillo.</p>
        <p>Los implantes dentales no son una moda, la colocación y rehabilitación es sencilla en manos expertas, tome en cuenta la información y siempre acuda con un odontólogo con especialidad en implantes certificado. &nbsp;<i class="fas fa-square"></i></p>
