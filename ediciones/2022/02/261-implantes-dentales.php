        <p>El titanio es el segundo metal más biocompatible después del oro y no existe rechazo como tal, ni es tóxico para su cuerpo, tampoco al pasar por un detector de metal sonará la alarma.</p>
        <p>Los dientes que mayormente son sustituidos por implantes son el primer molar (la tercera muela de atrás para adelante comenzando por la del juicio).</p>
        <p>El segundo lugar lo ocupan los dientes de enfrente (centrales), frecuentemente debido a una factura, golpe o accidente. La opción de un puente en este sitio no es la más saludable, ya que se tendrían que desgastar los dientes sanos, y no resulta tan estético.</p>
        <p>A continuación expondré algunos puntos importantes sobre esta opción de rehabilitación oral que debe conocer.</p>

        <p><span>Buena alternativa.</span> Si perdió uno o varios dientes a consecuencia de la edad; tras el fracaso de un puente; por un rendimiento defectuoso de una prótesis removible o prótesis total; para mejorar el aspecto estético, en estos casos la solución son los implantes dentales.</p>

        <p><span>Diseño de sonrisa.</span> En la planificación del implante comenzamos por el determinar qué colocaremos sobre el implante: una corona unitaria, un puente, o toda una dentadura; y así saber la posición deseada de la colocación de dicho implante.</p>
        <p>Con una tomografía se observará la densidad, anchura y grosor del hueso y con un escaneo de la boca o con la toma de un molde podremos realizar el estudio y saber si sólo es colocar el implante o si será necesario realizar injertos de hueso o de encía. Estos tres parámetros determinarán la colocación del implante y su costo.</p>

        <p><span>Proceso indoloro.</span> Todo se hace bajo anestesia local. Actualmente utilizamos una técnica llamada cirugía guiada, en la cual con una guía quirúrgica que nos indica la posición en la que se colocará el implante, sin necesidad de hacer cortes en la encía, ni suturar. Sin sangrado, sin dolor postoperatorio, es decir, un procedimiento mínimamente invasivo.</p>

