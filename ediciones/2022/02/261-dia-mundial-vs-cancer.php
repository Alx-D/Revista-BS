        <p>De acuerdo con las cifras, el cáncer se ha convertido en la segunda causa de muerte en nuestro planeta, sólo después de las enfermedades cardiovasculares.</p>
        <p>En el mundo, de acuerdo a los datos emanados por la Organización Mundial de la Salud (OMS) y la UICC (Union for International Cancer Control), en 2020 se presentaron 20 millones de casos nuevos y 10 millones fallecieron por esta causa.</p>
        <p>Se pronostica, asimismo, que esta carga se incrementará un 60%, aproximadamente, en próximos años, manifestándose más profusamente en los países cuyos niveles de ingresos son bajos y medios.</p>

        <p class="descanso">Revisión local</p>
        <p>La situación de América es preocupante, los mismos organismos indican que en el 2020, 4 millones de personas fueron diagnosticadas y 1.4 millones murieron de cáncer. Alrededor de 57% de los nuevos casos de cáncer, así como el 47% de las muertes, se presentan en individuos de 69 años o más. Es decir, el 20% de todos los casos nuevos en el mundo ocurren en América, y el 14% murieron debido a esta enfermedad.</p>
        <p>El INEGI expone las siguientes cifras para nuestro país: entre enero y agosto de 2020 se registraron más de 683 mil 800 defunciones, de las cuales 9% se debieron a tumores malignos (60 mil 421). Un 51% ocurrieron en mujeres y el resto en hombres.</p>
        <p>Sonora, por su parte, es una de las entidades cuya mortalidad por cáncer registró el rango más alto (de acuerdo a la estratificación que realiza esta dependencia federal), mismo que oscila entre 73.4 y 92.1 casos por cada 100 mil habitantes. Estos datos correspondieron al año 2019, son los datos más actualizados. En la misma clasificación se encuentran Sinaloa, Chihuahua, Nuevo León, Veracruz y Ciudad México.</p>

        <p class="descanso">Tomar acción</p>
        <p>Ante este panorama global, los organismo internacionales han lanzado la campaña “Por unos cuidados más justos”, que, precisamente, apela a la disminución de la brecha de equidad que nos afecta, y que pese a los impresionantes avances en la prevención, diagnóstico y tratamiento del cáncer, a la hora de buscar atención oncológica, muchos pacientes se topan con barreras como sus ingresos, educación, localización geográfica y la discriminación basada en etnia, raza, género, orientación sexual, edad, discapacidad y estilo de vida, entre otros factores que pueden afectar negativamente la atención del cáncer, afirma la UICC.</p>
        <p>Asevera también que 3.7 millones de personas podrían ser salvadas con estrategias adecuadas a los recursos ya existentes en cuanto a prevención, detección temprana y tratamiento del cáncer oportuno y de calidad.</p>

        <p class="descanso">El compromiso es de todos</p>
        <p>Si pretendemos abatir las cifras pronosticadas, cada uno tenemos la capacidad de reducir el impacto actual con miradas al futuro. La OMS afirma que de un tercio a la mitad de los casos de cáncer pueden prevenirse reduciendo la prevalencia de los factores de riesgo conocidos. El control del tabaco y la vacunación contra VPH son ejemplos de intervenciones.</p>
        <p>La prevención primaria del cáncer más eficaz se basa en enfoques de todo gobierno, con legislación, regulación y políticas fiscales combinadas con actividades para cambiar el comportamiento individual y de la comunidad. Los mensajes de salud pública y la promoción de la salud deben presentar evidencia sobre factores de riego específicos.</p>

        <p class="descanso">Hay esperanza</p>
