      <div class="texto">
        <p>La salud bucodental es fundamental para gozar de buena salud y calidad de vida, la cavidad bucal contribuye a
          la protección del cuerpo por medio de la masticación y deglución, también favorece el autoestima, expresión,
          comunicación y estética facial.</p>
        <p>La Organización Mundial de la Salud define la salud bucal como la ausencia de dolencias o trastornos en la
          cavidad bucodental que limiten o repercutan a la persona en su bienestar psicosocial. Estas dolencias o
          trastornos pueden ser dolor, cáncer, infecciones, llagas, hasta enfermedades periodontales, pérdida de dientes
          o trastornos bucodentales.</p>
        <p>A nivel mundial más del 90% de la población padece algún problema de salud bucal, siendo los más comunes la
          caries y la enfermedad periodontal; sin embargo, sólo un 60% de ésta tiene acceso a los servicios de salud,
          los cuales se limitan a acciones muy básicas del cuidado bucal; y las enfermedades bucodentales son la cuarta
          patología más cara a tratar (Boletín Epidemiológico Semana 26, 2016, Secretaría de Salud).</p>
        <p>Hay otros problemas bucales más complejos y de difícil manejo como el cáncer de boca, las enfermedades
          infecciosas bucodentales, la maloclusión, los traumatismos físicos y las lesiones.</p>
        <p>Practicar una buena higiene bucal y visitar al dentista frecuentemente es la mejor manera para evitar
          problemas bucales, de esta forma podrá prevenir cualquier padecimiento a futuro.</p>
        <p>Cepillado de dientes al menos dos veces al día, uso de hilo dental diariamente, uso de enjuague bucal
          preferentemente sin alcohol, mantener una dieta saludable baja en azúcares y alimentos procesados, evitar el
          consumo de tabaco y café, no abusar de blanqueamientos dentales y cambiar de cepillo dental cuando pierda su
          firmeza son las principales recomendaciones para lograrlo.</p>

        <div class="texto-resaltado-bx">
          <p class="descanso" style="margin-top: 0;">Principales afecciones</p>
          <p>Las bacterias que tenemos en la boca forman una película a las dos horas de habernos cepillado los dientes,
            produciendo la llamada placa bacteriana. Si dicha placa, permanece en la boca durante 48 horas sin ser
            cepillada, comienza a endurecerse con los iones que tenemos en la saliva y se transforma en sarro o cálculo.
          </p>
          <p>El sarro o cálculo adherido al diente y tejido gingival de la encía es un indicador significativo de
            deficiente higiene bucal, se manifiesta con un color marrón o amarillento en los dientes o encías lo que es
            considerado un importante factor de riesgo para afecciones más graves como caries, gingivitis y enfermedad
            periodontal. Por tanto, tendremos más inflamación (gingivitis) o enfermedad periodontal “piorrea”.</p>
          <p>Según los resultados del Sistema de Vigilancia Epidemiológica de Patologías Bucales (SIVEPAB) 2019, de
            México, se examinó la cavidad bucal de un total de 99 mil seis niñas, niños y adolescentes de 0 a 19 años de
            edad que acuden a los servicios de salud, y se encontró que cinco de cada diez de ellos, de seis a 19 años
            de edad, tenían visibles detritos o cálculo en los dientes.</p>
          <p>Del total de la población adulta examinada, 213 mil 619 personas, de 20 a 99 años de edad, se mostró que
            68% tenían visibles detritos o cálculo sobre los dientes, el cual aumenta considerablemente con la edad,
            alcanzando hasta 74.2% en el grupo de 60 a 64 años.</p>
          <p>Respecto a dientes cariados, perdidos u obturados en dentición temporal, en el grupo de 1 a 10 años, se
            muestra que los menores tienen 3.5 dientes afectados de los cuales tres estaban cariados.</p>
          <p>La prevalencia de caries dental fue de 93.3% en la población adulta examinada, en todos los grupos ésta fue
            superior a 84%; sin embargo, en la población a partir de los 40 años la prevalencia fue superior a 95%.</p>
          <p>De 131 mil 903 pacientes adultos revisados, 59.2% tenían algún signo de enfermedad periodontal, 22% tenían
            gingivitis.</p>
          <p>La gingivitis y enfermedad periodontal representaba el quinto lugar dentro de las 20 principales causas de
            enfermedad sujetas a vigilancia epidemiológica a nivel nacional en el año 2019, con 1.1 millones de casos
            nuevos. Igualmente, en Sonora representaba el cuarto lugar como causa de enfermedad, con 37 mil 454 casos
            nuevos, de acuerdo a la Secretaría de Salud.</p>
          <p>El año 2020, por la pandemia y la disminución de la oferta de servicios, esta enfermedad bajó al décimo y
            sexto lugar, respectivamente, como causa de enfermedad. Aún así, ésta y la caries son padecimientos muy
            frecuentes, como se observa en la población revisada por SIVEPAB.</p>
          <p>Cuide su salud bucal, visite a su dentista regularmente y procure su Buena Salud.</p>
