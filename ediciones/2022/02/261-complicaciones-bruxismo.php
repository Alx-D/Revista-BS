        <p>El bruxismo suele aparecer a edades tempranas (generalmente en la adolescencia) y afecta en igual medida a
          hombres y mujeres. Además, se trata de un hábito muy perjudicial que, en función de su frecuencia, puede
          provocar desgastes extremos y dolor en la musculatura mandibular y en la articulación temporomandibular.</p>

        <p class="descanso">Causas</p>
        <p>Aunque existen diferentes teorías sobre las posibles causas de su aparición, por el momento se desconoce el
          origen real de este problema de salud dental.</p>
        <p>Un gran número de expertos coinciden en la idea de que el estrés y los trastornos del sueño podrían ser dos
          de las principales causas de esta dolencia, aunque también contemplan otros factores que pueden intervenir en
          la aparición del bruxismo como son: la mala alimentación, alineación inadecuada de los dientes, presencia de
          trastornos respiratorios durante el sueño, malas posturas a la hora de dormir o ansiedad, entre otras.</p>

        <p class="descanso">Consecuencias</p>
        <p>Además de hipersensibilidad dental y dolor constante en los músculos faciales y en los dientes, el bruxismo
          puede provocar otros problemas. Los revisamos a continuación.</p>
        <ul class="lista-disco-nota">
          <li><span style="font-weight: 600;">Hipersensibilidad aguda:</span> al rechinar los dientes, las piezas
            dentales se desgastan, astillan y rompen, produciendo dolor en la zona mandibular.</li>
          <li><span style="font-weight: 600;">Sensibilidad dental:</span> especialmente al frío, al calor y a los
            alimentos dulces.</li>
          <li><span style="font-weight: 600;">Irradiación del dolor:</span> el malestar puede extenderse a otros
            órganos, produciendo dolor de cuello, oído o cabeza.</li>
          <li><span style="font-weight: 600;">Insomnio:</span> el malestar producido por el dolor puede derivar en
            dificultades y problemas para conciliar el sueño.</li>
        </ul>
        <p>
          Cuando el problema del bruxismo no se corrige a tiempo suele desembocar en patologías o alteraciones
          articulares y en una disyunción maxilar que puede producir trismo (un síntoma que dificulta o limita la
          correcta apertura de la boca) e, incluso, llegar a generar una contractura muscular muy dolorosa.
        </p>
        <p>
          Pero las consecuencias de apretar y rechinar los dientes durante el sueño pueden ser aún más serias. Pueden
          surgir trastornos de la articulación temporomandibular susceptibles de causar dolor diurno de cabeza, oídos o
          cuello, además de molestias al abrir la boca como dolor, chasquido en la articulación, dificultad para
          masticar
          y, si el problema es grave, incluso para hablar.
        </p>

