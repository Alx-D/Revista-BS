      <div class="texto">
        <p>El bruxismo, también conocido como la enfermedad silenciosa, es una patología que sufre aproximadamente un
          70% de la población de México. Consiste en apretar de forma inconsciente la mandíbula y rechinar los dientes,
          produciendo el desgaste de los mismos. Puede darse tanto por el día como por la noche, aunque el más frecuente
          es el que se produce durante el sueño.</p>
        <p>La mayoría de las veces, la persona afectada no es consciente de que tiene bruxismo hasta que otra le
          advierte que al dormir rechina los dientes, o es el dentista quien le informa tras realizarle una revisión
          dental rutinaria.</p>

