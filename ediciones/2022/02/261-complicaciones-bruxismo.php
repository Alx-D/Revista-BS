        <ul class="lista-disco-nota">
          <li><span style="font-weight: 600;">Férula de descarga:</span> en la mayoría de los casos, especialmente
            cuando se trata de bruxismo de predominio nocturno, el especialista recomienda al paciente el uso de una
            férula de descarga. Se trata de una prótesis ajustada a la dentadura del paciente, que debe colocarse
            durante las horas de sueño, y cuya función es intentar relajar y reconocer la articulación ATM para evitar
            el rechinar de los dientes y el consecuente desgaste del esmalte.</li>
          <li><span style="font-weight: 600;">Rehabilitación protésica:</span> en casos de bruxismo más avanzado, en los
            que existe un importante desgaste dental y una disminución de la dimensión vertical que afecta a la
            articulación temporomandibular, se recomienda rehabilitar protésicamente las piezas dentales con el fin de
            recuperar la posición articular idónea.</li>
        </ul>

        <p class="descanso">Recomendaciones generales</p>
        <p>En todos los casos, los especialistas aconsejan realizar cualquier tipo de actividad que ayude a controlar,
          reducir o eliminar el estrés y la tensión muscular, ya que suelen ser desencadenantes del bruxismo; realizar
          ejercicios de relajación, masajear las zonas musculares mandibulares afectadas y dormir las horas adecuadas
          manteniendo una correcta postura corporal. La visita regular al odontólogo es la mejor manera de que pueda ser
          diagnosticada. &nbsp;<i class="fas fa-square"></i></p>
