          <div style="margin: 0 0 10px;" class="descanso">Ejemplos de maloclusiones:</div>
          <p><span>Protusión maxilar y retrusión mandibular:</span> en estos pacientes los dientes superiores se encuentran adelantados respecto a los inferiores, en este tipo
            de casos es común que el paciente presente problemas respiratorios o síndrome de apnea obstructiva del sueño, el paciente refiere que ronca o que se despierta varias ocasiones al dormir.</p>

          <p><span>Retrusión maxilar y/o protusión mandibular:</span> los dientes inferiores están por delante de los superiores, en estos casos se acentúa el problema estético de la armonía facial y funcional del paciente.</p>

          <p><span>Mordida abierta:</span> se caracteriza por una falta de contacto entre los dientes de ambas arcadas, la cual puede deberse a una malposición de los dientes, de los huesos o combinación de ambos. Cuando la causa es esquelética, el tratamiento incluye cirugía.</p>

          <p><span>Asimetría facial:</span> no existe la simetría total en la naturaleza humana y eso no significa que todos los pacientes sean candidatos a operarse, sólo aquéllos que presenten un grado de asimetría facial evidente para su entorno social, profesional y familiar, y que involucren problemas oclusales.</p>

          <p><span>Sonrisa gingival:</span> cuando los pacientes al sonreír muestran una excesiva porción de encía en la arcada superior, esto puede relacionarse a un labio superior corto o porque el maxilar tuvo un crecimiento excesivo. Cuando el problema estético no se puede solucionar exclusivamente con ortodoncia, se realiza cirugía ortognática.</p>
