        <p>Al igual que en los casos convencionales de ortodoncia, en los pacientes que requieren cirugía ortognática, se les piden estudios radiográficos, tomografías y fotografías, en donde se realiza un análisis facial, funcional y cefalométricos para realizar un diagnóstico y plan de tratamiento para obtener el mejor resultado en cada caso.</p>

        <p>Posterior al diagnóstico, se inicia la ortodoncia para una vez preparado el paciente, enviarlo con el cirujano maxilofacial para programar la cirugía, una vez realizado este procedimiento, se lleva a cabo una ortodoncia postquirúrgica donde conseguimos el detallado final de la oclusión, mientras se estabilizan los movimientos óseos realizados en la operación.</p>
        <p>Una vez finalizada la ortodoncia, como en todos los pacientes, es necesario colocar aparatos de retención para evitar cualquier tipo de recidiva. &nbsp;<i class="fas fa-square"></i></p>
