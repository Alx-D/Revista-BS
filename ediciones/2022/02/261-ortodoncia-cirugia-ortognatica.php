      <div class="texto">
        <p>Hablar de ortodoncia es hablar de implementación de tratamientos en todas las edades, tanto en recién nacidos, cuando presentan labio y paladar fisurado; en edades tempranas, cuando aplicamos tratamientos de ortopedia; así como en jóvenes y adultos, en quienes diseñamos tratamientos tales como el uso de brackets.</p>
        <p>Actualmente, la ortodoncia es más solicitada por los pacientes para conseguir resultados dentales y faciales estéticos, sin embargo, la ortodoncia no se limita a este tipo de tratamientos, pues existen ciertos casos, que, dada su naturaleza, requieren de tratamientos distintos. Como ejemplo tenemos los casos donde el crecimiento de los huesos maxilares no es correcto y resulta necesario realizar un tratamiento que involucre una cirugía ortognática.</p>

        <p class="descanso">¿Qué es una cirugía ortognática?</p>
        <p>Es una intervención quirúrgica donde es posible corregir de manera muy eficiente deformidades dentomaxilofaciales mediante movimientos óseos maxilares y mandibulares para lograr un equilibrio perfecto entre todas las características faciales del paciente.</p>
        <p>Existen varios factores a valorar antes de realizar una cirugía ortognática, ya que se debe realizar un diagnóstico y plan de tratamiento valorando no sólo la estética facial, sino la función: respiración, masticación y fonación del paciente, ya que como ortodoncistas siempre buscamos lograr una armonía facial con hermosa sonrisa, estabilidad oclusal y salud periodontal a largo plazo.</p>

        <div class="foto-der-pie">
          <figure>
            <img src="/ediciones/2022/02/imgs/261-Dra-Dayana-Duron-CIRUGIA-ORTO-2.jpg" alt="">
            <figcaption class="f2 pie">Al combinar técnicas de ortodoncia y de cirugía maxilofacial es posible corregir maloclusiones en las que se ven comprometidas la estética y la función de los maxilares.</figcaption>
          </figure>
        </div>

        <p>Dado que dentro de nuestros objetivos figura conservar o corregir el factor que esté afectando la armonía de la dentadura, cuando existen casos donde los problemas dentales no sólo involucran mal posición de los dientes, sino que, los huesos de la cara crecieron de forma asimétrica, la mejor solución es la preparación prequirúrgica para posteriormente someter al paciente a cirugía de los maxilares.</p>
        <p>La necesidad de intervenir quirúrgicamente para corregir este tipo de problemas faciales se presenta cuando en uno o varios huesos del rostro, maxilar, mandíbula, mentón o pómulos, se observa un crecimiento anormal que altera la armonía facial.</p>
        <p>Estas deformidades requieren intervención quirúrgica debido a que, además de causar un desequilibrio en las facciones de los pacientes, pueden causar serios problemas funcionales: dificultad para respirar, apnea del sueño, trastornos de la articulación temporomandibular, problemas de maloclusión y digestivos, o problemas de dicción, entre otros.</p>
        <p>Someterse hoy en día a este tipo de cirugías es más sencillo que en el pasado, debido a los avances tecnológicos que resultan en planeaciones digitales y ofrecen resultados más precisos. Sin embargo, para tratar esta clase anomalías, muchas veces resulta necesario complementar la intervención quirúrgica con un tratamiento de ortodoncia, pues de este modo es posible alinear los dientes de tal modo que cuando la oclusión sea corregida por medios quirúrgicos a su posición correcta, la posición de los dientes también sea la correcta; logrando que ambas estructuras óseas coincidan entre sí, eliminando completamente el problema facial inicial.</p>
        <p>Tanto la planeación como el protocolo inicial de tratamiento del paciente es tarea de nosotros como ortodoncistas, sin embargo, en estos casos se establece una relación estrecha con el cirujano maxilofacial, ya que él es quien opera al paciente una vez que nosotros eliminamos las compensaciones dentales.</p>

        <p class="descanso">¿Cuándo es necesario realizar ortodoncia y cirugía ortognática?</p>
        <p>Está indicada en aquellos casos en los que la malformación dentofacial afecta negativamente a la vida del paciente, ya sea por cuestiones de función o estética; en muchos casos el paciente no puede respirar, comer o hablar correctamente y eso afecta directamente en la calidad de vida de quien presenta el problema.</p>
        <p>Hay diferentes tipos de maloclusiones dentales cuyo origen está en una incorrecta relación o desarrollo entre el maxilar y la mandíbula, lo aconsejable es solucionar estos problemas durante la infancia, cuando las estructuras óseas son moldeables y es posible guiar su crecimiento mediante aparatos de ortopedia, en caso de que un paciente ya haya terminado su maduración esqueletal y presente una maloclusión severa, ya no es posible un tratamiento sin cirugía ortognática.</p>

