        <p class="descanso">¿Cuál es el proceso que se realiza en este tipo de tratamiento?</p>

        <div class="foto-der-pie">
          <figure>
            <img src="/ediciones/2022/02/imgs/261-Dra-Dayana-Duron-CIRUGIA-ORTO-3.jpg" alt="">
