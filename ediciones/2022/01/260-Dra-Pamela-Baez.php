          <p>Glóbulos rojos, glóbulos blancos y plaquetas son las principales células que componen la sangre, problemas y enfermedades en cada una de ellas se manifiestan de forma particular.</p>
          <p>“La principal causa de visita a hematología es la anemia, que es la baja de la hemoglobina en sangre. Ésta suele ser benigna en la mayoría de las ocasiones, y puede tratarse de forma efectiva; sin embargo, es importante hacer una evaluación completa para que no se escape ningún detalle relevante de la salud del paciente”, explicó en charla con Revista BS la doctora <b>Pamela Báez Islas</b>.</p>
          <p>La especialista en hematología y trasplante de médula ósea señaló que otros problemas relativamente frecuentes tratados en el consultorio de hematología están relacionados con las plaquetas: ya sea una baja en éstas o formación anormal de trombos (coágulos).</p>
          <p>Existen algunos estudios de la sangre que son sencillos y con los cuales se pueden diagnosticar problemas hematológicos.</p>
          <p>“La biometría hemática completa es el estudio básico inicial de toda evaluación por el hematólogo, la complementamos con la visualización directa de la sangre al microscopio”, compartió la entrevistada.</p>
          <p>El tiempo de coagulación es otro estudio básico, se suele creer que es sólo para preparación quirúrgica, comentó, pero en general es útil para conocer el funcionamiento de la sangre; con base en los hallazgos, se pueden solicitar posteriores estudios de laboratorio o de imagen.</p>

          <p class="descanso">Preste atención</p>
          <p>De acuerdo a las células sanguíneas afectadas, las manifestaciones difieren: debilidad, cansancio con actividades sencillas, falta de concentración, palpitaciones y, en casos más severos, palidez en la piel pueden indicar anemia.</p>
          <p>“Los leucocitos son las células que nos protegen contra las infecciones, por lo que cuando éstos se afectan, los principales síntomas son infecciones de repetición o infecciones severas no esperadas”, apuntó.</p>
          <p>Cuando se presenta algún problema en las plaquetas y las proteínas de coagulación tendrán lugar moretones, sangrados de encías, de nariz o menstruaciones abundantes en el caso de mujeres.</p>
          <p>Para cuidar de la salud de la sangre, así como de la salud integral, la especialista recomendó incluir en sus chequeos anuales una biometría hemática, además, si se tienen enfermedades de la sangre ya conocidas, así como secuelas hematológicas de cáncer, la revisión periódica por el especialista en hematología es crucial.</p>
          <p>“Quiero invitar a nuestros lectores a unirse a los programas de donación altruista de sangre; todos los individuos sanos pueden compartir de su salud y de su amor apoyando a los pacientes que requieren sangre ya que con su donación nos dan mucho y no quita nada”, finalizó la doctora Báez Islas. &nbsp;<i class="fas fa-square remate-esp"></i></p>
