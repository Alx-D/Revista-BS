      <div class="texto">
        <p>En la etapa infantil, antes de terminar el crecimiento, se pueden presentar diversas alteraciones ortopédicas en los niños que es muy importante detectar a tiempo. Existen situaciones congénitas donde se presentan anormalidades como la ausencia de extremidades tanto superiores como inferiores, falta o aumento en el número de los dedos, estas situaciones se asocian a síndromes de origen genético y requieren tratamiento especifico de acuerdo a cada situación.</p>

        <p class="descanso">Al nacimiento</p>
        <p>La valoración ortopédica de un niño se puede iniciar desde su nacimiento, donde especialmente se identifica la luxación congénita de cadera, que es la falta de formación adecuada de la cadera y, por lo tanto, no está colocada en su lugar normal, si ésta se identifica a tiempo, es posible hacer un manejo de manera conservadora, es decir, sin requerir de una cirugía.</p>

        <div class="rc-color rojoc rc-der">
          <div class="rc-foto-bx">
            <img class="w1200" src="/ediciones/2022/01/imgs/260-Dr-Jose-Antonio-Gonzalez-ORTOPEDIA-NINOS-2.jpg" style="margin: 0 auto 15px; display: block;">
          </div>
          <p class="f2 rc-color-cbz-1r guion" style="color: white;">Atención</p>
          <ul class="rc-color-lista">
            <li>Verifique si hay callos o juanetes</li>
            <li>Examine cómo se sienta y se para</li>
            <li>Evalúe la marcha y postura de su hijo</li>
            <li>Observe los ángulos de rodilla y tobillos</li>
            <li>Identifique si hay extremidades dispares</li>
            <li>Preste atención a expresiones de dolor</li>
          </ul>
        </div>

        <p>Otra situación que se puede identificar desde el nacimiento es el pie equino varo, éste se presenta de manera anormal en una posición en puntilla y hacia adentro de uno o de ambos pies, al igual que la luxación congénita de cadera, si se identifica de manera temprana, puede corregirse de manera conservadora sin cirugía. Estas dos situaciones se deben comúnmente a posiciones inadecuadas del bebé dentro del útero.</p>

        <p class="descanso">Temprana infancia</p>
        <p>A partir de los dos años y medio de edad del niño se puede evaluar la marcha, ya que en ese momento la estructura del cerebro que da la fineza de la marcha y movimientos ya se encuentra desarrollada y aquí podemos identificar el pie plano, la rotación de los pies, anteversiones de caderas, acortamientos de extremidades, marcha inadecuada y ofrecer el mejor tratamiento ortopédico en ese momento, se puede iniciar con plantillas o algún aparato ortopédico temporal.</p>
        <p>Más adelante, a partir de los 10 años, debemos evaluar la postura, identificar si el niño se joroba, si los hombros están alineados o uno de ellos se encuentra más caído. En el consultorio podemos evaluar la posición normal de la columna, si existen deformidades como la escoliosis o si está bien balanceada, angulaciones inadecuadas de la pelvis; podemos evaluar las rodillas, si tienen las angulaciones adecuadas para la edad, y si los tobillos están bien balanceados.</p>
        <p>En este punto ya podemos complementar también con algún estudio radiográfico para apoyarnos en la evaluación y ésta sea de lo más completa, para así proveer un tratamiento adecuado.</p>

