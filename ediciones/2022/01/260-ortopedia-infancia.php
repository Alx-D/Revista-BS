        <p class="descanso">Desde el hogar</p>
        <p>La evaluación de los problemas ortopédicos siempre empieza en casa, observemos a nuestros hijos, hay que ver cómo caminan, cómo se paran, qué posición mantienen al estar en sus clases virtuales enfrente de la computadora, observar sus rodillas si éstas tienen mucha angulación al igual que sus tobillos, ver si sus dedos presentan alguna deformidad como callos o juanetes, y así podemos plantearnos como padres las siguientes preguntas:</p>
        <ul class="lista-disco-nota">
          <li>¿tiene dolor en alguna de sus extremidades?</li>
          <li>¿alguna de sus piernas o brazos son más cortos que el otro?</li>
          <li>¿camina correctamente?</li>
          <li>¿se sienta y se para adecuadamente?</li>
          <li>¿se joroba o tiene una mala postura?</li>
          <li>¿se le ven los hombros caídos?</li>
          <li>¿es normal que le truenen bastante y con dolor sus articulaciones?</li>
          <li>¿es normal el dolor y la fiebre nocturnas con el crecimiento?</li>
          <li>¿es normal que le salgan muchos callos o juanetes en los pies?</li>
        </ul>
        <p>Una vez identificada alguna situación y si existe alguna duda con respecto al desarrollo de nuestros pequeños, se debe acudir con el médico experto en estas situaciones como el ortopedista, ya que contamos con herramientas necesarias para valorar adecuadamente los problemas ortopédicos y así dar el tratamiento más adecuado en ese momento.</p>
        <p>Es importante detectar a tiempo cualquier situación que no sea normal en nuestros pequeños, ya que los niños en diversas etapas crecen muy rápido y un retardo en el diagnóstico pudiera requerir de tratamiento muy agresivo, como la realización de una cirugía.</p>
        <p>Así que mi recomendación es evaluar ortopédicamente a los niños por lo menos una vez al año, hasta el término de su crecimiento. &nbsp;<i class="fas fa-square"></i></p>
