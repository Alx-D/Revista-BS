        <p>Algo importante en las anemias es detectar cuando éstas son por ruptura de los glóbulos rojos y no por falta
          en la producción y que se observa con ictericia, es decir, cambio en el color de la piel a un tono amarillo
          así como oscurecimiento en la orina; éstas suelen tener manifestaciones más agudas y severas por lo que deben
          ser atendidas prontamente por el experto.</p>
        <p>Otros problemas en la sangre son aquéllos de falta de coagulación que llamamos hemofilias, y que puede ser
          por falta de proteínas que generan sangrados o disminución en el número de plaquetas. Estas alteraciones se
          manifiestan con sangrados en nariz y/o encías, moretones sin motivo aparente, o de gran tamaño para el
          estímulo que los provocó, así como petequias, que son pequeños puntos rojos en la piel.</p>
        <p>El exceso de coagulación también es un problema ya que se relaciona con trombosis e infartos en múltiples
          sitios por lo que aquellas personas con eventos de trombosis en sitios inusuales, menores de 45 años,
          problemas para lograr adecuada anticoagulación o con más de dos eventos trombóticos deben ser evaluados. Tanto
          las enfermedades hemorrágicas como las trombóticas ponen en riesgo la vida por lo que requieren una evaluación
          oportuna y eficiente.</p>
        <p>Finalmente, hablando de los leucocitos, sus alteraciones dependerán si estos cuentan con baja o alta
          producción y en múltiples ocasiones éstas suelen ser asintomáticas, detectadas en estudios de control.</p>
        <p>Si bien la mayoría de las veces, estas alteraciones suelen ser reactivas a infecciones o inflamación, son
          hallazgos que requieren de orientación médica. Dentro de las alteraciones de los linfocitos, subgrupo de
          leucocitos, encontramos el crecimiento de los ganglios, los cuales suelen detectarse como palpación de
          tumoraciones (bolitas) más frecuentemente en el cuello y arriba de las clavículas.</p>
        <p>Los signos y síntomas que requieren evaluación por el hematólogo son amplios, se podría resumir que ante
          alguna de las manifestaciones comentadas anteriormente, la realización de la biometría hemática, estudio
          básico de laboratorio, puede detectar de forma inicial la gran mayoría de los problemas en la sangre. Así, el
          lector que ya conoce quién es el experto en la sangre, ante cualquier alteración detectada deberá acudir en
          tiempo y forma a la hematóloga. &nbsp;<i class="fas fa-square"></i></p>
