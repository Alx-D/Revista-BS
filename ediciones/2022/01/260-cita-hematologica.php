      <div class="texto">
        <p>Frecuentemente encuentro en la población un desconocimiento por el papel de la hematología por lo que considero
          necesario exponer cuál es trabajo de los hematólogos y hematólogas, y así lograr identificar los signos y síntomas que
          requieren ser atendidos por esta especialidad.</p>
        <p>Hematología se deriva del griego “haimato”, que significa sangre, y “logia”, que quiere decir tratado; se
          define entonces como la rama de la medicina que estudia la naturaleza, función y enfermedades de la sangre,
          así como los órganos donde se forma.</p>
        <p>Dicho en una forma más extensa, los hematólogos estudiamos todo lo que se relacione con la sangre, la cual se
          conforma por tres grandes grupos celulares: los glóbulos rojos, llamados eritrocitos que se encargan de
          acarrear el oxígeno a todo nuestro cuerpo; las plaquetas, que tienen la acción de formar coágulos; y
          finalmente los glóbulos blancos, llamados leucocitos, y que se subclasifican en linfocitos, neutrófilos,
          eosinófilos, basófilos y monocitos, y cuya función es protegernos contra los gérmenes del medio externo con
          actividad celular directa o producción de anticuerpos.</p>
        <p>Este conjunto de células se encuentran mezcladas en un líquido llamado plasma en el cual se encuentran
          flotando diversas sustancias también necesarias para el funcionamiento de la sangre como los anticuerpos y
          proteínas de la coagulación y anticoagulación.</p>
        <p>Adicional a esto, hay que señalar que la hematología también estudia los órganos donde nuestras células
          sanguíneas se crean y maduran, los cuales son la médula ósea en el interior de los huesos y los tejidos
          linfáticos como el timo, los ganglios y el bazo.</p>

        <p class="descanso">Campo de acción</p>
        <p>Así pues, los especialistas en hematología estudian desde anemias por deficiencia de nutrientes, que suelen
          curarse de forma rápida y sencilla, hasta enfermedades con manejos más complejos como mieloma múltiple,
          linfomas, leucemias e incluso realizan trasplantes de médula ósea.</p>
        <p>Con este resumen, mi intención es extender el conocimiento de lo amplio que es el estudio de la sangre y las
          múltiples manifestaciones corporales que puede dar el inadecuado funcionamiento de ella, simplemente invito al
          lector a pensar ¿Dónde no cuenta usted con sangre?</p>
        <p>Por mucho, las anemias son el motivo más frecuente de consulta en hematología y que puede deberse a múltiples
          causas. Tiende a manifestarse con cansancio rápido, palpitaciones, taquicardia, somnolencia o insomnio. En el
          caso de que la causa de la anemia sea deficiencia de hierro (motivo más frecuente de anemia a nivel mundial),
          suele presentarse también caída de cabello, uñas frágiles, fisuras en la boca y alteraciones en el gusto.</p>
        <div class="foto-cnt-pie">
