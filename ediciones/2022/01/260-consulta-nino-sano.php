      <div class="texto">
        <p>En el área de la medicina el término de consulta de niño sano es algo que se viene utilizando décadas atrás.
          Básicamente consiste en dar seguimiento sistemático a los pacientes pediátricos con el fin de confirmar que se
          encuentran en buen estado de salud, y poder detectar hábitos, comportamientos, síntomas o enfermedades de
          manera temprana.</p>
        <p>La consulta del niño sano va variando según las etapas del crecimiento, aunque el fin es el mismo: preservar
          la salud. El modo varía ya que no es lo mismo una cita de control de niño sano de un recién nacido a la de un
          adolescente.</p>
        <p>Generalmente se inicia con un interrogatorio, donde se busca conocer el estado del paciente durante los
          últimos meses, recordar datos importantes como antecedentes de enfermedades que tiene o ha tenido, se
          interroga específicamente buscando focos rojos.</p>
        <p>Posteriormente, se procede a un chequeo general del estado físico del cuerpo que consiste la exploración,
          ésta se enfoca más en ciertos aspectos, según la edad del paciente. La exploración consiste tanto en buscar
          que todo esté en correcto estado, como en buscar intencionalmente algún indicio de algo anormal.</p>
        <p>Datos básicos que se deben obtener siempre en consulta de niño sano son el peso y la estatura del paciente,
          éstos se grafican para saber si están dentro de parámetros normales de crecimiento, esto aporta información
          relevante del estado físico del paciente.</p>
        <p>En recién nacidos y durante los primeros años es muy importante medir el perímetro de la cabeza, esto también
          aporta datos de un buen crecimiento y, ante alteraciones de la misma, nos vemos obligados a estudiar a fondo
          estas alteraciones, lo mismo con el peso y estatura.</p>

