        <p class="descanso">Constancia</p>
        <p>La periodicidad de estas consultas va a depender en gran parte del estado de salud del paciente y de la
          decisión del médico, ya sea porque hay indicios de alguna anormalidad o una enfermedad establecida que
          requiera de seguimiento frecuente, aunque ya no entraría en consulta del niño sano.</p>
        <p>En términos generales, la primera consulta sería en los primeros días de vida, del quinto al séptimo día;
          posteriormente, al mes de vida, luego a los dos meses, luego cada dos meses hasta el año de vida.</p>
        <p>Seguimos del primer al segundo año cada tres meses, luego, al cumplir los dos años es una consulta a los seis
          meses y, por último, después de los tres años de edad es una vez por año.</p>
        <p>En la consulta del niño sano se pueden detectar todo tipo de irregularidades: problemas físicos, mentales,
          hábitos alimenticios, comportamientos extraños, retraso en los hitos del desarrollo, etcétera.</p>
        <p>De ahí la importancia de acudir siempre a las consultas de control, ya que si se retrasa la detección de
          alguna de estas irregularidades, es tiempo perdido para actuar rápidamente, y a veces esto puede tener
          consecuencias irreversibles.</p>

        <p class="descanso">Trabajo en conjunto</p>
        <p>La idea del control del niño sano es que tanto padres como pediatras busquemos lo mejor para el paciente. No
          se trata de una mera exploración, hay que resolver todas las dudas que tengan los padres, empoderarlos de
          diferentes maneras para que sepan actuar ante situaciones de riesgo y sepan detectar focos de alarma, por eso
          la importancia de que el pediatra informe muy bien a los padres sobre la etapa en la que está el niño, lo que
          puede suceder, cambios que van a notar, a qué deben estar atentos; de esta manera el pediatra hace equipo con
          los padres.</p>
        <p>Por último, aunque uno piense que su hijo está en perfecto estado de salud y no hay necesidad de revisión,
          siempre alguien externo y capacitado estará mejor preparado para saber qué buscar y qué detectar según la
          etapa de crecimiento de su hijo y de esa manera prevenir. &nbsp;<i class="fas fa-square"></i></p>
