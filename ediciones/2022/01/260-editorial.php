      <div class="texto">
        <p>Primeramente, les deseo muy feliz año 2022 con salud y bienestar sobre todas las cosas.</p>
        <p>El director general de la Organización Mundial de la Salud (OMS), Tedros Adhanom Ghebreyesus, al inicio del
          año externó con preocupación que durante la semana del 27 de diciembre de 2021 al 2 de enero de 2022, tras un
          aumento gradual desde octubre, el número mundial de nuevos casos de Covid-19 aumentó bruscamente en un 71%, en
          comparación con la semana anterior, registrando casi 9.5 millones de nuevos contagios y más de 41 mil muertes,
          pese a que los decesos disminuyeron un 10%.</p>
        <p>En este inicio del año 2022 se han visto frenadas muchas expectativas personales y sociales ante el temor, la
          enfermedad y los gastos de bolsillo en salud inesperados para prevenir, realizarse pruebas diagnósticas o bien
          para recuperar la salud.</p>
        <p>También en el país y en Sonora en la primera semana del año se incrementaron los casos de Covid-19, superando
          en varios días consecutivos el número de casos máximos históricos registrados.</p>
        <p>Este aumento inusitado se le atribuye en gran parte a la nueva variante ómicron, del virus SARS-Cov-2 que
          hasta ahora se ha comentado que es más contagiosa, Tedros Adhanom Ghebreyesus señaló: “Aunque la variante
          ómicron parece revestir menos gravedad que la delta, sobre todo en personas vacunadas, ello no significa que
          deba ser catalogada de 'leve'”.</p>
        <p>Si bien en nuestro estado aún la variante delta es predominante, de acuerdo a la conferencia de prensa del 13
          de enero del 2022 de la Secretaría de Salud de Sonora, cualquiera de ellas representa un riesgo para la salud,
          más en personas no vacunadas y en personas que viven con enfermedades crónicas (diabetes, hipertensión
          arterial, obesidad, enfermedad pulmonar, asma, cardiopatías, entre otras) o factores de riesgo (consumo de
          tabaco, consumo de alcohol, sedentarismo, sobrepeso, dieta inadecuada).</p>

        <p class="descanso">Prevención y atención</p>
        <p>Ante este aumento de casos para limitar la trasmisión es muy importante, además de la vacunación, las medidas
          preventivas como el uso de cubrebocas, lavado frecuente de manos, conservar una sana distancia cuando menos de
          1.5 metros, estornudo de etiqueta, ventilación en lugares cerrados y evitar aglomeraciones.</p>
        <p>Consulte a su médico ante la sospecha de enfermedad respiratoria viral, ante los siguientes datos: persona de
          cualquier edad que en los últimos diez días haya presentado al menos uno de los siguientes signos y síntomas:
          tos, disnea (dificultad para respirar), fiebre o dolor de cabeza acompañados de al menos uno de los siguientes
          signos o síntomas: dolor muscular o de articulaciones, dolor de garganta, escalofríos, dolor torácico,
          congestión nasal o rinorrea, aumento de frecuencia respiratoria, pérdida del olfato o gusto, conjuntivitis. En
          menores de cinco años de edad, la irritabilidad puede sustituir al dolor de cabeza.</p>
        <p>Es muy importante el control médico periódico de quienes viven con padecimientos crónicos. De acuerdo a los
          resultados de Encuesta Nacional de Salud y Nutrición (ENSANUT) 2018, en México el porcentaje de la población
          de 20 años y más con diagnóstico médico previo de diabetes es de 10.3%. En Sonora es de 11.2%. Igualmente,
          para hipertensión arterial es de 18.4% en México y en Sonora, de 24.6%.</p>
        <p>Esta encuesta refiere que en México en 2018 el porcentaje de adultos de 20 años y más con sobrepeso y
          obesidad es de 75.2%. En población de 12 a 19 años de edad el sobrepeso y obesidad de manera conjunta es de
          38.4%. Sonora es de los cinco estados con mayor obesidad en el país con un porcentaje de 22.2% en este último
          grupo de edad.</p>
        <p>El porcentaje de la población de 20 años y más que consume tabaco en Sonora es de 13.9%, el promedio nacional
          es de 10.1%. El tabaquismo causa diversas enfermedades como diferentes tipos de cáncer, infartos, accidentes
          cerebrovasculares y enfermedades respiratorias, como la enfermedad pulmonar obstructiva crónica. Durante el
          embarazo aumenta el riesgo de bajo peso al nacer y parto prematuro.</p>
        <p>El consumo de alcohol es un factor de riesgo en el aumento de accidentes y lesiones, violencia, homicidio,
          suicidio y trastornos mentales, más que cualquier otra sustancia psicoactiva. En Sonora las personas de 20
          años o más, diario o semanalmente ha consumido alcohol el 22.7%. &nbsp;<i class="fas fa-square"></i></p>
