        <p class="descanso">¿Cuándo recurrir a un geriatra?</p>
        <p>Por lo anterior y mucho más, un médico geriatra o gerontólogo está capacitado para poder brindar la atención
          y orientación necesarias tanto al paciente como a su entorno social, con el objetivo de procurar su salud y
          acrecentar su calidad de vida.</p>
        <p>Idealmente, se sugiere que todo adulto a partir de los 60 años acuda una vez al año a una valoración
          geronto-geriátrica integral, para identificar de forma oportuna los problemas y necesidades del mayor, con el
          fin de elaborar un plan de tratamiento y seguimiento individualizado.</p>
        <p>El control anual permite emitir recomendaciones en el estilo de vida, incluyendo sugerencias nutricionales,
          de ejercicio físico acorde a las condiciones del paciente, vacunas necesarias para su edad; y nos facilita
          además la detección precoz de alteraciones en la memoria o capacidad cognitiva, trastornos del sueño y/o
          depresión, y alertarnos de sintomatología temprana en enfermedades como el cáncer de próstata o de mama, y en
          algunos casos la necesidad de cuidados paliativos.</p>
        <p>En el caso de los ancianos con patologías crónicas diagnosticadas, como diabetes e hipertensión arterial, en
          quienes ingieren más de cinco fármacos diferentes o en los pacientes con los mencionados síndromes
          geriátricos, se recomienda una vigilancia estrecha mensual o bimestral por parte del geriatra para aminorar
          las complicaciones y evitar interacciones medicamentosas que repercutan en el bienestar del paciente.</p>
        <p>La geriatría es compañera para la prevención, diagnóstico oportuno, tratamiento y rehabilitación
