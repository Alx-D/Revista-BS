        <p class="descanso">Nuestra labor</p>
        <p>La gerontología y la geriatría son las especialidades que estudian el proceso del envejecimiento tanto en la
          esfera física, como mental y social; los cambios que suceden y las formas de adaptación a ellos, así como la
          comprensión y atención a las enfermedades que aparecen con mayor frecuencia en los adultos mayores.</p>
        <p>Existen términos muy exclusivos para este grupo etario, como los síndromes geriátricos, entre los cuales
          destacan la polifarmacia, la desnutrición, la inestabilidad y el riesgo de caídas, la inmovilización
          prolongada y las úlceras por presión, el delirium, la incontinencia urinaria, entre otros, que son cuestiones
          que generalmente en los demás periodos de la vida no se tienen presentes.</p>
        <p>Del mismo modo, hay escalas muy particulares para la evaluación del estado de salud del adulto mayor, su
          funcionalidad es un pilar clave y se mide con la capacidad de realizar actividades básicas e instrumentales de
          la vida diaria.</p>
        <p>Por otro lado, la valoración del estado cognitivo también cuenta con tamizajes especiales que orientan hacia
          deterioros leves o probables demencias.</p>
        <p>La revisión de la agudeza visual y auditiva, así como el estado de salud bucal son cruciales para disminuir
          accidentes y malnutrición; y los valores de la relación entre el peso y la talla (índice de masa corporal) es
          diferente también en comparación los adultos jóvenes.</p>
        <p>Igualmente, cobra sentido gigante el papel de los cuidadores formales o los familiares que toman dicha
          responsiva, siendo el reto y misión primordial crear conciencia en ellos de que todas las acciones deben
