          mayor haga y participe en todo lo que pueda, y que no se le limite sólo por comodidad del cuidador.</p>
        <p>Y desde luego, revisar aspectos tan mágicos como la satisfacción y el compromiso con la vida son parte clave
          de la atención geriátrica.</p>

