      <div class="texto">
        <p>El oncólogo es el médico encargado del diagnóstico, tratamiento y seguimiento de los pacientes con cáncer.
          Ésta es una enfermedad heterogénea que se produce cuando algunas de las billones de células que componen
          nuestro cuerpo pierden su ciclo natural de vida y empiezan a reproducirse células anormales de forma
          descontrolada, ocasionando que el cuerpo funcione de forma diferente a como debería hacerlo.</p>
        <p>
          Por lo general, el cáncer durante sus inicios no da síntomas y cuando presenta una alteración considerable en
          el funcionamiento del cuerpo inician las manifestaciones de la enfermedad en el organismo.
          Existen muchos tipos de cáncer, ya que se puede originar en cualquier parte del cuerpo. Generalmente se
          clasifican en dos categorías principales: cánceres hematológicos y tumores sólidos. Cada una de estas
          enfermedades presenta manifestaciones clínicas diferentes y es muy variable la presentación en cada persona.
        </p>

        <div class="texto-resaltado-bx">
          <p class="descanso" style="margin-top: 0;">Comprender los síntomas</p>
          <p>En general, cualquier tipo de cáncer en etapas tempranas no da síntomas y frecuentemente, cuando éstos se llegan a
            presentar, pueden ser muy inespecíficos. Los signos y síntomas del cáncer dependerán de su ubicación, tamaño,
            afectación de órganos alrededor o la presencia de metástasis (propagación a distancia).
            Los síntomas inespecíficos que pueden asociarse a la presencia de cáncer pueden ser la fiebre persistente, cansancio
            extremo o pérdida de peso. Esto puede deberse a que las células tumorales consumen gran parte del suministro de
            energía del cuerpo o que liberen sustancias que afectan la forma en que el cuerpo produce su energía.</p>
          <p>No obstante, la mayoría de estos síntomas pueden deberse a otros padecimientos diferentes al cáncer, pero si
            persisten, la recomendación es que acuda a su médico a determinar la causa.</p>
          <p>A continuación, se mencionan algunos de los signos y síntomas más comunes que se pueden presentar como causa de
            algún cáncer:</p>
          <ul class="lista-disco-nota">
            <li>Cansancio extremo o debilidad generalizada que no mejora con el reposo.</li>
            <li>Pérdida de peso mayor a 10 kilogramos sin razón aparente.</li>
            <li>Pérdida del apetito, alteraciones en la deglución, dolor al tragar, dolor abdominal, náuseas y vómitos.</li>
            <li>Presencia de una nodulación o endurecimiento del seno.</li>
            <li>Aparición de una protuberancia en cualquier parte de las extremidades con crecimiento progresivo en corto
              periodo de tiempo o mayor a 5 centímetros.</li>
            <li>Dolor de aparición reciente, que no cesa a tratamiento común y que es progresivo.</li>
            <li>Cambios en la coloración de la piel, crecimiento de lunares en corto tiempo, la presencia de ulceración o
              sangrado en la piel.</li>
            <li>Aparición de coloración amarillenta en la piel o coloración amarilla en los ojos.</li>
            <li>Tos crónica o irritación de la garganta que no desaparece.</li>
            <li>Sangrado inusual o presencia de hematomas (moretes) sin razón aparente.</li>
            <li>Cambios en los hábitos intestinales (estreñimiento crónico o diarrea), o cambios en el aspecto de las heces.
            </li>
            <li>Sangrado al orinar, o la necesidad de orinar de forma frecuente.</li>
            <li>Fiebre o sudoración persistente de predominio nocturno.</li>
            <li>Dolor de cabeza persistente, asociado a cambios en la visión o audición.</li>
            <li>Úlceras en la boca, labios o mejillas con sangrado, dolor y una aparición mayor a 2 centímetros.</li>
            <li>Presencia de abultamientos palpables en el abdomen, senos o extremidades.</li>
            <li>Sangrado transvaginal no asociado a la menstruación o presencia de sangrado transvaginal en mujeres
              postmenopáusicas.</li>
            <li>Sangrado rectal asociado a dolor rectal.</li>
          </ul>
