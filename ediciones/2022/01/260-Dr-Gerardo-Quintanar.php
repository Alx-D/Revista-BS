          <p>Abarcando el sistema urinario de ambos sexos y el reproductivo masculino, la urología cuenta con un amplio
            campo de acción que comprende tanto lo clínico como lo quirúrgico.</p>
          <p>Para los profesionales de esta rama médica actuar desde la prevención es una prioridad, explicó en
            entrevista con Revista BS <b>Gerardo Quintanar Fimbres</b>, especialista en urología.</p>
          <p>“La importancia de acudir a su visita periódica con el urólogo es poder atender oportunamente las
            enfermedades del sistema urinario y genital masculino, y así prevenir su deterioro”, apuntó el entrevistado.
          </p>
          <p>Para poder atender a tiempo las diversas patologías masculinas es necesaria su detección oportuna, por
            ello, Quintanar Fimbres señaló tres momentos clave para acudir con su especialista.</p>
          <p>“En el momento de sentir inquietud o dudas; al encontrar anormalidades en los genitales; y desde los 40
            años para la detección oportuna de cáncer de próstata”, enlistó.</p>
          <p>Respecto a esa malignidad, el profesional de la salud hizo énfasis en la importancia de la consulta
            urológica para su detección oportuna, pues se trata de un padecimiento que no se puede prevenir, pero que sí
            se puede controlar e, inclusive, curar.</p>
          <p>“El cáncer de próstata no es una enfermedad que se puede prevenir, sin embargo, cuando logramos detectarlo
            a tiempo, sí podemos curarlo y, en los demás casos, podemos ofrecer tratamientos para evitar su progresión”,
            señaló.</p>

          <p class="descanso">Cuidado integral</p>
          <p>Recordando que el sector al que atienden los urólogos comprende también mujeres y niños, el doctor
            Quintanar Fimbres comentó algunos signos y síntomas que requieren acudir con este especialista de forma
            inmediata.</p>
          <p>“Cuando se presenta sangrado en la orina, también si hay un aumento en las veces para ir al baño, y en los
            casos en que se presenta dolor en genitales masculinos de forma súbita”, destacó.</p>
          <p>Enfermedades venéreas, cálculos urinarios y problemas prostáticos son los tres principales padecimientos
            que el entrevistado atiende en su consulta; para su apropiado diagnóstico, el examen de orina y el antígeno
            prostático son los exámenes básicos de rutina, que posteriormente pueden ser complementados con otros
            estudios.</p>
          <p>Si bien, aún existen tabús al respecto de atender padecimientos de los sistemas urinario y reproductor, el
            experto invitó a no dejarse llevar por vergüenza y prejuicios, y consultar a tiempo cualquier molestia, por
            intrascendente que pueda parecer.</p>
          <p>“Diagnósticos oportunos mejoran la calidad de vida, no deje pasar tiempo con dudas o malestares por pena”,
