        <p>La prevención de los factores de riesgo (FR) asociados a las enfermedades no transmisibles (ENT) debe ser el objetivo central de cualquier estrategia de política pública. Los hallazgos de la <span>Encuesta Nacional de Salud y Nutrición 2021 sobre Covid-19 </span>permiten disponer de información de suma relevancia para la detección, monitoreo y atención de los principales problemas de salud de la población, así como de sus factores de riesgo.</p>

        <p class="nt-descanso">Algunos resultados</p>
        <p>La prevalencia de sobrepeso y obesidad en población de 20 o más años en México fue de 72.4%: 75% en mujeres y 69.6% en hombres. La hipertensión arterial fue de 26.4% para mujeres y 30.5% para hombres.</p>
        <p>A partir de muestras de sangre venosa, la prevalencia de diabetes en adultos fue de 15.8%, constituyendo un 5.4% el hallazgo en la encuesta. De 32.1% de la población con colesterol alto, el 18% no contaba con un diagnóstico, mientras que del 51.4% con triglicéridos altos, el 35.9% desconocía esta condición.</p>
        <p>Según Rivera Domarco y Cols., en la <span>Obesidad en México, INSP, 2018</span>, La Estrategia Nacional para la Prevención y el Control del Sobrepeso, la Obesidad y la Diabetes ha sido insuficiente para modificar el entorno alimentario y de actividad física.</p>
        <p>Su componente educativo tampoco ha logrado motivar a la población a adoptar una alimentación más saludable y realizar más actividad física. Estos dos elementos, así como la modificación del entorno y la motivación de cambio, son indispensables para revertir las altas prevalencias de sobrepeso y obesidad en México.</p>
        <p>La mayoría de los factores de riesgo que favorecen el desarrollo de las ENT son prevenibles. Se debe fortalecer el proceso orientado a generar conciencia, en el ámbito intersectorial e interinstitucional respecto de las ENT y la salud mental.</p>
        <p>De manera preventiva, se deben desarrollar acciones para minimizar los FR que son modificables; por ejemplo, el consumo de alcohol, el tabaco, la dieta y la inactividad física son los FR más importantes que se deben atender para disminuir las ENT y sus complicaciones.</p>

