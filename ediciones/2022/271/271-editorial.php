        <p>La prevalencia de sobrepeso y obesidad en la población de 12 a 19 años se encontró en 42.9%, lo cual resulta compatible con el porcentaje de adolescentes que reportaron consumir bebidas endulzadas de manera habitual (91.7%), proporción que resulta mayor a la encontrada para agua (77.9%).</p>
        <p>Respecto a la prevalencia de obesidad por región en adolescentes, Sonora se encuentra en la región con más alta prevalencia de obesidad, la Pacífico-Norte con (32%), región en donde los hombres adolescentes presentaron la más elevada prevalencia de obesidad (43.5%).</p>
        <p>En el módulo Covid-19 de la encuesta, 27.7% reportó que realizaban actividad física o practicaban algún deporte antes de la pandemia. De esa fracción, el 68.9% indicó que su actividad disminuyó, siendo el grupo de los adolescentes de 10 a 14 años el que mostró mayor reducción (71.5%).</p>

        <p class="nt-descanso">¿Qué hacer?</p>
        <p>Una dieta saludable y sostenible debe iniciarse desde los primeros años de vida. Se debe proteger, promover y apoyar la lactancia materna exclusiva en los primeros seis meses de vida e introducir a partir de esa edad alimentos frescos y locales a la dieta, y continuar amamantando hasta los dos años o más si así se desea.</p>
        <p>Otros temas relevantes de salud para seguimiento es la vacunación universal. El 27.5% de niños de un año tuvieron un esquema completo. La tercera parte de los niños de uno y dos años tuvieron un esquema de cuatro vacunas.</p>
        <p>Sobre la atención prenatal de las madres de menores de dos años, el 86.3% indicó que contaron con un control adecuado de mínimo cinco consultas y el 98.8% tuvo un parto atendido por personal del área médica.</p>
        <p>Continúe con los cuidados para enfermedades respiratorias mediante el lavado frecuente de manos, estornudo de etiqueta, conservar una sana distancia, aislamiento voluntario, saludo a distancia, no saludar de beso, de mano o abrazo, uso de cubrebocas en espacios cerrados. Vacúnese contra influenza.</p>
