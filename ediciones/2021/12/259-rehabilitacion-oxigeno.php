        <p>El evento vascular cerebral es un daño directo a las estructuras cerebrales por deficiencia de oxígeno,
          causando así una isquemia al tejido cerebral, afectando su funcionamiento físico, psicológico, cognitivo y de
          lenguaje. Una de las características principales de los pacientes con evento vascular cerebral es que
          desarrollan una hemiplejia, que es la parálisis de la mitad del cuerpo generalmente de forma contralateral a
          la lesión.</p>
        <p>Al verse afectada una zona de difícil acceso, el cerebro, se ha limitado a tratar al paciente con terapia
          física y rehabilitación según la evolución de la patología de cada paciente, es decir, lo que ésta nos permita
          avanzar según su autorecuperación.</p>
        <p>Pero, por qué esperar a una mejoría que el propio organismo propone, siendo lenta y de largo plazo, cuando
          existen zonas con tejido cerebral muerto y otras zonas en penumbra isquémica luchando por sobrevivir.</p>
        <p>Durante la sesión de oxigenación hiperbárica, el paciente respira oxígeno puro al 100%, mientras se eleva la
          presión atmosférica dentro de la cámara con la finalidad de que el oxígeno respirado se diluya en el plasma
          sanguíneo y pueda llegar a los sitios donde no pudo llegar a través de la sangre, rescatando así las células
          neuronales que se encuentran en la penumbra isquémica, luchando por sobrevivir; promoviendo así la plasticidad
          neuronal y la creación de nuevos vasos sanguíneos (angio-génesis), evitando que se extienda la muerte del
          tejido cerebral y la instalación de secuelas permanentes importantes.</p>
