        <p>Decidimos dedicar el último número del año de Revista BS a revisar los avances en salud que son motivo de celebrar. Por ello, y aprovechando que nos encontramos aún en el mes en el que se recuerda la incansable lucha contra el SIDA, compartimos con usted, lector, algunos de los grandes logros de esta batalla.</p>
