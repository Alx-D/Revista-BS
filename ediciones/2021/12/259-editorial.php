        <p>A cuatro décadas, hay varios avances destacables en plataformas diagnósticas, servicios de salud, derechos humanos y, sobre todo, en tratamiento antirretroviral (ARV); atribuidos principalmente a las actividades de colectivos, sociedad civil, investigadores y académicos.</p>
        <p>De los logros más significativos es el llamado PrEP (Profilaxis Pre-exposición), que son medicamentos altamente eficaces para prevenir la infección por el VIH, indicado para las personas que están en mayor riesgo de contraer el virus.</p>
        <p>A efecto del tratamiento antirretroviral (ARV), actualmente una persona puede tener su carga viral en la sangre tan baja que no se detecta, incluso es incapaz de transmitir el VIH a otra persona. Esto no quiere decir que se eliminó el virus de su organismo, pues recordemos que esta infección no se ha logrado eliminar.</p>
        <p>Las mayores expectativas para evitar la transmisión se centran en el desarrollo de una vacuna y de acciones focalizadas eficientes para la detección oportuna en población clave y grupos vulnerables, ya que nuevas infecciones pueden ser evitables, como en el caso de recién nacidos de mujeres con VIH que, por un diagnóstico tardío, no se logra evitar su transmisión a pesar de contar con insumos para detección y acceso al tratamiento universal garantizado.</p>

        <p class="descanso">Camino por andar</p>
        <p>A cuarenta años de la epidemia, el estigma, la discriminación y la vulnerabilidad de derechos humanos siguen siendo lapidantes en todos los aspectos de la respuesta al VIH y prevalecen entre personal de salud y las instituciones.</p>
        <p>Desde el inicio de esta epidemia, a nivel mundial son casi 80 millones de personas infectadas con VIH, de las cuales 36.3 millones han fallecido por causas relacionadas al SIDA.</p>
        <p>En el año 2020 había 37.7 millones de personas que vivían con VIH, siendo 1.5 millones de infecciones nuevas y 680 mil defunciones asociadas al SIDA. Se estima que alrededor de 6.1 millones de personas viven con VIH sin saberlo.</p>
        <p>De las 37.7 millones de personas viviendo con VIH (PVVIH), 36 millones son adultos y 1.7 millones son menores de 14 años. Siendo el grupo de mujeres y niñas el mayor, con 53% de los casos.</p>
        <p>Al mes de junio de 2021 la cantidad de personas con acceso a ARV era de 28.2 millones en comparación con 7.8 millones que había en 2010. La proporción de personas con acceso al tratamiento en el año 2020 fue de 73% y, específicamente, en mujeres embarazadas fue de 85%.</p>
        <p>En México, el primer caso de VIH se identificó en 1983, desde entonces y hasta noviembre de 2021, se han notificado 328 mil 791 personas con VIH, de ellas, 201 mil 439 se encontraban vivas (61.3%), 111 mil 229 ya fallecieron (33.8%).</p>
        <p>La prevalencia de VIH al cierre del 2020 estimó que 4 de cada mil personas vivían con VIH al final de ese año. La tasa de mortalidad general en 2019 fue de 4.2 defunciones por cada 100 mil habitantes, 1.5 en mujeres y 1.7 en hombres.</p>
        <p>Al 30 de junio del 2021, existen cerca de 205 mil personas en tratamiento antirretroviral en todas las instituciones del sector salud. Las mujeres representan el 16% del total con 32 mil 924 en tratamiento.</p>
        <p>En este año, en Sonora se presentan 6 casos nuevos de VIH por cada 100 mil habitantes, mientras que, fallecen 4 personas por casusas relacionadas al SIDA por cada 100 mil habitantes. Y se estima que 74 de cada 100 mil habitantes viven con la enfermedad. &nbsp;<i class="fas fa-square"></i></p>
