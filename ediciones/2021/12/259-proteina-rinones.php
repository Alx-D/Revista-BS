        <p>Sin embargo, a pesar de lo dicho anteriormente, algunos nutriólogos deportivos sugieren que el elevado
          requerimiento proteico de los atletas puede ser cubierto con una dieta natural, por lo que generalmente los
          suplementos proteicos no son necesarios.</p>
        <p>La suplementación con creatina ha demostrado beneficios en el aumento de la masa muscular y de su contenido
          en los músculos, en el desarrollo de mayor potencia y aumento en las fuerzas durante el ejercicio y menor
          fatiga durante los entrenamientos.</p>
        <p>Desde que su uso se hizo masivo entre los deportistas se ha planteado que su suplementación pudiera tener
          efectos deletéreos sobre la función renal.</p>

        <p class="descanso">Esclareciendo</p>
        <p>¿Puede la suplementación de creatina y suplementos proteicos simular una enfermedad renal? Sí. En los
          últimos años es frecuente que los laboratorios clínicos, cuando se les solicita una determinación de
          creatinina (CRN) sérica, informen la velocidad de filtración glomerular estimada (eVFG), calculada con
          fórmulas que incluyen entre sus componentes la concentración de creatinina.</p>
        <p>Por ello, cualquier factor que eleve su concentración independiente de cambios en la función renal,
          originará una estimación de la eVFG inferior a la real, pudiéndose diagnosticar erróneamente una enfermedad
          renal o insuficiencia renal, con consecuencias emocionales importantes para los pacientes, sobrecarga
          asistencial y aumento en los gastos de los sistemas de salud (interconsultas a especialistas, repetición de
          exámenes, solicitud de imágenes).</p>
