        <p>Existen en la literatura varias comunicaciones acerca del diagnóstico erróneo de nefropatía y de sospecha de
          nefrotoxicidad por drogas, originadas en la suplementación de creatina y suplementos proteicos con fines
          deportivos. La creatina no es considerada un medicamento y por ello habitualmente su ingesta no es mencionada
          por los pacientes ni tampoco es preguntada por los médicos.</p>
        <p>En los sujetos sanos que usan suplementos de creatina o suplementación con proteínas por tiempos prolongados
          ¿se deteriora su función renal? No. Una investigación efectuada en atletas de ejercicios de resistencia
          entrenados por no menos de un año y con una alta ingesta proteica (1.2 a 3.1 g/kg/día) a quienes se les
          administró creatina en dosis de carga y de mantención o placebo por 12 semanas, no mostró cambios en las
          concentraciones de urea y creatinina sérica ni en la velocidad de filtración glomerular. Concluyeron que los
          suplementos dietéticos eran seguros y que no afectaban la función renal. &nbsp;<i class="fas fa-square"></i>
