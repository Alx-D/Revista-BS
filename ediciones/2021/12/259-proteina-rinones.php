      <div class="texto">
        <p>Normalmente, los adultos excretan menos de 150 a 200 mg al día de proteínas en la orina: la microproteinuria
          es cuando la excreción es de 30 a 300 mg de albúmina (proteína en mayor concentración en la orina) por gramo
          de creatinina: y la proteinuria, cuando la excreción excede de los 300 mg de albúmina por gramo de creatinina.
        </p>
        <p>El análisis de proteínas en orina se ha convertido en un método diagnóstico extensamente utilizado, debido a
          que puede ser un marcador temprano para algunos tipos de nefropatías, incluso cuando la tasa de filtración
          glomerular es normal.</p>
        <p>Los suplementos proteicos son uno de los suplementos ergogénicos más utilizados por los atletas de alto
          rendimiento, puesto que se cree que incrementan la masa muscular al prevenir el catabolismo proteico durante
          la actividad física.</p>
        <p>No obstante, se ha postulado que su consumo pudiese causar insuficiencia renal crónica al incrementar la
          presión glomerular y promover una hiperfiltración. Así mismo, existe el antecedente de dos reportes de casos,
          según los cuales se ha asociado el consumo de suplemento proteico con la exacerbación de daño renal
          preexistente.</p>
        <p>Pese a lo mencionado, existen publicaciones que señalan que el consumo de suplementos proteicos en atletas
          sanos no presenta algún peligro para la función renal. También se ha postulado que una dieta alta en proteínas
          en individuos sanos, atletas o no atletas, no conlleva un riesgo para la función renal.</p>

        <p class="descanso">Niveles</p>
        <p>Diferentes estudios han demostrado que el requerimiento proteico varía dependiendo de la actividad, la edad,
          el género y las condiciones fisiológicas especiales de cada individuo, y es mayor en atletas, niños y
          ancianos.</p>
        <p>Se ha sugerido que los atletas que llevan a cabo un entrenamiento anaeróbico requieren de 1.6 a 1.7 g de
          proteína por kg de peso corporal, mientras que los atletas que llevan a cabo un entrenamiento aeróbico
          necesitan de 1.2 a 1.4 g de proteína por kg de peso, lo cual está de 150 a 200% por encima de la cantidad
          diaria recomendada en no atletas, esto adjudicado en parte por el gran requerimiento calórico de la actividad
          física, pero también por la proteólisis que sufren los músculos y tendones durante la actividad física
          extensa.</p>
        <div class="foto-cnt-pie">
