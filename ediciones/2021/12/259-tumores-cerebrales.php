      <div class="texto">
        <p>Los tumores cerebrales ocupan el segundo lugar por frecuencia dentro de todas las neoplasias infantiles, sólo
          por debajo de las leucemias agudas, y son el tumor sólido que mayormente se presenta en menores de 15 años.
        </p>
        <p>Impactan de forma importante en la sociedad y en cada una de las familias afectadas. Se presenta en niños de
          todas las edades, sin embargo, la mayoría suelen aparecer en escolares.</p>
        <p>Existen algunas clasificaciones simplificadas para identificar al amplio número de tumores, ya sea por su
          localización, su extensión o su histología.
          Los tumores más comunes son los astrocitomas, méduloblastomas, ependimomas, craneofaringiomas, tumores
          germinales, entre otros.</p>

        <p class="descanso">¿Cómo detectarlos oportunamente?</p>
        <p>Conociendo los signos y síntomas de la disfunción neurológica, puesto que varían de acuerdo a la edad del
          paciente, el sitio de origen y el tamaño de la tumoración.</p>
        <p>Sus síntomas más comunes, aunque pueden ser muy variados, son: dolor de cabeza, náusea y vómito, mareo,
          síncope (desmayo) y crisis convulsivas, vista doble, alteración de movimientos oculares, debilidad de alguna
          extremidad, dificultad para hablar, alteraciones en la conducta, irritabilidad, aletargamiento o cualquier
          afección neurológica no explicable.</p>
        <div class="rc-color azulo rc-der">
          <div class="rc-foto-bx">
            <img class="w1200" src="/ediciones/2021/12/imgs/259-Dres-Vizcaino-Chavez-TUMORES-CEREBRALES-nt.jpg" style="margin: 0 auto 15px; display: block;">
          </div>
          <p class="f2 rc-color-cbz-1r guion" style="color: white;">Detecte</p>
          <ul class="rc-color-lista">
            <li>Dolor de cabeza</li>
            <li>Náuseas y vómito</li>
            <li>Mareo y desmayo</li>
            <li>Crisis convulsivas</li>
            <li>Vista doble</li>
            <li>Movimientos oculares anormales</li>
            <li>Debilidad de alguna extremidad</li>
            <li>Dificultad para hablar</li>
            <li>Alteraciones en la conducta</li>
            <li>Irritabilidad</li>
            <li>Aletargamiento</li>
          </ul>
        </div>
        <p>Cuando se combinan dos síntomas que se han vuelto persistentes como cefalea o vómito, predominantemente por
          las mañanas, es sugestivo de una tumoración.</p>

        <p class="descanso">¿Qué hacer ante sospecha de estas afecciones?</p>
        <p>Acudir inmediatamente a valoración médica, no dejar pasar el síntoma que ya no se comporta de manera
          habitual, que no resuelve o que incrementa.</p>
        <p>Si el médico considera que se justifica realizar imagenología, la resonancia magnética es el estudio de
          elección, pues permite una identificación más precisa que por medio de la tomografía computarizada.</p>
        <p>No obstante, de no contar con resonancia magnética o si se trata de un caso de urgencia, la tomografía sigue
          siendo una buena opción.</p>

        <p class="descanso">¿Existen tratamientos para los tumores cerebrales?</p>
        <p>Afortunadamente existe tratamiento y debe ser multidisciplinario, incluyendo áreas como neurocirugía,
          oncología, rehabilitación, apoyo emocional, pediatría, endocrinología, radiooncología, nutrición, entre otras.
          Siempre tratando cada caso de manera individualizada.</p>
        <p>La piedra angular del tratamiento es la búsqueda de la resección completa del tumor por parte del equipo de
          neurocirugía, teniendo tres objetivos principales:</p>
        <ol class="lista-num-nota">
          <li>Obtención de muestra para diagnóstico histo-patológico</li>
          <li>Resolución de sintomatología por efecto de masa del tumor</li>
          <li>Mejorar la expectativa de vida, conseguir la curación o, de no ser posible, disminuir considerablemente la
            carga tumoral</li>
        </ol>
        <p>Habiendo determinado la benignidad o malignidad del tumor, el oncólogo a cargo planteará un tratamiento que
          incluya quimioterapia y/o radioterapia particularizando cada caso. &nbsp;<i class="fas fa-square"></i></p>
