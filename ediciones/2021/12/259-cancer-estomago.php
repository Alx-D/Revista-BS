      <div class="texto">
        <p>La gran problemática con el cáncer de estómago, también llamado gástrico, se debe a que la gran mayoría de
          los casos son diagnosticados con enfermedad avanzada, donde las opciones de manejo se vuelven muy limitadas.
        </p>
        <p>Este tipo de cáncer se origina a partir de un crecimiento anormal de las células que lo componen, por lo
          general forman úlceras o pólipos que posteriormente infiltran a todo el órgano.</p>
        <p>Se clasifica en dos tipos principales: intestinal y difuso. El primero es el más frecuente, se asocia a un
          mejor pronóstico y mejor respuesta al tratamiento. Por el contrario, el cáncer gástrico difuso tiene una
          presentación clínica más agresiva, con mayor riesgo de presentar metástasis ganglionares o a distancia.</p>

