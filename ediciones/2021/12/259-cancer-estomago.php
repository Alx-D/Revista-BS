        <p class="descanso">En riesgo</p>
        <p>Los principales factores de riesgo para el desarrollo de esta enfermedad son la infección por Helicobacter
          pylori y una historia familiar de cáncer gástrico. Helicobacter pylori es una bacteria que infecta al
          estómago, predispone a las personas a desarrollar úlceras gástricas o gastritis, por lo tanto, la presencia de
          esta bacteria de forma persistente se asocia a un incremento en el riesgo de desarrollar cáncer de estómago.
        </p>
        <p>A pesar de que la gran mayoría de los tumores del estómago son esporádicos, del 1 al 3% tienen un origen
          familiar, donde el tipo difuso es el más frecuente.</p>
        <p>Se toman como hallazgos de sospecha para desarrollo de cáncer gástrico en una familia si se presentan las
          siguientes condiciones: historia de al menos dos familiares con cáncer gástrico difuso, diagnóstico antes de
          los 40 años, o historia familiar o personal de cáncer gástrico y cáncer de mama lobulillar diagnosticado antes
          de los 50 años.</p>
        <p>En caso de detectar alguna de las condiciones anteriores, se debe valorar un estudio genético en la familia.
        </p>
        <p>Otros factores de riesgo asociados con un incremento en el riesgo de padecer cáncer gástrico son la obesidad,
          una dieta alta en alimentos ahumados y salados, bajo consumo de frutas y verduras, gastritis crónica,
          tabaquismo y la presencia de pólipos gástricos.</p>

        <p class="descanso">Detectarlo</p>
        <p>Los signos y síntomas que nos pueden hacer sospechar de la presencia de un cáncer gástrico son: dificultad
          para tragar, sensación de hinchazón después de comer, saciedad temprana, acidez estomacal (gastritis),
          indigestión, náusea, dolor abdominal, pérdida de peso involuntaria o vómitos.</p>
        <p>Desafortunadamente en 10 a 20% de los pacientes con este tipo de cáncer no presenta ningún síntoma.</p>
        <p>Si una persona presenta alguno de estos síntomas con frecuencia, es importante que acuda a una valoración por
          un médico para iniciar un protocolo de estudio y descartar la posibilidad de presentar esta enfermedad.</p>
        <p>El diagnóstico se realiza a través de la realización de una endoscopía gástrica, donde a través de una cámara
          se valora la capa más interna del estómago (mucosa) en todas sus porciones y si se detecta alguna alteración,
          se toma una biopsia para confirmar por patología la presencia de la enfermedad.</p>
        <p>Además, es necesaria la realización de una tomografía de tórax y abdomen para valorar la extensión de la
          enfermedad y determinar la mejor alternativa de tratamiento.</p>
