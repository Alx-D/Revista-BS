        <p>Pilar fundamental del tratamiento es la cirugía, sin embargo, siempre debe ser acompañada de al menos
          quimioterapia con el objetivo de lograr una curación o control de la enfermedad.</p>
        <p>El tratamiento depende principalmente de si la enfermedad se encuentra localizada en el estómago o si se ha
          extendido a otros sitios como los ganglios linfáticos u otros órganos. Los factores más importantes para
          definir la modalidad de tratamiento a seguir van desde la edad, la presencia de comorbilidades, la etapa
          clínica y el estado nutricional.</p>

        <p class="descanso">Manejo quirúrgico</p>
        <p>Si una persona es diagnosticada con cáncer gástrico debe ser valorada lo más pronto posible por un cirujano
          oncólogo para ver si es posible realizar una cirugía con el objetivo de remover una parte o la totalidad del
          estómago junto con los ganglios adyacentes a éste.</p>
        <p>En caso de no ser candidato a una cirugía, el paciente debe ser referido con un oncólogo médico para iniciar
          tratamiento sistémico con el objetivo de controlar la enfermedad y buscar una respuesta favorable con la
          opción de realizar una cirugía posteriormente.</p>
        <p>Hay que señalar, que el pronóstico de esta enfermedad depende principalmente de la extensión al momento del
          diagnóstico. Por lo tanto, entre más temprano se detecte la enfermedad, es mejor la sobrevida y existen mayor
          número de opciones de tratamiento.</p>
        <p>La mejor forma de prevenir este tipo de cáncer es a través de hábitos saludables como mantener un peso ideal,
          una dieta con muchas frutas y verduras, dejar de fumar y evitar alimentos ahumados o embutidos. &nbsp;<i class="fas fa-square"></i></p>
