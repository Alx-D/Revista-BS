          <p>Mediante el uso de rayos X, rayos gamma de alta energía, haces de electrones y otras radiaciones ionizantes, la radiooncología es el campo de la medicina que se dedica al diagnóstico, cuidados clínicos y terapéuticos de los pacientes oncológicos, expuso en charla con Revista BS la doctora <b>Claudia Alejandra Montenegro Amador</b>.</p>
          <p>“Se utilizan dosis cuidadosamente dirigidas y reguladas de radiación de alta energía para eliminar las células cancerosas. El efecto es que la radiación provoca que algunas células cancerosas mueran inmediatamente después del tratamiento, pero la mayoría mueren posteriormente o quedan incapacitadas como resultado del daño inducido por la radiación a los cromosomas y ADN de las células cancerosas”, explicó la especialista en radiooncología.</p>
          <p>El uso de la radioterapia constituye uno de los avances más importantes en medicina, se utiliza en el tratamiento tanto de enfermedades no cancerosas, así como de neoplasias malignas.</p>
          <p>Su aplicación es versátil, ya que su uso en diferentes momentos tiene variados resultados, pudiendo usarse para destruir las células cancerígenas y curar el cáncer, como para aliviar sus síntomas.</p>
          <p>“Puede curar el cáncer, impedir que regrese, detener o hacer más lento su crecimiento. Cuando el objetivo es aliviar los síntomas, se conoce como tratamientos paliativos. La radiación externa puede reducir el tamaño de los tumores para tratar el dolor y otros problemas, como dificultad para respirar o falta de control de la vejiga y del intestino”, apuntó la especialista.</p>
          <p>La radioterapia, comentó Montenegro Amador, ha permitido ampliar las posibilidades en el tratamiento del cáncer, por ejemplo, al complementar la cirugía conservadora en cáncer de mama.</p>
          <p>“Se ha vuelto indispensable en la medicina moderna con una perspectiva conservadora, ya que, en el cáncer de mama, permite a las pacientes evitar una mastectomía y su impacto emocional”, subrayó.</p>
          <p>Aprovechando el periodo de fin de año, la doctora invitó a realizar visitas periódicas al médico como una recomendación a favor de la prevención.</p>
