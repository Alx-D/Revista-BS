      <div class="texto">
        <p>Vértigo se define como la sensación de rotación en el espacio, ya sea del propio paciente o del medio que le
          rodea. Se puede sentir como estar fuera de balance, dando vueltas, cayendo o inclinándose. Se describe una
          variedad de síntomas subjetivos y semejantes al vértigo por lo que es importante distinguir entre ellos:</p>
        <ul class="lista-disco-nota">
          <li><b>Desequilibrio</b>, inestabilidad postural al estar de pie, que puede ser por una alteración
            neurológica, debilidad músculo-esquelética o alteraciones visuales.</li>
          <li><b>Mareo,</b> se puede describir como una sensación de cabeza vacía o estar caminando en un puente
            colgante.
            De éstos, el vértigo tiene la mayor prevalencia en la población general (38%), donde vértigo periférico
            corresponde a más del 90% de las consultas y el central, alrededor del 5 y10%.</li>
        </ul>
        <p>Este padecimiento es un motivo de consulta frecuente en la práctica ambulatoria y en los servicios de
          urgencia, llegando a constituir 3 a 5% de las atenciones médicas en adultos.</p>

        <p class="descanso">Clasificación</p>
        <p>El vértigo se clasifica en dos grupos: periférico y el central. El primero se debe a un problema en la parte
          del oído interno que controla el equilibrio, puede ser del laberinto, o estructuras centrales vestibulares del
          tronco cerebral, lesión o alteración del nervio vestibular, que conecta el oído interno y el tronco
          encefálico.</p>
        <p>Por su parte, el vértigo central se debe a un problema en el cerebro, que puede involucrar el tronco
          encefálico o cerebelo, localizado en la parte posterior del cerebro.</p>
        <p>Es importante distinguir la diferencia entre vértigo central o periférico. Generalmente, episodios bruscos,
          súbitos, e intensos de vértigo, con intervalos asintomáticos, podrían ser periféricos. Por otro lado, la
          sensación continua, lenta y progresiva de vértigo sugiere un proceso de origen central.</p>
        <p>Generalmente los movimientos de la cabeza o cuerpo pueden exacerbar los síntomas en el vértigo periférico,
          así como la presencia de síntomas agregados como náuseas, vómito, sudoración o palidez. En el vértigo de
          origen central no se agregan estos síntomas, pero se presentan dolor de cabeza e inestabilidad.</p>
        <p>Es importante mencionar que si hubo antecedentes de episodios similares o de enfermedades óticas agregadas,
          como secreción por el oído (otorrea), otitis media aguda de repetición, sugieren un origen periférico.</p>
