        <p class="descanso">Causas</p>
        <p>Algunas de las principales causas del vértigo periférico son el vértigo postural paroxístico benigno,
          neuritis vestibular, enfermedad de Meniere, tumores como el neurinoma del acústico, laberintitis (que es una
          inflamación en una porción del oído interno llamado laberinto), una fístula perilinfática, colesteatoma,
          herpes zoster ótico.</p>
        <p>Para el vértigo central, las causas pueden ser una hemorragia cerebelar, isquemia del tallo cerebral,
          insuficiencia vértebrobasilar, tumores, migraña, esclerosis múltiple.</p>
        <p>De los vértigos periféricos, el vértigo postural paroxístico benigno predomina entre los demás, y predomina
          en la población femenina (2 a 1). Suele aparecer como consecuencia de ciertos cambios en la posición de la
          cabeza, como inclinar la cabeza hacia adelante o hacia abajo. Puede desaparecer por sí solo en unas semanas,
          pero de persistir los síntomas, el tratamiento consiste en una serie de movimientos conocidos como maniobra de
          reposicionamiento canalicular.</p>

        <p class="descanso">Vértigo y otros padecimientos</p>
        <p>Estudios han evidenciado al vértigo postural paroxístico benigno con la migraña, predominando también en las
          mujeres (5 a 1), llamándose vértigo migrañoso. 27 a 33% de las personas con migraña presentan vértigos
          episódicos. La migraña constituye un factor de predisposición para los vértigos.</p>
        <p>El vértigo puede aparecer antes de la cefalea en forma de aura, ya sea durante el periodo de cefalalgia o
          después. Con el tiempo, pueden aparecer vértigos años después de que las migrañas hayan desaparecido, por
          ejemplo, después de la menopausia.</p>
        <p>Su abordaje comprende varias facetas como eliminar los factores desencadenantes como algunos alimentos,
          estrés, mejorar el sueño; así como fármacos.</p>
        <p>Otro factor de riesgo para la aparición de episodios de vértigo en la mujer son las enfermedades metabólicas,
          como diabetes y obesidad. Lograr mejorar la calidad de vida de estos pacientes es fundamental por lo que la
          atención a la obesidad y enfermedades crónicas es crucial.</p>
        <p>Es importante estar atento a la hora de determinar las características del vértigo y de sus síntomas
          agregados, sean o no de características otológicas. Ante la mínima duda, es esencial consultar con un
          especialista otorrinolaringólogo o neurólogo. &nbsp;<i class="fas fa-square"></i></p>
