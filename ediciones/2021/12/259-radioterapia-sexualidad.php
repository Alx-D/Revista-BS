        <p class="descanso">Radioterapia y sus efectos</p>
        <p>La radiación dirigida a la pelvis puede causar disfunción eréctil, ya que puede causar lesiones a los nervios
          de la región pélvica, disminuir el flujo sanguíneo hacia el pene o reducir el nivel de testosterona en el
          cuerpo. Los efectos secundarios de la radiación comienzan lentamente alrededor de seis meses a un año después
          del tratamiento.</p>
        <p>Tanto la cantidad de radiación que reciba como la extensión de la región pélvica que requiera tratamiento
          están directamente relacionadas con la posibilidad de presentar disfunción eréctil.</p>
        <p>Los hombres que fuman o tienen antecedentes de enfermedades cardiacas, presión arterial alta o diabetes
          también pueden correr un riesgo más alto de presentar disfunción eréctil; es posible que estas afecciones ya
          hayan causado cierto daño en las arterias, que puede empeorar a causa de la radiación.</p>

        <p class="descanso">Sobrellevar los efectos</p>
        <p>Algunos efectos secundarios sexuales del tratamiento oncológico se resolverán en unas semanas. Otros pueden
          durar uno o dos años después del tratamiento, y algunos serán permanentes.</p>
        <p>Siempre es importante hablar con su médico y obtener toda la información acerca de qué está afectando su
          desempeño sexual.</p>
        <p>Los hombres que se han sometido a un tratamiento contra el cáncer pueden descubrir que simplemente se
          necesita tiempo para recuperar la función sexual. Los medicamentos y otras opciones de tratamiento funcionan
