      <div class="texto">
        <p>Los tratamientos contra el cáncer generan diversos signos y síntomas que pueden dificultar las relaciones
          sexuales con la pareja. Sin embargo, esto no significa que no se pueda tener una vida sexual saludable después
          del tratamiento oncológico.</p>
        <p>Generalmente, la afección depende del sitio donde está la enfermedad y del tipo de cáncer; las enfermedades
          localizadas a nivel pélvico tienden a provocar disfunciones sexuales más frecuentemente que otros tipos de
          cáncer.</p>
        <p>Los efectos secundarios sexuales son más frecuentes después del tratamiento de los siguientes tipos de
          cáncer: vejiga, colon, próstata y rectal.</p>

        <p class="descanso">Efectos esperados</p>
        <p>Los hombres mayores tienen mayor probabilidad de padecer disfunción sexual después del tratamiento
          oncológico. Esto se debe a que la mayoría de los hombres mayores, incluso los que no han tenido cáncer,
          tendrán dificultades sexuales en algún momento.</p>
        <p>Por lo tanto, los hombres mayores que se sometieron a un tratamiento oncológico pueden tener efectos
          secundarios sexuales relacionados con el envejecimiento más que con el tratamiento. También es posible que
          éste acelere los efectos secundarios sexuales asociados con el envejecimiento normal.</p>
        <p>El tratamiento oncológico puede producir diversos efectos secundarios sexuales, entre ellos:</p>
        <ul class="lista-disco-nota">
          <li>Incapacidad para lograr o mantener una erección (disfunción eréctil)</li>
          <li>Dificultad para alcanzar el clímax</li>
          <li>Orgasmo sin expulsión de semen (orgasmo seco)</li>
          <li>Orgasmos más débiles y menos satisfactorios</li>
          <li>Pérdida de interés en el sexo</li>
          <li>Dolor durante las relaciones sexuales</li>
          <li>Menos energía para la actividad sexual</li>
          <li>Sentirse menos atractivo</li>
        </ul>
