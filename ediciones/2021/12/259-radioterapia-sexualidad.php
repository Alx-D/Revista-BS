        <p>El cáncer puede provocar efectos secundarios sexuales si involucra los órganos sexuales. Los tipos de cáncer
          que no afectan los órganos sexuales también pueden afectar su sexualidad porque cambian su imagen corporal y
          hacen que se sienta menos atractivo, o porque causan fatiga, depresión y menor interés en el sexo.</p>
        <p>A veces, los factores emocionales podrían tener efectos secundarios sexuales, además de los cambios físicos
          que atraviesa durante el tratamiento.</p>

