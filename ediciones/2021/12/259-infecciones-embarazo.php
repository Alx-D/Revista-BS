      <div class="texto">
        <p>Las infecciones de vías urinarias pueden presentarse como bacteriuria asintomática, cistitis aguda (infección
          de la vejiga) o pielonefritis (infección renal).</p>
        <p>Mientras que en una cistitis se presentan signos y síntomas como urgencia urinaria, aumento de la frecuencia
          urinaria, ardor al orinar, turbidez y olor fétido de la orina; cuando se trata de mujeres embarazadas, el
          concepto de bacteriuria asintomática es especialmente importante. Se refiere a la colonización de la orina por
          un germen en ausencia de síntomas urinarios o generales.</p>
        <p>En caso de pielonefritis los síntomas incluyen: escalofríos, dolor en un costado, fiebre, náuseas, vómito y,
          con menor frecuencia, ardor al orinar y aumento de la frecuencia urinaria.</p>

        <p class="descanso">Incidencia</p>
        <p>Pacientes no embarazadas a menudo resuelven la bacteriuria asintomática sin tratamiento, pero las mujeres
          embarazadas presentan síntomas con mayor frecuencia y la bacteriuria tiende a permanecer con el riesgo de
          desarrollar pielonefritis.</p>
