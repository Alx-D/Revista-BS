        <p>La prevalencia de bacteriuria en mujeres embarazadas varía de 4 a 7% y la incidencia de pielonefritis aguda
          varía de 25 a 35% en mujeres con bacteriuria no tratadas.</p>
        <p>Hay que destacar que los cambios anatómicos y fisiológicos inducidos por el embarazo alteran
          significativamente la historia natural de la bacteriuria. Pueden hacer que las mujeres embarazadas sean más
          susceptibles a la pielonefritis y que requieran tratamiento.</p>
        <p>Entre los diversos factores que predisponen a la mujer embarazada a una mayor frecuencia de infecciones
          urinarias están la hidronefrosis fisiológica durante la gestación (dilatación del sistema colector renal
          debido a dificultad para la eliminación de la orina, que se presenta hasta en el 90% de las embarazadas y que
          se atribuye tanto a los efectos relajantes musculares de la progesterona durante el embarazo como a la
          obstrucción mecánica de los uréteres por el agrandamiento del útero en el borde pélvico), uretra corta,
          cambios vesicales, estasis urinaria y cambios fisicoquímicos en la orina.</p>

        <p class="descanso">Efectos en el embarazo</p>
        <p>La complicación más frecuente debido a infección de vías urinarias bajas en la mujer embarazada es el parto
          prematuro, siendo una de las causas de morbilidad y mortalidad perinatal.</p>
        <p>Representa el 75% de las muertes perinatales y el 50% de las secuelas neurológicas atribuibles directamente a
          la prematurez. Su frecuencia se estima en 5 a 10% de todos los embarazos.</p>
        <p>Las pacientes embarazadas deben ser evaluadas para detectar la bacteriuria asintomática en la primera
          atención del embarazo para disminuir el riesgo de pielonefritis, así como para disminuir las complicaciones
          maternas y fetales.</p>
