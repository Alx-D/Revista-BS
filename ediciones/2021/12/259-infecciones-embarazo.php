        <p>Se debe realizar tamizaje para bacteriuria asintomática con un examen general de orina (EGO) en el primer
          trimestre del embarazo. En México, se realiza comúnmente entre las 12 y las 16 semanas de gestación, con un
          EGO de seguimiento a las 18-20 semanas y otro entre las 32-34 semanas de gestación.</p>
        <p>Además, se deberá solicitar cultivo de orina para confirmar el diagnóstico de infección de vías urinarias,
          siendo Escherichia coli el patógeno más común, se encuentra en más del 80% por ciento de los casos.</p>

        <p class="descanso">Tratamiento</p>
        <p>Es poco probable la resolución espontánea de la bacteriuria en mujeres embarazadas, por lo que debe tratarse.
          Un régimen de 4 a 7 días es considerado como el más recomendado, la elección del antibiótico se hará según el
          resultado del cultivo de orina, prestando especial atención a la toxicidad materna y fetal.</p>
        <p>Las mujeres embarazadas con pielonefritis aguda deben ser hospitalizadas y tratadas inicialmente con agentes
          antimicrobianos por vía intravenosa. Más del 95% de las pacientes responden en 24 horas. Posteriormente, se
          deben administrar los agentes orales adecuados durante al menos 14 días.</p>
        <p>Existen algunos medicamentos que están relativamente contraindicados durante el embarazo: fluoroquinolonas,
          TMP, cloranfenicol, eritromicina, tetraciclina, sulfonamidas.</p>
        <p>Como conclusión, para prevenir la bacteriuria asintomática y las infecciones urinarias se recomiendan
          modificaciones en el estilo de vida: vaciamiento completo de la vejiga en forma frecuente y después de
          mantener relaciones sexuales, consumo de líquidos en forma abundante (más de 2000 ml/día), aseo genital
          adecuado y uso de ropa interior de preferencia de algodón. &nbsp;<i class="fas fa-square"></i></p>
