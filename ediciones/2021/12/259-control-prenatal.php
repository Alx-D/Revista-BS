        <p>Sin embargo, aún existe una proporción significativa de embarazos que no son planeados, en los cuales las
          consecuencias negativas de comportamientos, enfermedades y medicamentos podrían afectar el desarrollo de un
          embarazo temprano y a menudo dañar al feto, antes de que la mujer se dé cuenta que está embarazada.</p>
        <p>Idealmente, hay que iniciar la ingesta de ácido fólico tres meses previos a la concepción y los primeros tres
          meses de embarazo, para disminuir el riesgo de defectos del tubo neural (defectos congénitos del cerebro,
          columna y médula espinal).</p>

        <p class="descanso">Primer control</p>
        <p>Se realiza una historia médica completa, se solicitan algunos exámenes de laboratorio y se imparte educación
          acerca de un embarazo saludable.</p>
        <p>Además, se realiza un examen físico; para él hay que pesar, medir y calcular el índice de masa corporal de la
          gestante para determinar la ganancia de peso recomendada. Tomar la presión arterial y la frecuencia cardiaca.
        </p>
        <p>Resulta muy importante conocer los antecedentes de riesgo en embarazos previos que tienen posibilidad de
          repetirse en gestaciones posteriores.</p>
        <p>Entre las semanas 11 y 13 debe realizarse el ultrasonido del primer trimestre, para determinar la edad
          gestacional, identificar el latido cardiaco, detección y medida de la translucencia nucal (descartar mayor
          riesgo de trisomías como el síndrome de Down).</p>

