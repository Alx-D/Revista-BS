        <p>El control prenatal es importante, incluso en embarazos saludables. Los controles regulares ayudan a
          identificar pacientes con mayor riesgo tanto obstétrico como perinatal, agregan intervenciones ligadas a la
          prevención de dichos riesgos y también contribuyen a promover conductas saludables durante el embarazo.</p>
        <p>Lo ideal es que los cuidados prenatales se inicien antes de la concepción. Hoy en día es más frecuente que
          las parejas programen sus embarazos, lo cual permite que consulten antes al especialista con el objetivo de
          reducir los riesgos que sean modificables.</p>
