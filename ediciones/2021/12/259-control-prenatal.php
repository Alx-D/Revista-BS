          <p class="f2 rc-gris-cbz-1r">Embarazo saludable</p>
          <div class="f3 rc-gris-texto">
            <p><b>Salud integral:</b> alimentación balanceada, cuidado dental, realizar ejercicio, evitar exposición a
              químicos peligrosos.</p>
            <p><b>Hábitos saludables:</b> cese el tabaquismo y evite el consumo de alcohol.</p>
            <p><b>Peso sano:</b> la mujer embarazada con sobrepreso u obesidad debe tener un control óptimo del peso.
            </p>
            <p><b>Actividad física:</b> realice ejercicios aeróbicos que no produzcan fatiga extrema y no sean un riesgo
              (deportes de contacto físico).</p>
            <p><b>En movimiento:</b> se aconseja caminar, nadar, bailar o hacer yoga.</p>
            <p><b>Actividad sexual:</b> hasta cuatro semanas antes de la fecha de parto no implica riesgos; debe
              suspenderse en presencia de síntomas de aborto o amenaza de parto pretérmino.</p>
            <p><b>Trabajo:</b> en embarazos no complicados puede continuar trabajando en nuestro país hasta las 34-36
              semanas, siempre que el trabajo no implique trabajo físico considerable.</p>
          </div>
