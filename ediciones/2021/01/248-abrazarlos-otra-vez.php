      <div class="texto">
        <p>Las vacunas contra Covid-19 son la esperanza en el horizonte para salir de la pandemia
          que
          ha azotado al mundo por cerca de un año, no obstante, al tratarse de un tema de salud, no están exentas
          de
          generar inquietudes.</p>
        <p>En México, a enero de 2021, ya fueron aprobadas por la Comisión Federal para la
          Protección
          contra Riesgos Sanitarios (Cofepris) dos vacunas: Pfizer/BioNTech y la hecha por la Universidad de
          Oxford y
          AstraZeneca.</p>
        <p>Mayo Clinic publicó recientemente en su sitio Web una guía con información relevante
          sobre
          las vacunas contra SARS-CoV-2, aquí le presentamos los puntos más importantes para México.</p>

        <p class="descanso">Beneficios</p>
        <p>Ya que no existe una forma de saber cómo reaccionará cada individuo ante el Covid-19, y
          los
          casos más graves terminan en defunción; además del riesgo siempre latente de contagiar a la familia,
          amigos
          y otras personas cercanas, el principal beneficio de vacunarse es crear anticuerpos contra Covid-19, sin
          necesidad de enfermarse.</p>
        <p>La vacuna puede evitar que enferme, o bien, en caso de contagio, evitar un cuadro
          grave.
          Además, la inmunización ayuda a proteger a las personas que están cerca de contagiarse, esto es
          especialmente importante para la población en riesgo de desarrollar complicaciones.</p>

        <p class="descanso">Funcionamiento</p>
        <p>Ambas vacunas aceptadas en México hasta el momento (enero de 2021) tienen una capacidad
          de
          proteger contra el SARS-CoV-2 de hasta 95%.</p>
        <p>La vacuna de Pfizer no contiene versiones debilitadas o muertas del virus, sino se
          compone
          por ácido ribonucleico mensajero (ARNm). Es decir, que se introduce el contenido genético del
          coronavirus a
          través del ARNm, generando así que el sistema inmune tenga la información suficiente para combatir la
          enfermedad (Infobae).</p>

        <div class="rc-color rc-der azulc">
          <p class="f2 rc-color-cbz-1r" style="color: #fff;">No baje la guardia</p>
          <ul class="rc-color-lista">
            <li>Recomendaciones de higiene y distanciamiento continúan</li>
            <li><b>Ojo:</b> puede infectarse antes de generar inmunidad</li>
            <li>Continúe el lavado frecuente de manos y distancia social</li>
            <li>Evite asistir a lugares concurridos y utilice cubrebocas</li>
            <li>Si presenta síntomas respiratorios, quédese en casa</li>
          </ul>
        </div>

        <p>Por su parte, la vacuna de AstraZeneca se basa en un virus de gripe común que fue
          genéticamente modificado, así el organismo reconoce al SARS-CoV-2 como amenaza (Infobae). Ambas vacunas
          requieren dos dosis que se aplican con 21 días de diferencia.</p>

        <p class="descanso">Seguridad</p>
        <p>Una de las principales preocupaciones ante la inmunización es si puede transmitir la
          enfermedad o si puede producir efectos secundarios.</p>
        <p>Como se describió arriba, ninguna de las vacunas contiene virus activos, por lo que no
          es
          posible que provoque la enfermedad. Pero hay que tomar en cuenta que tarda algunas semanas que el cuerpo
          genere inmunidad después de la aplicación de la vacuna, entonces usted puede infectarse de Covid-19
          justo
          antes o después de haber recibido la vacuna. Por ello es importante no bajar la guardia con las medidas
          de
          higiene y seguridad.</p>
        <p>En cuanto a los efectos secundarios se encuentran dolor y enrojecimiento del área donde
          se
          aplicó la vacuna, fiebre, fatiga, dolor de cabeza, dolor muscular o articular y escalofríos.</p>
        <p>Al aplicar la vacuna, se le dará un seguimiento por un lapso de 30 minutos para estar
          al
          pendiente de reacciones inmediatas. Generalmente los efectos secundarios aparecen en los primeros tres
          días
          tras la vacuna y suelen durar uno o dos días.</p>
        <p>Ya que las vacunas pueden ocasionar efectos secundarios similares a los síntomas de
          Covid-19, si ha estado expuesto a alguien con diagnóstico positivo y usted presenta síntomas más de tres
          días después de haberse vacunado o con duración de más de dos días, aíslese y hágase una prueba
          diagnóstica.
        </p>

        <p class="descanso">Precauciones</p>
        <p>Existen algunos grupos poblacionales que pueden sentir incertidumbre respecto a si
          deben o
          no optar por vacunarse, entre ellos están personas con alergias a vacunas o medicamentos y embarazadas.
        </p>
        <p>Quienes tienen antecedentes de reacciones alérgicas graves que no están relacionadas a
          vacunas u otros medicamentos inyectables, pueden vacunarse y deben ser monitoreados por 30 días.</p>

        <div class="foto-izq">
