        <p>Personas con reacción alérgica grave a otras vacunas o a medicamentos inyectables deben
          consultar con su médico si pueden recibir la inmunización. Quienes son alérgicos a los polisorbatos no
          deben
          recibir la vacuna de ARNm, es decir, la de Pfizer.</p>
        <p>Las gestantes o lactantes deben consultar con su médico riesgos y beneficios de ser
          vacunadas; si se encuentran dentro de los grupos a los que se recomienda vacunar, pueden optar por
          recibir
          la inmunización.</p>
        <p>Ya que las vacunas no se han probado en menores de 18 años, en unos casos, y en menores
          de
          16, en el caso de la BioNTech de Pfizer, los menores de esa edad no deben recibir la vacuna, pues aún no
          hay
          información sólida en cuanto a la seguridad para ese grupo poblacional.</p>

        <p class="descanso">Ya tuve Covid</p>
        <p>Haber tenido la enfermedad ofrece cierta inmunidad natural ante la reinfección, aún no
          se
          determina cuánto dura tal protección. Ya que una reinfección puede causar complicaciones graves, quienes
          tuvieron Covid-19 deben vacunarse. Pueden hacerlo hasta 90 días después del diagnóstico, periodo poco
          probable de reinfección.&nbsp;<i class="fas fa-square"></i></p>

