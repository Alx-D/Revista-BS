        <p class="f1 descanso">La vacuna</p>
        <p>Disminuir la muerte y las enfermedades graves tanto como sea posible es su objetivo,
          preservar el
          funcionamiento de la sociedad y reducir la carga adicional que Covid-19 representa para las personas que ya
          enfrentan disparidades sociales.
        </p>
        <p>Según la Guía Técnica de Aplicación de Vacuna BNT162b2, la vacuna se compone por ácido
          ribonucleico mensajero
          (ARNm) de una sola cadena, producido por transcripción (in vitro) libre de células a partir de
          plantillas de
          ácido desoxirribonucleico (ADN) que tiene la información que codifica la producción de la proteína viral
          S
          (“Spike”) del SARS-CoV-2.</p>
        <p>En las personas vacunadas se activa el sistema inmune para la producción de anticuerpos
          neutralizantes
          específicos contra este virus que, al unirse a la proteína S viral, impiden que el virus se acople a los
          receptores ACE2 de las células de las personas vacunadas, dificultando su entrada a las células.</p>
        <p class="f1 texto-resaltado">La presentación de la vacuna es en frasco ámpula de 0.45 ml que, una vez
          reconstituido,
          contiene 5 dosis de
          30 microgramos (µg) cada una. Los frascos ámpula de la vacuna congelada pueden mantenerse hasta por 6
          meses
          a una temperatura de -80° a -60° centígrados. </p> <br>
        <p>Para descongelar los frascos ámpula deben introducirse al refrigerador de vacunas para
          que se descongelen a
          una temperatura de entre 2° y 8°, procedimiento que tardará aproximadamente 3 horas.</p>
        <p>Una vez descongelada, la vacuna sin diluir puede almacenarse hasta por 5 días (120
          horas) entre 2° y 8°. El frasco ámpula se diluye con 1.8 ml de diluyente de cloruro de sodio,
          conteniendo 5
          dosis diluidas de 0.3ml.
          A partir de ese momento sólo dura 6 horas, manteniéndola a temperatura ambiente de entre 2° C y 25° C.
        </p>

        <p class="descanso">Indicaciones</p>
        <p>La vacuna está indicada para ser aplicada a personas a partir de los 16 años cumplidos.
          El esquema de
          vacunación es de dos dosis de 0.3 ml aplicadas por vía intramuscular en el músculo deltoides del brazo
          de
          menor uso, con un intervalo entre ambas de 21 días.</p>
        <p>Quienes son vacunados son observados por 30 minutos y se toma nota de eventos
          supuestamente atribuibles a vacunación o inmunización (ESAVI).</p>
        <p> La eficacia general, reportada a los 7 días posteriores a la aplicación de la segunda
          dosis de la vacuna,
          fue de 95% en los participantes en el ensayo clínico de Fase III. Hasta ahora no se ha establecido la
          duración del efecto protector.</p>
        <p>Hay grandes expectativas y esperanza que la vacuna venga a contener gradualmente al
          flagelo que hoy
          padecemos, y que se encuentra con mayor actividad que en meses previos, en tanto eso ocurre debemos
          seguirnos cuidando, con las medidas preventivas ya conocidas de sana distancia, uso de cubrebocas,
          lavado
          frecuente de manos o uso de gel alcohol.</p>
        <p>Ante cualquiera duda, síntomas o malestar consulte a su médico y procure su Buena
          Salud. <i class="fas fa-square"></i></p>
