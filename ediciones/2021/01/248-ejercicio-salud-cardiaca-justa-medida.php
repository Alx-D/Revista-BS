        <p>
          El 2020 nos demostró que la mejor inversión que podemos hacer en la
          vida es tener buena salud. Si algo ha hecho diferencia en todas las
          enfermedades, no sólo las cardiovasculares, es la capacidad que
          tenemos de realizar esfuerzos, lo que conocemos como condición física.
        </p>
        <p>
          Tener una buena condición física hace la diferencia en la mortalidad
          por todas las causas, es decir, quien tiene mejor condición física
          tiene menor riesgo de morir por cualquier causa, no sólo por motivos
          cardiovasculares, que alguien que no tiene buena condición física.
        </p>
        <p>
          ¿De qué depende nuestra condición física? Depende de la cantidad,
          intensidad y frecuencia con que se hace el ejercicio. Aunque los
          estudios que se han hecho a gran escala han demostrado que un poco de
          ejercicio es mejor que nada, hay una cantidad demostrada de ejercicio
          que funciona para prevenir y disminuir las enfermedades
          cardiovasculares.
        </p>

        <p class="descanso">Encuentre la medida adecuada</p>
        <p>
          La cantidad de ejercicio mínima para estos efectos es de 150 minutos a
          la a semana de ejercicio moderado, y 75 minutos a la semana de
          ejercicio intenso.
        </p>
        <p>
          El ejercicio moderado es caminar, nadar, bicicleta estacionaria o
          normal, a una intensidad en la que se siente lo suficiente como el
          calor del ejercicio, sin llegar a agitarse o sentirse cansado, se pude
          mantener una conversación, y al día siguiente no se siente cansado,
          adolorido, o sin poder realizarlo, todo esto por 30 minutos continuos,
          con un calentamiento y estiramiento previo si es posible.
        </p>
        <p>
          Por otro lado, el ejercicio intenso es trotar, correr, ejercicio por
          intervalos de alta intensidad, bicicleta a un ritmo más intenso y
          otras actividades en las que el esfuerzo es mayor (que en el ejercicio
          moderado), estos ejercicios son más recomendados para personas jóvenes
          o con más experiencia en el ejercicio. Se sugiere distribuirlo en 15
          minutos diarios por 5 días a la semana.
        </p>

