        <p>
          La ventaja del ejercicio moderado es que tiene bajo riesgo de
          complicaciones durante el ejercicio, es decir, tiene menor
          probabilidad de que se tenga un evento adverso durante su práctica,
          como un infarto, desmayo o lesiones durante el mismo. Es lo más
          recomendado en general para la población, incluyendo personas que
          tienen enfermedades crónico degenerativas, como diabetes,
          hipertensión, colesterol y triglicéridos elevados, hasta personas con
          insuficiencia cardiaca y angina de pecho, siempre con previa
          valoración por un cardiólogo.
        </p>
        <p>
          El ejercicio intenso se sugiere para personas que sean sanas o que ya
          tienen una valoración previa que descarta alguna enfermedad que lo
          ponga en riesgo de tener un problema.
        </p>
        <p>
          La cantidad y frecuencia también son importantes en cuanto a la dosis
          de ejercicio, porque un exceso de ejercicio a largo plazo también
          puede poner en riesgo a la persona de otro tipo de problemas. Sabemos,
          por estudios realizados en atletas, que quienes realizan ejercicio
          intenso más de dos horas diarias tienen mayor riesgo de arritmias a
          largo plazo, crecimiento del corazón, y a mayor edad mayor riesgo de
          infarto, si no se revisan con regularidad.
        </p>

