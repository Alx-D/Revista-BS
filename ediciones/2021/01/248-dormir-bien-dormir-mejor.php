        <p>
          Durante años se ha investigado extensamente todo lo
          relacionado con el sueño, y a pesar de estas investigaciones aún quedan muchos enigmas por resolver, sin
          embargo, lo que es evidente es que el sueño juega un papel fundamental en el bienestar físico y
          emocional.
        </p>
        <p>Cuando dormimos nos encontramos en un estado de inconsciencia, sin embargo muchas funciones mentales y
          corporales se mantienen activas. El sueño involucra una serie de procesos biológicos que nos ayudan a:
        </p>
        <ul class="lista-disco-nota">
          <li>Sentirnos descansados y con energía</li>
          <li>Aprendizaje y formación de recuerdos</li>
          <li>Reparación celular</li>
          <li>Desarrollo y crecimiento</li>
          <li>Combatir procesos infecciosos o inflamatorios</li>
        </ul>

        <p class="descanso">¿Cuánto sueño necesito?</p>
        <p>Se trata de una pregunta muy común, y la respuesta depende de diversos factores como la edad, estado de
          salud, estilo de vida; existen algunas recomendaciones generales dependiendo de la edad, pero pueden
          variar con base en los factores mencionados con anterioridad. En personas sanas se recomienda para los
          recién nacidos aproximadamente 18 horas de sueño al día, niños y adolescentes de 9 a 12 horas al día, y
          adultos (incluyendo adultos mayores) entre 6 y 8 horas diarias.</p>
        <p>Es importante aclarar que no sólo las horas de sueño son importantes, sino también la calidad del mismo,
          ya que existen diversos problemas que ocasionan que nuestro sueño sea superficial, que tardemos en
          quedar dormidos o que despertemos muy temprano y no podamos volver a dormir. Estas causas van desde
          problemas hormonales, ansiedad y otros trastornos emocionales, hasta causas tan simples y a la vez tan
          comunes como una mala higiene del sueño.</p>

        <div class="rc-color morado rc-izq">
          <p class="f2 rc-color-cbz-1r" style="color: white;">¿Rutina opuesta?</p>
          <p class="f3 rc-color-texto">Recomendaciones para personas que trabajan horarios nocturnos:</p>
          <ul class="rc-color-lista">
            <li>Tomar siestas o maximizar el tiempo disponible para dormir a cualquier hora</li>
            <li>Mantener las luces encendidas en el horario de trabajo</li>
            <li>Tratar de mantener turnos constantes (por ejemplo, mensuales)</li>
            <li>Limitar el consumo de cafeína o tabaco fuera del turno laboral</li>
            <li>Adaptar las medidas de higiene del sueño mencionadas arriba en las horas de descanso</li>
          </ul>
        </div>

        <p class="descanso">Complejo mecanismo</p>
        <p>El sueño está dado por una serie de ajustes hormonales que regulan un reloj interno que conocemos como
          ritmo circadiano, esto determina nuestras horas de actividad y de sueño, así como los horarios para
          alimentarnos.</p>
        <p>Para su regulación están involucrados una serie de neurotransmisores que responden a estímulos generados
          a partir de nuestros ojos. Cuando el ojo deja de percibir cierto espectro de luz, asume que el día está
          por terminar y comienza la producción de estas hormonas y neurotransmisores que inician las etapas del
          sueño.</p>
        <p>Sin embargo, nosotros también podemos generar cambios en este reloj interno si comenzamos a habituarnos a
          una rutina de sueño independiente de la duración del día y la noche, sin embargo, al no encontrarse
          regulada adecuadamente por los marcadores biológicos a los que nuestro cerebro está acostumbrado, puede
          llevar a que nuestro sueño sea pobre, de mala calidad y no reparador.</p>
        <p>Se entenderá, por lo tanto, como higiene del sueño a las medidas que podemos llevar a cabo para asegurar
          que nuestro sueño sea reparador. Dentro de las consecuencias en la salud mental de un sueño no reparador
          se encuentran:</p>
        <ul class="lista-disco-nota">
          <li>Irritabilidad</li>
          <li>Ansiedad</li>
          <li>Depresión</li>
          <li>Psicosis</li>
        </ul>
        <p>Entonces ¿qué puedo hacer para asegurar un sueño reparador? Algunas medidas recomendadas para una buena
          higiene del sueño son las siguientes:</p>
        <ul class="lista-disco-nota">
          <li>Tener horarios de sueño bien establecidos para dormir y despertar</li>
          <li>Limitar las siestas durante el día</li>
          <li>Evitar el consumo de estimulantes como nicotina o cafeína, sobre todo por las
            noches</li>
          <li>Evitar consumir alimentos pesados o bebidas alcohólicas por las noches</li>
          <li>Mantener una rutina de sueño relajante (tomar un baño, leer o escuchar música
            relajante, mantener
            una temperatura agradable en la habitación)</li>
          <li>Evitar distractores por las noches como luz amarilla o intensa y sonidos fuertes</li>
          <li>Evitar el uso de dispositivos electrónicos</li>
        </ul>
        <p>Si a pesar de lo anterior persisten los problemas para conciliar o mantener el sueño, es importante
          acudir con un especialista para descartar otros problemas médicos que puedan estar asociados a este
          malestar. &nbsp;<i class="fas fa-square"></i></p>
