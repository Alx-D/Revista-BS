        <p>Es importante consultar con el médico cuándo puede reactivar su actividad física después de la cirugía.
          Se recomienda caminar y movilizarse después de la cirugía para favorecer la circulación sanguínea y
          evitar la formación de coágulos. Dependiendo del tipo de cáncer y cirugía puede ser necesario
          complementar la recuperación con una valoración por un especialista en rehabilitación física para
          disminuir el riesgo de complicaciones y secuelas.</p>

        <p class="descanso">Cambio de hábitos</p>
