      <div class="texto">
        <p>El cirujano oncólogo es el médico encargado del manejo del paciente con cáncer previo a cualquier
          cirugía. El manejo quirúrgico del paciente con cáncer puede ser complejo, ya que intervienen factores
          como el tipo de cáncer, la extensión de la cirugía, la etapa clínica, su estado de salud, antecedentes
          (enfermedades crónico degenerativas), hábitos alimenticios, entre otros.</p>
        <p>Todos estos factores impactan en el éxito de la cirugía e influyen antes y después de ella, a
          continuación, comentamos los más importantes:</p>

        <p class="descanso">Alimentación</p>
        <p>Es uno de los factores primordiales para el éxito en cualquier tipo de cirugía. Por lo general los
          pacientes con cáncer sufren de malnutrición por diversas causas como síntomas asociados al mismo cáncer
          (nausea, vómito, distensión abdominal, dolor, entre otros); falta de apetito y pérdida de peso
          ocasionados por el cáncer (caquexia); obesidad y sobrepeso.</p>

        <div class="rc-color rc-der rojoc">
          <p class="f2 rc-color-cbz-1r" style="color: #fff;">Éxito de la cirugía depende:</p>
          <ul class="rc-color-lista">
            <li>Tipo de cáncer</li>
            <li>Extensión de la cirugía</li>
            <li>Etapa clínica de la enfermedad</li>
            <li>Estado de salud del paciente</li>
            <li>Antecedentes clínicos</li>
            <li>Hábitos alimenticios</li>
          </ul>
        </div>

        <p class="descanso">Alcohol y tabaco</p>
        <p>El consumo de alcohol se asocia a mayor número de complicaciones postoperatorias, por lo que debe
          suspenderse previo a la cirugía por cáncer. El uso de tabaco en todas sus presentaciones debe ser
          suspendido al momento del diagnóstico de cáncer y de forma permanente.</p>
        <p>Los químicos del tabaco, especialmente de los cigarros, afectan directamente la cicatrización de las
          heridas y, a la vez, afectan la recuperación postoperatoria.</p>

        <p class="descanso">Enfermedades crónicas</p>
        <p>Es importante que se notifique al cirujano oncólogo de todos los padecimientos que el paciente tenga como
          diabetes, hipertensión, enfermedades de la tiroides, entre otros. El ajuste previo del tratamiento
          permite al paciente llegar en condiciones óptimas a su procedimiento y disminuye el riesgo de
          complicaciones postoperatorias pulmonares y cardiacas. La valoración preoperatoria por un médico
          internista o cardiólogo permite analizar el riesgo quirúrgico del paciente previo a la cirugía y
          corregir las alteraciones que se detecten en dicha valoración.</p>

        <p class="descanso">Ejercicio físico</p>
        <p>La actividad física es una pieza importante en la evolución del paciente quirúrgico. Los pacientes que
          tienen el hábito de realizar ejercicio de forma cotidiana, por lo general, presentan un mejor estado de
          salud y mejores condiciones cardiovasculares que disminuyen el riesgo de complicaciones quirúrgicas.</p>

