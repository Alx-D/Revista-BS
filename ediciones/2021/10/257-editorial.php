      <div class="texto">
        <p>El cáncer es un grupo de más de 100 enfermedades provocadas por diversos factores, como estilos de vida, genéticos y ambientales, entre otros, que dañan el ciclo de reproducción de las células del cuerpo.</p>
        <p>Existen diferentes tipos de células, pero todas tienen la misma estructura básica. Los tejidos son capas de células similares que cumplen con una función específica. Los diferentes tipos de tejidos se agrupan para formar órganos.</p>
        <p>Las células normales se dividen de manera ordenada. Éstas mueren cuando se han desgastado o se dañan y nuevas células toman su lugar. <strong>En el cáncer este proceso ordenado se descontrola</strong>: nuevas células se siguen formando, aunque el cuerpo no las necesite y las viejas no mueren cuando deberían. Esto causa problemas en el área del cuerpo en la que comenzó el cáncer (tumor canceroso). También pueden propagarse a otras partes del cuerpo.</p>
        <p>Dependiendo del órgano o tejido afectado es como se denominan las diversas variedades de cáncer: en los pulmones, en la mama, en el colon o en la sangre, con una gran diferencia en la manera en que crecen, se propagan y se manifiestan.</p>

        <p class="descanso">En cifras</p>
        <p>La Organización Mundial de la Salud (OMS) reconoce tres categorías de agentes externos: carcinógenos físicos (radiaciones ionizantes y ultravioletas), carcinógenos químicos (amianto, humo del tabaco, aflatoxinas, arsénico) y carcinógenos biológicos (algunos virus como el del papiloma humano, bacterias y parásitos).</p>
        <p>En México el año 2020 los tumores malignos (cáncer) fueron la cuarta causa de muerte con 90 mil 645 defunciones, con una tasa de 71.93 casos por cada cien mil habitantes. Representando el 8.3 por ciento de las muertes ocurridas en ese año (1 millón 86 mil 94). Un año antes, en 2019, se registraron 747 mil 784 defunciones, de las cuales 12% se deben a tumores malignos (88 mil 683), de acuerdo a INEGI y ESTATISTA.</p>
        <p>El 30 por ciento de los diferentes tipos de cáncer que presentan los mexicanos se relaciona con hábitos como tabaquismo, alcohol, consumo de alimentos calóricos, bebidas azucaradas y falta de ejercicio, informó el Director General del Instituto Nacional de Cancerología (INCan), Abelardo Meneses García.</p>
        <div class="texto-resaltado-bx">
          <p>En Sonora los tumores malignos fueron responsables de 2 mil 582 decesos el año 2020, ocupando la tercera causa de muerte, con una tasa de 83.97 casos por cada cien mil habitantes, representando el 9.2 por ciento de las muertes ocurridas en ese año (28 mil 152). El año previo, 2019, se registraron 18 mil 845 defunciones, de las cuales 13.13% fueron por tumores malignos (2 mil 475) (INEGI).</p>
        </div>
        <p>La variación principal en la proporción de muertes por cáncer del total de muertes ocurridas, tanto en lo nacional como estatal de un año a otro fue debido al número de muertes por Covid-19 en el año 2020 y también por la sobremortalidad consecuente.</p>
        <p>De acuerdo al comunicado del 08 de agosto de 2017 de la Secretaría de Salud, durante el foro “Cáncer, Desafíos en México y América Latina” que se llevó a cabo en el Centro Médico Nacional Siglo XXI, Meneses García indicó que los tumores malignos más frecuentes son: el cáncer de mama, con 20 mil casos, 14 mil de próstata y más de 8 mil casos de colon y pulmón. Los tipos de cáncer varían de manera importante dependiendo de la edad y sexo de las personas.</p>

        <p class="descanso">Panorama de tratamiento</p>
        <p>
          Los tratamientos contra el cáncer contemplan cirugía, medicamentos y radiación. En ocasiones más de un tipo de tratamiento es utilizado en función del tipo y etapa del cáncer.
          En cuanto a los medicamentos usados contra el cáncer, existen varios tipos: quimioterapia (quimio), terapia dirigida, inmunoterapia y terapia hormonal.
        </p>
        <p>
          La radioterapia también se usa para matar o disminuir el crecimiento de las células cancerosas. Puede usarse sola o junto con cirugía o quimioterapia.
          Sin duda, uno de los aspectos más relevantes es la prevención, la detección temprana y el tratamiento oportuno, por ello hay que visitar periódicamente a su médico y llevar un estilo de vida saludable.
        </p>
        <p>En este número especial de Revista BS, encontrará consejos y orientaciones sobre los cánceres más comunes y los tratamientos innovadores que hay en nuestro medio. Así como medidas preventivas diversas para ellos.</p>
        <p>También son muy importantes los cuidados paliativos, la rehabilitación física y mental, pero sobre todas las cosas se debe ampliar el acceso y disponibilidad de los medicamentos y tratamientos contra el cáncer, lo cual evitaría sufrimiento y muertes prematuras. Consulte a su médico y procure la Buena Salud. &nbsp;<i class="fas fa-square"></i></p>
