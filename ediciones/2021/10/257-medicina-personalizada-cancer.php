      <div class="texto">
        <p>Una de las principales revoluciones en el área de la oncología ha sido el poder establecer decisiones médicas
          de forma personalizada, es decir, en función de las características moleculares del tumor de cada paciente.
          Esto es lo que se denomina medicina personalizada (o de precisión, o dirigida).</p>
        <p>Para entender este nuevo enfoque médico, es necesario primero entender el tradicional: un medicamento para
          todos.</p>
        <p>Los genes contienen la información que indica a las células cómo crecer y desarrollarse. Muchos tipos de
          cáncer afectan o incluyen genes específicos. Y es que no hay dos cánceres iguales, cada tumor es diferente,
          pues es el resultado de una o más alteraciones genéticas de una célula. Como consecuencia de esta alteración,
          la célula crece sin control formando una masa o tumor que puede llegar a invadir tejidos u órganos sanos y
          diseminarse.</p>
        <p>Tradicionalmente, todos los pacientes diagnosticados con cáncer recibían la misma medicina; estos productos
          funcionaban mejor para algunos pacientes, aunque eran inefectivos para otros.</p>
        <p>La medicina personalizada, por el contrario, reconoce que cada paciente es único. Al utilizar pruebas de
          diagnóstico específicas, los médicos pueden en cierta medida predecir cómo responderá un paciente al
          tratamiento, y a su vez determinar la mejor dosis o duración del tratamiento. La esencia de la medicina
          personalizada es unir a cada paciente con su mejor tratamiento, basándose en marcadores biológicos
          específicos.</p>

        <p class="descanso" style="color: var(--morado);">Utilidad de la medicina de precisión</p>
        <p>La medicina personalizada para el cáncer utiliza información específica del tumor de una persona a fin de:
        </p>
        <ul class="lista-disco-nota">
          <li>Facilitar el diagnóstico</li>
          <li>Planificar el tratamiento</li>
          <li>Dar un pronóstico</li>
        </ul>
        <p>La medicina de precisión permite que un porcentaje de pacientes con diferentes tumores puedan recibir
          tratamientos dirigidos, los cuales tienen una mayor especificidad y por tanto una mayor eficacia y menor
          toxicidad, comparados con los tratamientos convencionales.</p>

        <p class="descanso" style="color: var(--morado);">Aplicaciones actuales</p>
        <p><strong>Cáncer de mama.</strong> Hoy sabemos que no existe un solo tipo de cáncer de mama, sino que se puede
          y se debe clasificar en diferentes subtipos. Los tratamientos establecidos irán enfocados en la presencia de
          receptores hormonales y/o expresión de la proteína HER2 (proteína que interviene en el crecimiento normal de
          las células), de esta manera estableceremos un tratamiento adecuado: hormonoterapia o terapia antiHER2,
          respectivamente.</p>
        <p><strong>Cáncer de pulmón.</strong> En los pacientes con cáncer de pulmón avanzado o metastásico no debería
          indicarse el tratamiento oncológico hasta haberse descartado si el paciente puede o no beneficiarse de una
          terapia personalizada. Esto se establece realizando un perfil mutacional completo. El tratamiento establecido
          podría ser, por ejemplo: quimioterapia, hormonoterapia o terapia dirigida.</p>
        <p><strong>Cáncer de colon.</strong> Actualmente, en la terapia del cáncer de colon metastásico, se dispone de
          agentes novedosos que están dirigidos a bloquear el crecimiento tumoral mediante la inhibición de la formación
          de vasos sanguíneos o inhibición de receptores que controlan la multiplicación y supervivencia de las células.
          La elección del tratamiento dependerá del análisis mutacional de diversos genes asociados al crecimiento
          tumoral.</p>

        <p class="descanso" style="color: var(--morado);">Conclusiones</p>
        <p>Aunque el ámbito del cáncer progresa hacia la medicina personalizada, con medicamentos dirigidos a tumores
          específicos, se ha puesto de manifiesto que muchos pacientes no son conscientes de esta revolución.</p>
        <p>Es evidente que sigue siendo necesario que los pacientes tengan más información sobre la medicina
          personalizada, una responsabilidad conjunta del equipo de atención multidisciplinario, los grupos de apoyo al
          paciente y los medios de comunicación. <i class="fas fa-square"></i></p>
