      <div class="texto">
        <p>La leucemia linfoblástica aguda (LLA) es un tipo de cáncer que se caracteriza por una alteración en la
          función de la médula ósea (fábrica de los elementos de la sangre) produciendo glóbulos blancos inmaduros de
          manera descontrolada, denominados linfoblastos.</p>
        <p>Este tipo de leucemia es el tipo de cáncer más común en los niños. En él las células leucémicas que aumentan
          en cantidad en la sangre disminuyen la producción de glóbulos rojos, glóbulos blancos y plaquetas normales.
          Esto puede resultar en infección, anemia grave y hemorragias.</p>

        <p class="descanso" style="color: var(--morado);">Signos y síntomas</p>
        <p>Debe consultarse a un médico ante la presencia de cualquiera de los siguientes problemas:</p>
        <ul class="lista-disco-nota">
          <li>Fiebre intermitente y de difícil control</li>
          <li>Palidez progresiva de la piel</li>
          <li>Moretones espontáneos sin causa aparente</li>
          <li>Sangrados por la nariz o en las encías</li>
          <li>Aumento de volumen en el abdomen por crecimiento de órganos como hígado y el bazo</li>
          <li>Dolor en los huesos o las articulaciones</li>
          <li>Pérdida del apetito</li>
          <li>Petequias (manchas planas como “puntitos rojo intenso” en la piel producidos por hemorragias)</li>
          <li>Crecimiento de ganglios no dolorosos en cuello, axilas y región inguinal</li>
          <li>Debilidad o sensación de cansancio</li>
        </ul>

        <p class="descanso" style="color: var(--morado);">Diagnóstico</p>
        <p>Es importante realizar un diagnóstico temprano mediante la revisión por su pediatra, el cual realizará la
          exploración física y un estudio muy sencillo de sangre, donde tendrá los elementos de sospecha y, entonces,
          derivará al oncólogo pediatra.</p>
        <p>Hay que señalar que el diagnóstico de leucemia trae consigo un sentimiento de miedo. Cuando el paciente es un
          niño, las emociones para la familia son sumamente difíciles.</p>
        <p>Durante la evaluación inicial por oncología es fundamental establecer un vínculo de confianza con los padres
          y el paciente, explicando de forma amplia y detallada la situación del enfermo.</p>

        <p class="descanso" style="color: var(--morado);">Horizonte alentador</p>
        <p>El manejo de la leucemia aguda linfoblástica infantil es considerado el mayor éxito en la historia entre el
          tratamiento de todos los cánceres diseminados en seres humanos.</p>
        <p>Con los protocolos actuales de manejo, utilizando quimioterapia, inmunoterapia y en algunos casos específicos
          trasplante de médula ósea, las posibilidades de curación alcanzan el 90% en los niños con grupo de riesgo
          denominado “habitual o bajo”; mientras que en los casos de “alto riesgo”, la probabilidad de curación es de
          alrededor del 70%.</p>
        <p>Todos los niños y adolescentes con leucemia, deben ser manejados por un grupo multidisciplinario de
          sub-especialistas pediátricos en centros que cuenten con la infraestructura necesaria para dichos pacientes.
        </p>
        <p>Durante 27 años de ejercicio profesional en la atención de niños con cáncer en el Estado de Sonora, hemos
          logrado que muchos pacientes con leucemia hayan superado la enfermedad y actualmente estén curados.</p>
        <p>La especialidad de oncología pediátrica es una rama de la medicina donde el trabajo integral por un equipo de
