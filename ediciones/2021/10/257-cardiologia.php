        <p>La radioterapia puede dañar las válvulas del corazón, el pericardio, el sistema eléctrico del corazón
          produciendo arritmias, además de que acelera la aterosclerosis. Frecuentemente este daño puede manifestarse
          entre diez y quince años posteriormente a haber recibido la radiación.</p>
        <p>Algunos tipos de quimioterapia pueden disminuir la función del corazón, otros pueden elevar la presión
          sistémica/pulmonar, otros favorecer la formación de coágulos. De ahí la importancia de la vigilancia para
          poder identificar tempranamente y brindar un tratamiento apropiado.</p>

        <p class="descanso" style="color: var(--morado);">Trabajo conjunto</p>
        <p>Hay que concientizar a la población y al gremio médico sobre la importancia de la vigilancia cardiológica
          cuando se reciben tratamientos contra el cáncer. Crear una unidad de vigilancia cardio-onco-hematología para
          así prevenir, identificar y tratar oportunamente las complicaciones cardiovasculares asociadas a las terapias
          contra el cáncer. Mejorar la calidad y esperanza de vida de los pacientes en tratamientos contra el cáncer o
          que han sobrevivido algún tipo de cáncer.</p>
        <p>De igual manera, existen acciones que todas las personas pueden realizar para actuar desde la prevención:
          mantener su peso ideal, evitar fumar, moderar el consumo de alcohol, realizar 30 minutos diarios de ejercicio
          e incrementar consumo de frutas y verduras, todo ello disminuirá el riesgo de enfermedades cardiovasculares y
          de muchos tipos de cáncer.</p>
        <p>Si son supervivientes de un cáncer o si se encuentran recibiendo un tratamiento contra el cáncer, es
          importante obtener una valoración cardiológica para prevenir, identificar tempranamente y brindar un
          tratamiento apropiado de las posibles complicaciones cardiovasculares de los tratamientos contra el cáncer. <i class="fas fa-square"></i></p>
