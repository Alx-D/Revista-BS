      <div class="texto">
        <p>Las enfermedades cardiovasculares y el cáncer son las principales causas de muerte en el mundo. Los factores
          de riesgo para ambas enfermedades son muy similares: mayor edad, obesidad, hipertensión, diabetes mellitus,
          falta de actividad física, consumo de tabaco y alcohol.</p>
        <p>Con los avances generados en los tratamientos contra el cáncer, hemos observado un incremento en la esperanza
          de vida de estos pacientes. Sin embargo, algunos de estos tratamientos pueden producir daño al corazón
          (toxicidad cardiaca inducida por terapias contra el cáncer). Los sobrevivientes de cáncer tienen un riesgo de
          4 a 6 veces mayor de padecer enfermedades cardiovasculares.</p>

        <div class="rc-gris rc-izq">
          <p class="f2 rc-gris-cbz-1r">Preguntas importantes</p>
          <p class="f3 rc-gris-texto">Algunas preguntas que debe realizar al estar en valoración cardiológica:</p>
          <ul class="rc-gris-lista">
            <li>¿Cuál es mi riesgo cardiovascular?</li>
            <li>¿Qué factores de riesgo son modificables y cómo los puedo mejorar?</li>
            <li>¿Cuál es el riesgo de desarrollar toxicidad cardiaca mediada por terapias contra el cáncer?</li>
            <li>¿Se amerita de algún tratamiento farmacológico o medidas no farmacológicos para disminuir este riesgo?
            </li>
          </ul>
        </div>

        <p>El papel del cardiólogo en el cuidado de los pacientes oncológicos es prevenir: identificar tempranamente y
          tratar las complicaciones cardiovasculares asociadas a las terapias contra el cáncer, manteniendo estrecha
          comunicación con oncólogos y hematólogos para así evitar interrupciones en el tratamiento contra el cáncer y
          obtener los mejores desenlaces clínicos.</p>
        <p>Con el incremento en la esperanza de vida, cada vez es más frecuente tener cáncer y enfermedades
          cardiovasculares asociadas.</p>
        <p>Las personas con enfermedades cardiovasculares se encuentran en mayor riesgo de desarrollar toxicidad
          cardiaca inducida por terapias contra el cáncer. En ocasiones deben de hacerse modificaciones en el
          tratamiento contra el cáncer o ajustes en el tratamiento de enfermedades cardiovasculares para proteger al
          corazón.</p>
        <p>Los pacientes con insuficiencia cardiaca tienen mayor concentración de sustancias pro-cancerígenas en su
          sangre, las cuales pueden favorecer el crecimiento de tumores.</p>

        <p class="descanso" style="color: var(--morado);">Manejo en cardiología</p>
        <p>El protocolo para pacientes oncológicos consiste inicialmente en identificar a los pacientes que se
          encuentren en mayor riesgo de desarrollar daño al corazón secundario a los tratamientos contra el cáncer, por
          ejemplo, pacientes con factores de riesgo cardiovascular, aquéllos que han recibido quimioterapia con
          potencial para generar daño al corazón, radioterapia en la región torácica, entre otros.</p>
        <p>Incluye además prevención mediante control de factores de riesgo cardiovascular: control de niveles de
          colesterol, presión arterial, glucosa, suspensión de tabaco.</p>
        <p>Además se debe dar seguimiento: vigilar la función del corazón mediante ecocardiograma (ultrasonido del
          corazón), revisiones clínicas y otros estudios de gabinete como electrocardiograma, estudios de laboratorio,
          prueba de esfuerzo, entre otros.</p>
        <p>Cuando se identifica algún daño, hay que comunicarlo al oncólogo o hematólogo, pues en ocasiones es necesario
          hacer modificaciones en el tratamiento contra el cáncer para proteger al corazón.</p>

        <p class="descanso" style="color: var(--morado);">Estrecha relación</p>
        <p>Hay muchas maneras en las que las terapias contra el cáncer pueden dañar al corazón.</p>
        <p>El cáncer por sí mismo es un estado crónico inflamatorio que favorece a los infartos y la formación de
          coágulos en el
          cuerpo.</p>
