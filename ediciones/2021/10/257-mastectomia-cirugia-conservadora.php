        <div class="texto">
          <p>Todos estos factores son valorados en conjunto por la paciente y el médico tratante, y se toma la decisión más adecuada en cuanto al tratamiento oncológico y deseo de la paciente.</p>
          <p>La decisión entre una cirugía conservadora y mastectomía es muy personal para cada caso, ya que en ella influyen sus deseos, preferencias, estilo de vida y prioridades.</p>

          <p class="descanso" style="color: var(--morado);">Analizando las aristas</p>
          <p>Como en todo procedimiento quirúrgico, es de suma importancia analizar las ventajas y desventajas de cada opción previo a la toma de decisiones.</p>
          <p>La principal ventaja de la cirugía conservadora es la preservación de la mama, ya que sólo se busca resecar el tumor con márgenes tridimensionales óptimos y, por lo general, este procedimiento sólo causa cambios sutiles en la forma de la mama o deja una pequeña cicatriz; por lo tanto, tiene un menor impacto en los sentimientos o imagen corporal de la mujer.</p>
          <p>Este procedimiento se asocia a un menor tiempo quirúrgico y menor índice de complicaciones, comparado con una mastectomía.</p>
          <p>
            La principal desventaja de la cirugía conservadora es que debe ser complementada por un tratamiento con radioterapia por 5 a 7 semanas, con el objetivo de disminuir la posibilidad de recurrencia en el tejido mamario residual, por lo que siempre que se opte por este procedimiento debe valorarse la disponibilidad de este recurso previo a la cirugía. Otra desventaja sobre esta modalidad terapéutica es que el uso de la radioterapia añade sus efectos adversos.
            Una de las principales ventajas de realizar una mastectomía en mujeres con cáncer de mama en etapas tempranas es la posibilidad de omitir la radioterapia y así evitar los efectos adversos de esta modalidad de tratamiento.
          </p>
          <p>En cuanto al tiempo quirúrgico y recuperación, la mastectomía implica un aumento en ambos, así como una mayor posibilidad de complicaciones. Sin embargo, la gran mayoría de las pacientes que optan por una mastectomía cuentan con la posibilidad de realizarse una reconstrucción en el mismo tiempo quirúrgico.</p>
          <p>La decisión final de optar por una mastectomía o cirugía conservadora en pacientes de cáncer de mama debe ofrecer tranquilidad a la paciente desde el aspecto psicológico, emocional, social y oncológico.</p>

          <p class="descanso" style="color: var(--morado);">Momento de decidir</p>
          <p>Para pacientes con cáncer de mama en etapas tempranas no existe una diferencia en la supervivencia entre ambas modalidades de tratamiento, y afortunadamente una vez evaluados todos los factores que pueden influir en esta decisión, no existe una opción incorrecta para la paciente.</p>
          <p>La decisión por una modalidad u otra no debe precipitarse, es de suma importancia tomarse el tiempo para analizar con cautela todos los riesgos y beneficios de cada opción quirúrgica. Es importante mencionar, que el haberse realizado una cirugía conservadora no influye en la necesidad de administrar tratamiento sistémico (quimioterapia) posterior a la cirugía.</p>
          <p>El rol del cirujano oncólogo siempre que valora a una paciente con cáncer de mama en etapa temprana es informarla completamente de su enfermedad, pronóstico y opciones quirúrgicas.</p>
          <p>Se deben exponer siempre los beneficios, riesgos y posibles complicaciones de cada cirugía, así como el impacto en la calidad de vida de la paciente y en su entorno global.</p>
          <p>Para finalizar, la decisión acerca de una modalidad terapéutica es muy personal para cada paciente y debe basarse en toda la información científica disponible así como en las características propias de cada persona. <i class="fas fa-square"></i></p>
