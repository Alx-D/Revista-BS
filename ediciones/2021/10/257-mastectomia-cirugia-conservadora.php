      <div class="texto">
        <p>Las mujeres diagnosticadas con cáncer de mama en etapas tempranas cuentan con dos alternativas de tratamiento quirúrgico con resultados oncológicos similares: cirugía conservadora de mama y mastectomía.</p>
        <p>Una cirugía conservadora de mama implica la remoción del tumor con un margen de seguridad adecuado, logrando preservar el resto de la glándula mamaria. La mastectomía se refiere al retiro de la glándula mamaria por completo y en ciertos escenarios va acompañada de la resección de piel, complejo areola-pezón y ganglios de la axila.</p>
        <p>Cerca de un 80% de las pacientes con cáncer de mama temprano son candidatas a ambas opciones quirúrgicas, por lo tanto, es de suma importancia analizar todos los factores que puedan influir en la decisión de la paciente.</p>
        <p>Durante la valoración inicial por el cirujano oncólogo se toman en cuenta factores propios de la enfermedad como: etapa clínica, presencia de metástasis ganglionares axilares, tamaño tumoral, multicentricidad (dos o más lesiones en diferentes cuadrantes mamarios), multifocalidad (dos o más lesiones en mismo cuadrante). Además de características propias de cada paciente como: deseo de la paciente, antecedente de radioterapia en tórax, relación del tamaño de la mama-tumor, enfermedades tejido conectivo, entre otros.</p>

