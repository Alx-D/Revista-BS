      <div class="texto">
        <p>Es conocido ya que el espectro clínico de la infección por SARS-CoV-2 puede ir desde el asintomático hasta una neumonía fulminante. Los pacientes con mayor riesgo de mortalidad por Covid-19 son varones, adultos mayores y con comorbilidades asociadas como hipertensión, obesidad, diabetes, tabaquismo, enfermedad pulmonar obstructiva crónica, asma, insuficiencia renal y cáncer.</p>
        <p>Los pacientes oncológicos tienen un mayor riesgo de contagio y de desarrollar formas más graves de la enfermedad, sin embargo, la heterogeneidad del cáncer de mama plantea múltiples escenarios clínicos con diferentes probabilidades de complicación por Covid-19 y objetivos diversos en el tratamiento oncológico.</p>
        <p>Se ha recomendado que los servicios de oncología continúen activos y abiertos, ya que el retraso en el tratamiento tiene un impacto negativo en la sobrevida, por lo que deben existir estrategias de priorización acordes a los recursos de cada unidad médica para todas las modalidades de tratamiento, siempre es aconsejable el manejo multidisciplinario y no es negociable; el internet puede ser una herramienta adicional.</p>
        <p>Debe continuarse con las medidas de prevención ya suficientemente conocidas, evitando además la saturación de las salas de espera, de quimioterapia y de radioterapia.</p>
        <p>Para los pacientes que están en seguimiento y que no tengan síntomas, los estudios de vigilancia pudieran ser retrasados, sin embargo, si hay sintomatología de recurrencia tumoral, se indicará a la paciente que deberá solicitar una consulta oncológica.</p>

        <p class="descanso" style="color: var(--morado);">Recomendaciones para cirugía</p>
        <p>
          Se debe evitar en lo posible los diferimientos de la programación quirúrgica en pacientes con intento curativo, ya que está bien comprobado que desde los tres meses de retraso quirúrgico inicia una reducción en la supervivencia.
          Se deberá realizar prueba de PCR 24 horas previas a la cirugía.
        </p>
        <p>
          Las cirugías para biopsias con atipia, cirugía reductora de riesgo, reconstrucción mamaria y enfermedad benigna se pueden diferir
          y priorizar otros diagnósticos.
        </p>
        <p>Es importante que se informe con su cirujano oncólogo sobre los procedimientos que pueden diferirse y aquéllos que deben priorizarse, así como el protocolo a seguir en cualquier caso.</p>
