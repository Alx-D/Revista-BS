        <p class="descanso" style="color: var(--morado);">Recomendaciones en oncología médica</p>
        <p>Las unidades de quimioterapia ambulatoria deben de continuar sus actividades. No es recomendable suspender o retrasar los tratamientos sistémicos con intento curativo o paliativo.</p>
        <p>En pacientes bajo tratamiento paliativo con múltiples líneas de quimioterapia tratamiento, mal pronóstico y estado funcional comprometido puede considerarse enviar a mejor soporte médico.</p>
        <p>Si un paciente presenta una prueba positiva por PCR para SARS-CoV-2 debe de suspender su tratamiento hasta recuperación del cuadro y, en caso de no tener complicaciones, puede reiniciar su terapia sistémica entre los 14 y 21 días posteriores.</p>
        <p>Con el objetivo de disminuir los tiempos de estancia en la unidad médica y la movilidad de los pacientes, se pueden realizar las siguientes acciones:</p>
        <ul class="lista-disco-nota">
          <li>Preferir los esquemas trisemanales intravenosos, las terapias orales y subcutáneas que no comprometan el resultado terapéutico oncológico.</li>
          <li>Es pertinente que los pacientes acudan sin acompañante a sus tratamientos ambulatorios y consultas, excepto cuando se requiera de una asistencia continua.</li>
          <li>Las citas de seguimiento y control de síntomas deben ser indicadas con el mayor intervalo de tiempo y de ser factible por telemedicina.</li>
          <li>Es pertinente ampliar el uso de factor estimulante de colonias en esquemas con riesgo moderado (10-20%) y alto de neutropenia febril (>20%), sobre todo en adultos mayores.</li>
          <li>Los esteroides pueden ser utilizados, cuando se encuentre indicado.</li>
          <li>Las terapias sistémicas como la endocrina y las terapias anti Her2 pueden ser utilizadas como “puente” para evitar desfases en la atención oncológica.</li>
          <li>Se recomienda el uso de ácido zoledrónico y denosumab trimensual, para el manejo de metástasis óseas, de ser factible preferir denosumab por su aplicación subcutánea.</li>
        </ul>

        <p class="descanso" style="color: var(--morado);">Recomendaciones en radioterapia</p>
        <p>Durante esta pandemia, las acciones en el tratamiento de radioterapia se resumen en el acrónimo RADS (Remote visits, Avoid radiation, Defer radiation, Shorten radiation), que refieren visitas remotas (telemedicina), omitir radiación (en situaciones seguras para los pacientes), demorar radioterapia (siempre y cuando sea posible retrasarla para momentos de menor contagio por Covid-19), acortar el tratamiento (utilizar hipofraccionamiento en los casos posibles).</p>
        <p>Ante la sospecha de Covid-19 en alguna paciente con cáncer de mama que acude al servicio de radioterapia se recomiendan los siguientes lineamientos:</p>
        <ul class="lista-disco-nota">
          <li>Paciente que iniciará radioterapia y diagnóstico de Covid-19: no iniciar tratamiento hasta haber cumplido 14 días en confinamiento domiciliario. Reiniciará tratamiento según indicaciones por infectología.</li>
          <li>Paciente en tratamiento que resulta positivo a Covid-19: suspender tratamiento y enviar a confinamiento domiciliario por 14 días.</li>
          <li>Paciente Covid-19 positivo que amerita radioterapia urgente (compresión medular y síndrome de vena cava superior): se administrará tratamiento; no se recomienda omitir este tratamiento a pacientes que reciban un beneficio en supervivencia y/o control local.</li>
        </ul>

        <p class="descanso" style="color: var(--morado);">Vacunación</p>
        <p>Si el paciente oncológico tiene acceso a un esquema de vacunación anti-Covid, es deseable que lo reciba. Si el paciente se encuentra en tratamiento sistémico activo deberá restringirse la aplicación de la vacuna, en caso de presentar neutropenia grave y/o neutropenia febril. No se recomienda administrar una vacuna anti-Covid durante los 14 días posteriores a procedimiento quirúrgico. Las pacientes en vigilancia por cáncer de mama pueden ser vacunadas. <i class="fas fa-square"></i></p>
