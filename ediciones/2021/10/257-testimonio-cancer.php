      <div class="texto">
        <p>Un cáncer de mama detectado en el año 2017 dio un giro a la vida Elena. El camino andado para recuperar su
          salud le permite ahora, a sus 49 años, hacer una retrospectiva para apreciar aquellas situaciones que en
          verdad enriquecen su vida.</p>
        <p>Con su siempre alegre carácter, desde el confinamiento en compañía de Kenya, su perrita pastor alemán,
          compartió con Revista BS su experiencia como sobreviviente de cáncer de mama.</p>

        <p style="margin-bottom: 0;"><strong>¿Puedes hablarme de cuál fue tu diagnóstico?</strong></p>
        <p><i>“El diagnóstico fue carcinoma ductal infiltrante grado II. Según mis médicos fue muy oportuna la
            detección, ya que el tumor era agresivo”.</i></p>

        <p style="margin-bottom: 0;"><strong>¿Cómo fue la detección, tú te sentiste algo o directamente fue en un
            estudio?</strong></p>
        <p><i>“Sentí molestia en el seno, a veces era ardor y otras, dolor, pero no era constante. Acudí al ginecólogo,
            él me mandó a realizar la mamografía y el ultrasonido. Los resultados sugerían realizar una biopsia y me
            realizaron todo súper rápido. Cuando menos pensé ya estaba en la etapa de las quimios”.</i></p>

        <p style="margin-bottom: 0;"><strong>¿Cuál ha sido tu tratamiento?</strong></p>
        <p>“El cirujano oncólogo, al ver los resultados, me programó la cirugía. Fue cuadrantectomía. Sólo quitaron una
          parte del seno, posteriormente inicié quimioterapias, vacunas, radioterapias, inmunoterapia, etcétera. Además
          de mis controles periódicos”.</p>

        <p style="margin-bottom: 0;"><strong>¿Cómo fue para ti recibir un diagnóstico de cáncer, cómo impactó tu
            vida?</strong></p>
        <p><i>“Para ser honesta, te mueve tu mundo en cuanto escuchas esa palabra, ya que nadie estamos preparados para
            que nos la digan. Luego pasas por todos los procesos de un duelo, pero te das cuenta que gracias a Dios y a
            los médicos, los tratamientos han evolucionado, ya no son tan invasivos. Y, de repente, ya estás a la mitad
            del tratamiento y es cuando te das cuenta que eres más fuerte de lo que creías, que estás rodeada de amor y
            cariño por parte de tus seres queridos, que la vida cambió pero de una forma que te permite apreciar o
            valorar más los pequeños detalles cotidianos, te das cuenta que las cosas como le llaman ahora “tóxicas” no
            las necesitas y las alejas de tu vida, simplemente tienes ganas de vivir feliz”.</i></p>

        <p style="margin-bottom: 0;"><strong>¿Cuál es tu estado actual?</strong></p>
        <p><i>“Estoy inmunoterapia pero también en hormonoterapia, la verdad ya he dejado de preguntar porque no quiero
            estar ansiosa por llegar al término de mi tratamiento, simplemente decidí vivir un día a la vez”.</i></p>

        <p style="margin-bottom: 0;"><strong>¿Cómo ha sido sobrellevar la pandemia como paciente oncológica?</strong>
        </p>
        <p><i>“Al principio, creo que como a todos, me afectó el encierro, después me he ido acostumbrando, pero sí
            extraño las reuniones presenciales entre amigos, abrazar a mis seres queridos. Lo bueno es que busqué clases
            virtuales y he aprendido a cocinar, estoy tomando clases en línea de idiomas. Pero sí extraño salir, espero
            que pronto podamos volver a retomar el ritmo de vida”.</i></p>

        <p style="margin-bottom: 0;"><strong>¿Qué es lo que más te ha servido para llevar tu tratamiento?</strong></p>
        <p><i>Sinceramente, el cariño de mis seres queridos, que no me han dejado para nada, y también de mis médicos y
            su personal, ¡son lo máximo! También he aprendido más de Dios, sobre todo que no nos deja solos ante la
            adversidad”.</i></p>

        <p style="margin-bottom: 0;"><strong>¿Qué consejo le darías a otras personas que estén frente a un diagnóstico
            similar?</strong></p>
        <p><i>“No hay por qué tener miedo. Nos toca ponernos el traje de valientes. Sé que no es fácil, pero hoy en día
            hay más posibilidades de salir victoriosos si nos lo detectamos a tiempo. El cáncer de mama es una
            enfermedad que ataca tanto hombres como mujeres y todos debemos estar siempre alertas de cualquier cambio en
            nuestros senos, no solamente en octubre”.</i> <i class="fas fa-square"></i></p>
