          <p>La alta prevalencia de diabetes tiene como resultado el desarrollo de otras enfermedades entre las que se destacan cardiopatías, ceguera y nefropatía, entre otras que merman la calidad de vida de los pacientes.</p>
          <p>“Se vuelve un problema de salud púbica por la cantidad de personas que se ven incapacitadas para poder seguir laborando como consecuencia de las secuelas que produce la diabetes, y los costos que genera para el estado atender y subsanar la atención de todos esos pacientes, pues ponen en crisis a todos los sistemas de salud del mundo”, explicó en entrevista con Revista BS el doctor <b>Sergio Guerra Landavazo</b>.</p>
          <p>El médico internista hizo especial énfasis en la importancia de mantener la diabetes y otras enfermedades crónicas bajo control para así evitar el desarrollo de sus complicaciones.</p>
          <p>“La diabetes bien controlada es una persona normal, como cualquiera otra persona. Creemos que es nada más tratar una cifra de azúcar, y lo que realmente hacemos al controlar la diabetes es tratar de prevenir complicaciones que produce”, destacó.</p>
          <p>El impacto final de la diabetes y otras enfermedades crónicas trasciende al individuo y afecta a la familia en conjunto debido a los costos de la enfermedad y la incapacidad que muchas veces causa en el paciente.</p>
          <p>“Termina mermando la calidad de vida de los propios pacientes y de su familia, que tienen que brindarle atención y hacerse cargo de los costos que genera la enfermedad y sus complicaciones”, apuntó.</p>

          <p class="descanso">Atender a tiempo</p>
          <p>Una vez establecido el diagnóstico de diabetes, resulta clave llevar un control periódico con un especialista que le asesore para mantener en control su enfermedad.</p>
          <p>“Es muy importante llevar el seguimiento de las enfermedades crónicas y degenerativas, con una persona que le asegure que lleva un control adecuado, que le vea de forma integral y que pueda manejar en forma adecuada todos los padecimientos asociados”, recomendó el especialista.</p>
          <p>Desde una mirada más amplia aun, es crucial prevenir la diabetes actuando desde diferentes flancos.</p>
          <p>“Necesitamos controlar la crisis de obesidad que existe en el país, para eso se pueden establecer muchas campañas públicas”, dijo, “pero también tenemos que hacer conciencia y establecer medidas de control en la casa y optar por estilos de vida más saludables: alimentación, ejercicio, evitar el sedentarismo”.</p>
          <p>Para finalizar, el doctor Guerra comentó que es importante monitorear la salud y realizar análisis anualmente en búsqueda de diabetes o riesgo, sobre todo cuando se tienen antecedentes familiares para padecerlas.</p>
          <p>“Cuando detectamos la diabetes desde fases tempranas podemos controlarlas y prevenir todas sus complicaciones, cuando la detectamos en fases avanzadas podemos evitar que progresen”, finalizó el especialista. &nbsp;<i class="fas fa-square remate"></i></p>
