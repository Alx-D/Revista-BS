          <p>Conformando uno de los tratamientos más innovadores para el manejo de los pacientes diabéticos, la terapia de oxigenación hiperbárica puede ayudar a estos pacientes a prevenir y aliviar úlceras causadas por el conocido pie diabético, al mejorar la oxigenación de los tejidos que están más lejos del corazón.</p>
          <p>“El problema que tienen los pacientes con diabetes es la poca irrigación sanguínea hacia los pies. Hay poca oxigenación, lo que genera un tejido hipóxico, que invariablemente se va a traducir en una herida que puede infectarse y trascender a una infección hasta el hueso y finalmente, una amputación”, explicó en entrevista con Revista BS <b>Oscar Daniel Flores López</b>, especialista en fisioterapia deportiva y oxigenación hiperbárica.</p>
          <p>Además del suministro de oxígeno puro al 100 por ciento, esta terapia incluye la elevación de la presión atmosférica dentro de la cámara para que el oxígeno se transporte por el plasma sanguíneo, llegando así a tejidos que están sufriendo por falta de buena irrigación.</p>
          <p>Es importante señalar que la medicina hiperbárica se puede utilizar en diferentes etapas del tratamiento del paciente diabético. Puede ser tanto preventiva, cuando no existen heridas, para mejorar la irrigación sanguínea; así como para ayudar a sanar úlceras, e inclusive, tras una amputación, para coadyuvar a una pronta cicatrización de la respectiva herida.</p>
          <p>Flores López explicó que existen muy pocas contraindicaciones absolutas para esta terapia: pacientes que tienen una enfermedad pulmonar obstructiva crónica, haber tenido neumotórax (colapso del pulmón) o si se tienen burbujas pulmonares.</p>

          <p style="color: #ff6b5b;" class="descanso-esp">Atención en conjunto</p>
          <p>Debido a la diversidad de complicaciones que un paciente con diabetes puede llegar a desarrollar, el control de esta enfermedad entre las diferentes especialidades implicadas es clave en su manejo.</p>
          <p>“Tenemos que estar muy bien comunicados el nutriólogo, el angiólogo, la medicina hiperbárica, etcétera, para poder desarrollar un buen plan de tratamiento, y así poder llevar por un buen camino al paciente, para que no tenga complicaciones”, subrayó el entrevistado.</p>
          <p>Aludiendo a la máxima de que prevenir es mejor que curar, el especialista destacó la importancia de actuar a conciencia para prevenir y detectar a tiempo la diabetes.</p>
          <p>“Es muy importante estarse haciendo estudios de laboratorio, para ver cómo está la glucosa en sangre, y también los triglicéridos y el colesterol, porque aquí en Sonora tenemos altos índices de pacientes que adquieren diversas enfermedades metabólicas”, finalizó Oscar Daniel Flores López. &nbsp;<i class="fas fa-square remate-esp"></i></p>
