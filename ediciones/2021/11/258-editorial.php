      <div class="texto">
        <p>La diabetes es un problema de salud pública mundial y su manejo efectivo, una prioridad, más de 463 millones
          de personas en todo el mundo viven con esta enfermedad <span class="cita">(“Diabetes care”, Health at a Glance
            2021, OECD).</span></p>
        <p>“La diabetes es un padecimiento en el cual el azúcar (o glucosa) en la sangre se encuentra en un nivel
          elevado. Esto se debe a que el cuerpo no produce o no utiliza adecuadamente la insulina, una hormona que ayuda
          a que las células transformen la glucosa que proviene de los alimentos en energía. Sin la suficiente insulina,
          la glucosa se mantiene en la sangre y con el tiempo, este exceso puede tener complicaciones graves”, <span class="cita">(“Diabetes en México”, Instituto Nacional de Salud Pública).</span></p>
        <p>Las defunciones a causa de la diabetes mellitus en México se ubican entre las principales causas de muerte
          desde el año 2000, en 2019 ocuparon el segundo lugar a nivel global, entre mujeres y entre hombres.</p>
        <p>El año 2019 ocurrieron 104 mil 314 defunciones por esta enfermedad, 49.6% en hombres (51 mil 711) y 50.4% en
          mujeres (52 mil 643). Sólo son superadas por las enfermedades del corazón que son la causa principal de
          defunción <span class="cita">(Estadística de Defunciones Registradas 2019, Nota Técnica, Instituto Nacional de
            Geografía y Estadística, INEGI).</span></p>
        <p>Durante 2020, se registraron 1 millón 69 mil 301 defunciones en México, 92.4% (1 millón 3 mil 645) de ellas
          se debieron a enfermedades y problemas relacionados con la salud, observando un aumento del 3.6% con respecto
          a 2019.</p>
        <p>Las tres principales causas de muerte a nivel nacional son enfermedades del corazón (218 mil 885, 20.2%),
          Covid-19 (201 mil 163, 18.5%) y diabetes mellitus (151 mil 214, 13.9%), lo anterior de acuerdo al Comunicado
          de Prensa Núm. 402/21, del 29 de julio de 2021, INEGI.</p>
        <p>En lo que respecta a las muertes por diabetes, se presentaron 46 mil 860 casos más que en 2019; de ellos, 3
          mil 515 casos (2.33%) correspondieron a la diabetes mellitus insulinodependiente.</p>
        <p>En Sonora el año 2020 se presentaron un total de 28 mil 152 defunciones, las principales causas de muerte
          fueron Covid-19 con 6 mil 407, enfermedades del corazón con 5 mil 998, tumores malignos con 2 mil 582 y
          diabetes mellitus con 2 mil 319, de acuerdo a la información de la Secretaría de Salud.</p>

        <p class="descanso">Actuar a tiempo</p>
        <p>Para efectos de prevención debemos considerar los factores de riesgo para la diabetes: edad, obesidad,
          sedentarismo, alimentación inadecuada, antecedentes familiares y algunos factores genéticos.</p>
        <p>La diabetes es una causa principal de enfermedad cardiovascular, ceguera, falla renal, impotencia sexual y
          amputación de miembros inferiores. Por ello es sustancial el manejo adecuado de la enfermedad al igual que la
          presión arterial, evitar el tabaco y control de lípidos para reducir complicaciones.</p>
        <p>Durante la pandemia se evidenció que la diabetes constituye un factor de riesgo importante para
          hospitalización y muerte por Covid-19, desafortunadamente también en esta contingencia se ha interrumpido el
          manejo rutinario de la diabetes y los medicamentos escasean.</p>
        <p>Para un control efectivo de los niveles de glucosa se requiere asistir con regularidad a la atención primaria
          de la salud para un manejo integral con médico, nutriólogo, psicólogo, activador físico, promotor de la salud,
          dentista, fisioterapeuta; realizarse pruebas de glucosa en sangre, apego a los medicamentos y recomendaciones;
          así como un estilo de vida saludable. Éste debe incluir alimentación rica en fibra, moderar el consumo de
          carbohidratos y bocadillos con alto contenido calórico, disminuir los alimentos altos en grasas saturadas,
          grasas trans y alto contenido de sodio, evitar las bebidas azucaradas y refrescos, así como el consumo de
          alcohol.</p>
        <p>Realizar actividad física con regularidad, mantener un peso saludable, no fumar, levantarse unos minutos
          después de un periodo prolongado de estar sentado. El objeto de todas estas medidas es retrasar las
          complicaciones y muertes prematuras, detectar y diagnosticar oportunamente en sus visitas al médico aquellos
          signos de alarma que permitan una atención especializada expedita.
        </p>Consulte a su médico, procure la Buena salud y aproveche las orientaciones de nuestros colaboradores en este
        número especial. &nbsp;<i class="fas fa-square"></i></p>
