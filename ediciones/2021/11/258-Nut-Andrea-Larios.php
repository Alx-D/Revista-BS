          <p>Revista BS charló con la nutrióloga <b>Andrea Larios Coronado</b> para conocer su punto de vista respecto al manejo desde su especialidad de los pacientes con diabetes, compartimos sus comentarios.</p>

          <p class="texto-preg">¿Cuáles son los principales ajustes que debe hacer en su alimentación una persona que recibe diagnóstico de diabetes?</p>
          <p>Lo primero que se pregunta una persona con diagnostico de diabetes es: ¿Qué puedo comer? La alimentación es uno de los pilares del tratamiento y resultado del paciente con diabetes, y a su vez, uno de los más difíciles de seguir.</p>
          <p><i>En la actualidad, se acepta que no existe una “dieta para personas con diabetes”; sino una serie de recomendaciones individualizadas para cada persona en función de sus características.</i></p>
          <p><i>La base del tratamiento es la educación para el autocontrol del paciente, la combinación de fármacos, dieta y ejercicio. La distribución de los nutrientes de la dieta para la población que vive con diabetes es la misma que se recomienda a la población general.</i></p>
          <p><i>Por su parte, la cantidad y horario de toma de los hidratos de carbono se debe adaptar a la pauta farmacológica y a los hábitos de ejercicio físico del paciente.</i></p>
          <p><i>Las proteínas deben representar del 10 al 30 por ciento del aporte calórico total. El porcentaje de energía procedente de la grasa se determinará en función del peso corporal, las glucemias y el perfil lipídico.</i></p>

          <p class="texto-preg">Teniendo en cuenta la alimentación y actividad física en Sonora, ¿cómo influyen tales hábitos en la recurrencia de la diabetes?</p>
          <p>Sonora se encuentra dentro de las entidades con mayor número de sobrepeso y obesidad a nivel nacional, indicado por el Centro de Investigación en Alimentos y Desarrollo (CIAD), en agosto de 2020.</p>
          <p><i>La alimentación de la población de Sonora se constituye en un 55 a 60 por ciento de carbohidratos, destacando: refrescos de cola, refrescos de sabor, tés industrializados, bebidas carbonatadas, cereal para desayuno con alto contenido de azúcar, galletas, dulces, frituras de maíz, etcétera.</i></p>
          <p><i>Un 34 a 41 por ciento de grasa, lo que sobrepasa la recomendación diaria de ser menor al 30 por ciento.</i></p>
          <p><i>Destaca el consumo elevado de comida rápida o de la calle. Por lo cual el aumento del índice de masa corporal (IMC) se asocia con un mayor riesgo de sufrir diabetes.</i></p>

          <p class="texto-preg">¿Qué acciones debería tomar la población general para bajar la tasa de enfermedad y muerte por diabetes?</p>
          <p>Como forma preventiva para disminuir esta enfermedad es crear un estilo de vida saludable: consumir alimentos ricos en fibra, cereales integrales, leguminosas.</p>
          <p><i>Hacer actividad física regularmente, reducir las bebidas carbonatadas y darle mayor lugar al agua. Reducir el número de productos industrializados.</i> &nbsp;<i
            class="fas fa-square remate"></i></p>
