          <p>El elevado nivel de glucosa ocasionado por la diabetes mellitus genera daño vascular en diferentes órganos;
            los riñones se encuentran en especial riesgo y deben ser motivo de vigilancia periódica.</p>
          <p>“La diabetes es la principal causa de enfermedad renal crónica en el mundo, pues ocasiona toxicidad a nivel
            renal, de ahí que tenemos establecido niveles "meta" de glucosa, para evitar ese daño”, expuso en entrevista
            con Revista BS el doctor <b>Fernando Platt Soto</b>.</p>
          <p>El especialista en nefrología señaló que una vez establecido el diagnóstico, las principales
            recomendaciones son tener control periódico con internista o endocrinólogo y así mantener cifras controladas
            de glucosa de la forma más constante posible.</p>
          <p>“Sabemos que es difícil controlar la diabetes 100% todo el tiempo, pero mientras mejor se mantenga, menores
            complicaciones asociadas tendrá, y éstas pueden ser desde problema renal, hasta llegar a necesitar diálisis,
            así como problemas del corazón, embolias, coágulos en arterias de las piernas, ceguera, etcétera”, subrayó.
          </p>

          <p class="descanso-esp" style="color: #ff6b5b;">Atención a tiempo</p>
          <p>La labor del nefrólogo en pacientes con diabetes es fundamental, sobre todo hay que entender que su
            participación en el cuidado del paciente diabético desde un primer momento se traducirá en un mejor panorama
            para su salud renal.</p>
          <p>“Lamentablemente, en nuestra práctica cotidiana es frecuente que nos envíen a valorar pacientes cuando ya
            tienen un daño renal avanzado por la diabetes, y el paciente en muchos casos desconocía el daño renal hasta
            que nos llega al consultorio”, comentó. “Todo esto limita las posibilidades de mejorar el pronóstico de la
            nefropatía diabética y se logra un menor éxito en el tratamiento”.</p>
          <p>Platt Soto señaló que la periodicidad de la consulta nefrológica se determinará tomando en cuenta las
            características propias del paciente, así como el estado de su enfermedad.</p>
          <p>“Es muy importante que le den la importancia que se merece a la diabetes, y de esta manera no permitirle
            que cause daños a sus órganos. Visiten a un nefrólogo para revisar si hay datos de afección a los riñones,
            es mejor detectarlos de manera temprana para así preservar su función el mayor tiempo posible”, finalizó el
            especialista. &nbsp;<i class="fas fa-square remate-esp"></i></p>
