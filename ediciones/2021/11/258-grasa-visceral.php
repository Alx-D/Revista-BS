      <div class="texto">
        <p>Cuando se consume energía en exceso, proveniente de alimentos y bebidas, el cuerpo la almacena en forma de
          grasa en el tejido adiposo, que se distribuye en las distintas regiones del cuerpo.</p>
        En particular, la obesidad se presenta cuando <p>ocurre una acumulación excesiva de grasa corporal y ésta puede
          provocar daños a la salud; pero dependiendo en qué parte del cuerpo se localice, representa un mayor riesgo
          para el desarrollo de enfermedades no transmisibles.</p>
        <p>Considerando, entonces, la distribución de la grasa corporal, la obesidad se puede catalogar como obesidad
          general u obesidad abdominal.</p>
        <p>La obesidad abdominal se caracteriza por una mayor concentración de grasa en la cintura, la cual se conoce
          como grasa visceral, ésta se encuentra almacenada en la cavidad abdominal rodeando los órganos internos.</p>
        <p>Esta grasa es considerada un factor de riesgo cardio-metabólico pues su almacenamiento propicia diversas
          alteraciones metabólicas, incluyendo la resistencia a la insulina y la elevación en los niveles de
          triglicéridos, glucosa y colesterol; que a su vez aumentan el riesgo de padecer enfermedades no transmisibles
          como hipertensión, síndrome metabólico, diabetes tipo 2, enfermedad cardiovascular, entre otras.</p>

        <p class="descanso">Detección</p>
        <p>Hoy en día, existen equipos de imagen que utilizan rayos-X para determinar el contenido de grasa visceral;
          sin embargo, aunque son métodos rápidos y no invasivos, suelen ser muy costosos.</p>
        <div class="rc-color naranjac rc-der">
          <div class="rc-foto-bx">
