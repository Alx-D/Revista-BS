          <p class="f2 rc-color-cbz-1r guion">Modifique sus hábitos</p>
          <ul class="rc-gris-lista">
            <li>Evite alimentos fritos o altos en grasa saturada y colesterol</li>
            <li>Huya de carnes procesadas, refrescos y bebidas endulzadas</li>
            <li>Aumente el consumo de frutas y fibra</li>
            <li>Realice al menos 150 minutos semanales de ejercicio aeróbico</li>
            <li>Evite el consumo de alcohol y tabaco</li>
          </ul>
        </div>
        <p>También diversos estudios han visto que el consumo de ciertos alimentos favorece el almacenamiento de grasa
          visceral. Ejemplo de éstos son los alimentos fritos o altos en grasa saturada y/o colesterol, las carnes
          procesadas, los refrescos y otras bebidas endulzadas.</p>
        <p>Por el contrario, se ha observado que un mayor consumo de frutas, fibra y productos lácteos bajos en grasa se
          relaciona con menor acumulación de grasa visceral, incluso con su disminución.</p>
        <p>También se ha visto que realizar ejercicio aeróbico de intensidad moderada a vigorosa por lo menos 150
          minutos a la semana puede disminuir la cantidad de grasa visceral, al igual que el peso corporal.</p>
        <p>Entre otros hábitos de estilo de vida que se relacionan con la acumulación de grasa visceral se encuentran el
          consumo de alcohol, el tiempo que permanecemos sentados durante el día y fumar.</p>
        <p>Si bien, el aumento del contenido de grasa visceral es el resultado de la interacción compleja entre muchos
          factores, la mayoría de ellos son hábitos modificables; por lo que diversos expertos en el área concuerdan que
          consumir una dieta balanceada y realizar ejercicio diariamente puede disminuir el contenido de grasa visceral.
