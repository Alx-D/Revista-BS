          <p>Los altos niveles de azúcar en la sangre derivados de la diabetes mellitus van dañando progresivamente los
            vasos sanguíneos de diferentes órganos, en el caso de los riñones, este daño provoca su mal funcionamiento
            y, a largo plazo, se convertirá en insuficiencia renal.</p>
          <p>“La enfermedad de los riñones significa que éstos están dañados y no pueden filtrar la sangre como
            deberían”, detalló en plática con Revista BS el doctor <b>Joel Ernesto Verdugo Correa</b>.</p>
          <p>El especialista en nefrología comentó que la diabetes es la principal causa de las enfermedades de los
            riñones y también es la primera razón para requerir diálisis, de ahí la importancia de llevar un seguimiento
            nefrológico desde la detección de la diabetes.</p>
          <p>“Cuando se acude de manera tardía, muchas veces la única opción que hay de tratamiento es iniciar ya sea la
            diálisis peritoneal, hemodiálisis o bien, el trasplante renal”, señaló.</p>

          <p class="descanso">Detección a tiempo</p>
          <p>En los pacientes con diabetes es necesario realizar periódicamente un examen de orina en busca de exceso de
            albúmina, ya que una cantidad alta de esa proteína es una señal clara de daño renal; el especialista
            recomendó realizar este estudio una vez al año, así también acudir a revisión con el nefrólogo en ese mismo
            lapso.</p>
          <p>“Teniendo en mente que la diabetes ocupa el primer lugar en conducir a la necesidad de diálisis, todo
            paciente debe acudir al nefrólogo a valoración anual al momento del diagnóstico de diabetes mellitus tipo 2
            y a los cinco años del diagnóstico de diabetes mellitus tipo 1”, compartió el entrevistado.</p>
          <p>La diabetes no es estrictamente una condena de daño renal, llevar un estilo de vida saludable puede ayudar
            a tener niveles de glucosa en sangre y presión arterial ideales y así preservar la salud de los riñones.</p>
          <p>Dejar de fumar, llevar un plan alimenticio con la asesoría de un nutriólogo, limitar el consumo de sal y
            sodio, hacer actividad física como parte de su rutina, mantener un peso saludable y dormir de siete a ocho
            horas todas las noches son las principales pautas para mantener buena salud renal.</p>
          <p>“La diabetes es un ejemplo de enfermedad con la cual, al darle al paciente las herramientas, puede
            manejarse muy bien. Es por ello que controlar la diabetes es importante, si no lo hace, hay consecuencias
            con las que tendrá que lidiar más adelante en la vida”, advirtió el doctor Joel Ernesto Verdugo Correa.
