        <p class="descanso">Derribemos mitos</p>
        <p>Existen algunas ideas recurrentes en torno a la alimentación de las personas con diabetes, a continuación
          comparto algunos mitos, así como la opinión especializada al respecto.</p>
        <br>
        <p style="color: #344497;"><strong>Mito: <i>“Las personas que viven con diabetes tienen que reducir
              drásticamente los carbohidratos”.</i></strong></p>
        <p><strong>Realidad:</strong> Los pacientes con diabetes no deben de reducir drásticamente los carbohidratos, ni
          mucho menos, quitarlos por completo de su dieta.</p>
        <p>El objetivo es conocer el tipo de carbohidratos y controlar las cantidades. Los alimentos ricos en hidratos
          de carbono son una importante fuente de energía para nuestro organismo y forman parte de una dieta saludable y
          equilibrada.</p>
        <br>
        <p style="color: #344497;"><strong>Mito: <i>“Las personas que viven con diabetes tienen una serie de alimentos
              prohibidos”</i></strong></p>
        <p><strong>Realidad:</strong> Es muy común escuchar que las personas que viven con diabetes no pueden comer
          ciertos alimentos como plátanos, mango o pan.</p>
        <p>Realmente no existen alimentos prohibidos para las personas con diabetes, aunque es importante controlar el
          consumo de aquéllos ricos en hidratos de carbono en función del tratamiento, los niveles de azúcar en sangre y
          de las recomendaciones del profesional de salud.</p>
        <br>
        <p style="color: #344497;"><strong>Mito: <i>“Las personas que viven con diabetes deben tomar alimentos
              especiales para diabetes”.</i></strong></p>
        <p><strong>Realidad:</strong> Una persona que vive con diabetes no tiene que tomar “alimentos especiales”, ni
          comer de forma diferente que el resto de su familia.</p>

