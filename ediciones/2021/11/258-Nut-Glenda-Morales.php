          <p>La creencia de que una dieta por un periodo de tiempo curará la diabetes es uno de los grandes desafíos para los profesionales de la salud que acompañan a pacientes con este padecimiento.</p>
<p>“Una vez que está diagnosticada, la diabetes no se cura, se controla. Es muy común pensar que si bajan de peso y cambian su dieta por un par de meses, se revertirá la enfermedad. Luego quieren volver a los malos hábitos en cuanto se regulan”, señaló en charla con Revista BS la nutrióloga <b>Glenda Morales Becerra</b>.</p>
<p>La maestra en nutrición clínica explicó que existen otros mitos que dificultan el manejo de los pacientes con diabetes, como creer que la enfermedad puede curarse con algunas hierbas o que algunos alimentos “light” pueden sustituir a sus versiones regulares.</p>
<p>“Se cree que la Coca Cola Light es un buen sustituto para la Coca regular, pero no lo es porque contiene azúcar tipo aspartame, que también tiene efectos en los niveles de insulina del paciente”, detalló.</p>

<p class="descanso">Momento de cambios</p>
<p>La especialista habló de los ajustes que debe realizar una persona que ha recibido diagnóstico de diabetes.</p>
<p>“Lo primerito es quitar azúcares simples, harinas refinadas, dulces y soda”, apuntó. “Por lo general, el paciente recién diagnosticado con diabetes toma una cantidad grande de refrescos o bebidas azucaradas”.</p>
<p>Frutas de bajo índice glicémico, como frutos rojos, manzana verde y papaya, son las más recomendadas para la nueva dieta del paciente diabético, así como versiones integrales de pan, pasta y arroz, para sustituir a las harinas refinadas.</p>

<p class="descanso">Prevención</p>
<p>Teniendo en cuenta que mantener un peso saludable reduce hasta en un 80% la probabilidad desarrollar diabetes, Morales Becerra destacó algunos de los hábitos que en Sonora están relacionados con los altos índices de obesidad y a su vez con diabetes, triglicéridos, colesterol y problemas cardiovasculares.</p>
<p>“La tortilla de harina, la cerveza y la carne crean la mayor cantidad de problemas metabólicos asociados a la obesidad en el sonorense”, indicó.</p>
<p>De forma general son varios los flancos desde los que se puede prevenir la diabetes: salir a caminar cuatro veces por semana y llevar una dieta rica en cereales integrales, frutas y verduras. Además si se cuenta con antecedentes familiares para diabetes hay que llevar un monitoreo periódico de su estado de salud.</p>
<p>“Si tiene un familiar directo con diabetes, debería realizarse laboratorios al menos una vez al año para revisar que sus niveles de glucosa se encuentran normales”, recomendó.&nbsp;<i
