          <p>Hablar de detección temprana en una enfermedad que permanece silenciosa hasta que sus complicaciones han avanzado implica entonces una búsqueda intencionada por parte de las personas, sobre todo aquéllas que saben que tienen factores de riesgo.</p>
          <p>“Los datos de alarma en diabetes como orinar mucho, comer mucho, tomar mucha agua, nos habla de enfermedad muy avanzada. No deberíamos esperar nunca a ver estos síntomas. Cualquier persona que no esté en su peso ideal y que tenga antecedentes familiares de diabetes debería darle prioridad a su detección temprana”, advirtió en charla con Revista BS el doctor <b>Rubén Cardoza Encinas</b>.</p>
          <p>El especialista en medicina interna destacó el hecho de que la detección de la diabetes mellitus es barata, en contraposición con los grandes gastos que implica tratar a largo plazo las consecuencias de este padecimiento.</p>
          <p>“La enfermedad de la diabetes es lentamente progresiva al deterioro, durante todo ese tiempo se requieren grandes insumos, grandes cantidades de dinero para poder mantener tratamientos que son correctivos de algo que se pudo haber prevenido”, sentenció.</p>
          <p>Cardoza Encinas dio algunas pautas para actuar desde la prevención: huir de alimentos procesados preparando uno mismo todo lo que consume, evitar bebidas endulzadas recurriendo al agua simple y mantenerse activo llevando su conteo de pasos diarios son acciones que hacen la diferencia de acuerdo al especialista.</p>

          <p class="descanso">Atención integral</p>
          <p>En la actualidad, comentó el entrevistado, el tratamiento de la diabetes ha avanzado notoriamente con más medicamentos cada vez más seguros, ofreciendo a los pacientes alternativas para cada situación particular.</p>
          <p>Parte esencial del manejo del paciente diabético por parte del médico internista es conocer cada caso y tratarlo de manera completa y personalizada.</p>
          <p>“Se pretende llevar un control integral de todos los factores de riesgo que nos llevan a todas las complicaciones. El paciente habitualmente tiene otras enfermedades como hipertensión, problemas del colesterol o los triglicéridos, fumar u obesidad. Se tienen que controlar todos los factores de riesgo y vigilar la progresión en los principales órganos que pueden verse dañados por la diabetes”, subrayó.</p>
          <p>Para entender la seriedad de la diabetes, el doctor Cardoza hizo la comparación con la hipertensión: tanto diabetes como hipertensión pueden ser factor de riesgo para muerte cardiovascular, no obstante, la diabetes además genera problemas en riñones, neurológicos, y en la salud vascular.</p>
          <p>“Mucha gente tiene miedo a un diagnóstico, cuando el miedo debería ser por tener la enfermedad y no detectarla. Lo que deberíamos temer es llegar a las complicaciones, por lo tanto, exhorto a todos a revisarse, a prevenir y a tratar de manera temprana”, concluyó. &nbsp;<i class="fas fa-square remate"></i></p>
