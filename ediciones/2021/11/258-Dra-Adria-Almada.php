          <p>Para combatir la diabetes como un problema de salud pública es necesario entender sus proporciones a nivel social y su estrecha relación con la obesidad en México.</p>
          <p>“Vivimos en un país obesogénico; siete de cada diez mexicanos padecen sobrepeso u obesidad, y es bien sabido que una de las principales causas de la diabetes es la resistencia a la insulina generada por la obesidad”, explicó a Revista BS la doctora <b>Adria Jazmín Almada Córdova</b>.</p>
          <p>La especialista en medicina interna hizo énfasis en que la diabetes es una enfermedad que a largo plazo puede mermar la calidad de vida de quien la padece pues afecta órganos vitales como ojos, riñones, cerebro y corazón, de ahí la importancia de prevenirla o detectarla a tiempo para llevar un buen manejo de la enfermedad.</p>
          <p>“En los primeros años tiende a ser una enfermedad asintomática, pero precisamente son los primeros años donde se pueden prevenir las complicaciones a largo plazo, si se logra estar en metas de control”, apuntó.</p>
          <p>Resulta importante destacar que si usted fue diagnosticado con diabetes, no está solo, pues el acompañamiento de los especialistas le puede brindar las herramientas para mantener el padecimiento bajo control.</p>
          <p>“La importancia radica en la prevención de las complicaciones a largo plazo. Es bien sabido que una persona en metas de control adecuadas no sufrirá o serán más leves las complicaciones. De la mano de su médico internista puede lograrlo”, expresó.</p>

          <p style="color: #ff6b5b;" class="descanso-esp">Actuar para prevenir</p>
          <p>Mantenerse lo más cerca posible de su peso ideal, hacer ejercicio, disminuir el consumo de azúcares refinados fueron algunas medidas enlistadas por la especialista para llevar una vida más saludable y prevenir la diabetes.</p>
          <p>“Si su herencia genética es fuerte hacia esta enfermedad, lo más apropiado es acudir a chequeos regulares con el fin de prevenir o retrasar lo más posible la enfermedad”, recomendó.</p>
          <p>Es crucial entender que las complicaciones que tiene la diabetes mellitus en los pacientes, a gran escala, se traducen en enormes gastos institucionales en medicamentos, hospitalización y terapias de sustitución renal.</p>
          <p>“Reitero que si desde el primer nivel de atención se instauraran programas preventivos se disminuirían los casos de manera considerable”, concluyó la doctora Adria Almada Córdova. &nbsp;<i class="fas fa-square remate-esp"></i></p>
