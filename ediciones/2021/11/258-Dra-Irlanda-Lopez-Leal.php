          <p>Con una prevalencia global estimada de 420 millones de personas, la diabetes es un problema de salud
            mundial que
            preocupa debido a las consecuencias en los ámbitos individual y social.</p>
          <p>“La diabetes supone una enorme carga para las personas, las familias, los sistemas de salud y las
            sociedades
            debido al tratamiento requerido, las complicaciones agudas y crónicas, su demanda de servicios de salud, el
            impacto directo en la calidad de vida y la pérdida de años de vida”, expuso en charla con Revista BS la
            endocrinóloga <b>Irlanda Nataly Lopez Leal</b>.</p>
          <p>El aumento del sobrepeso y la obesidad, la disminución de actividad física, la ingesta de carbohidratos de
            baja
            calidad, el consumo de bebidas azucaradas, la comida rápida y el envejecimiento son algunos de los factores
            que
            han detonado que la diabetes sea un padecimiento que afecta a todas las regiones del mundo, edades y estatus
            socioeconómicos, señaló la especialista.</p>
          <p>La endocrinología estudia las glándulas endocrinas, hormonas y todas sus enfermedades. “Tengo entrenamiento
            especial para el cuidado, diagnóstico, prevención, seguimiento y manejo de complicaciones agudas y crónicas
            asociadas a la diabetes. Todo un enfoque centrado en el paciente y sus necesidades específicas”, explicó.
          </p>
          <p>“En mi familia hay personas con diabetes”, compartió, “por ello conozco de primera mano la necesidad de
            contar
            con personal de salud especializado, con un trato humano y comunicación empática, sin juzgar al paciente.
            Las
            personas con diabetes merecen ser escuchadas, tratadas de la manera más digna, humana y con la mayor calidad
            posible”.</p>

          <p class="descanso">Tomar acción ya</p>
          <p>Además de las ya bien conocidas complicaciones de la diabetes, la entrevistada señaló que esta enfermedad
            también
            se asocia cada vez más con otras como cánceres, enfermedades del hígado, demencia, discapacidad, fragilidad
            y una
            vejez no saludable.</p>
          <p>Combatir el sobrepeso y la obesidad es la principal medida de prevención. Cuando ya existe un diagnóstico,
            la
            valoración médica personalizada por el especialista puede mejorar el pronóstico de la enfermedad.</p>
          <p>México ocupa el noveno lugar mundial de casos de diabetes mellitus, y se enfila a tener el séptimo lugar en
            2025, lo que más allá del ranking se traduce en un grave problema de salud pública que debe ser combatido
            firmemente tanto en la escala individual como desde el ámbito institucional.</p>
          <p>“Se requiere que las instituciones de salud del país refuercen sus campañas preventivas, lleven a cabo
            programas de apoyo a pacientes con prediabetes, donde se eduque y se realicen detecciones tempranas para
            retrasar el desarrollo de la diabetes mellitus”, afirmó la doctora Irlanda Lopez Leal. &nbsp;<i
