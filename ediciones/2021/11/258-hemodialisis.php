        <p class="descanso" style="color: var(--rojo);">¿Dónde se recibe el tratamiento?</p>
        <p>La mayoría de las personas acuden a una unidad de hemodiálisis para recibir el tratamiento. En la unidad de
          hemodiálisis, el personal médico y enfermería configuran la máquina de diálisis y ayudan al paciente a
          conectarse. Un equipo de personal de la salud estará disponible para ayudarle. El paciente seguirá asistiendo
          a consulta con el médico.</p>

        <p class="descanso" style="color: var(--rojo);">¿Puede el dializador hacer todo lo que los riñones del paciente
          alguna vez hicieron?</p>
        <p>No. La hemodiálisis puede reemplazar parte de la función renal, pero no toda. La diálisis ayudará a mejorar
          el nivel de energía del paciente, así también los cambios que realice en su dieta pueden ayudarle a sentirse
          mejor.</p>
        <p>Limitar la cantidad de agua y otros líquidos que el paciente bebe y absorbe a través de los alimentos puede
          ayudar a evitar que se acumule demasiado líquido en el organismo entre tratamientos. Los medicamentos también
          ayudan a mantener la salud mientras el paciente está en diálisis.</p>

        <p class="descanso" style="color: var(--rojo);">¿Cómo es el horario para el tratamiento?</p>
        <p>El paciente tendrá un horario fijo para los tratamientos, generalmente tres veces por semana. Cada sesión de
          diálisis dura aproximadamente 3 a 4 horas.</p>

        <p class="descanso" style="color: var(--rojo);">¿Cómo se prepara el paciente?</p>
        <p>La diálisis es un tratamiento complejo que toma tiempo entender. Debido a que la mayoría de las personas no
          se sienten mal sino hasta poco antes de comenzar la diálisis, es probable que aún se sientan bien cuando el
          médico les hable por primera vez sobre la necesidad de prepararse para recibir diálisis. No se trata de
          comenzar la diálisis antes de que sea necesario, pero la preparación toma tiempo.</p>

        <p class="descanso" style="color: var(--rojo);">¿Qué cambios tendrá que hacer el paciente cuando comience la
          hemodiálisis?</p>
        <ul class="lista-disco-nota">
          <li>El paciente tendrá que cambiar lo que come y bebe. Limitar el sodio, alimentos ricos en fósforo y la cantidad de líquido que bebe, incluso el líquido que se encuentra en los alimentos</li>
          <li>Cuidar el acceso venoso, es la línea vital, por lo tanto debe protegerlo.</li>
          <li>El equipo médico puede ajustarle los medicamentos que toma.</li>
          <li>El líquido se acumula en el organismo entre los tratamientos de hemodiálisis.
            Podría necesitar agregar proteínas a la dieta porque la hemodiálisis elimina las proteínas, escoger
            alimentos con la cantidad adecuada de potasio, tomar vitaminas elaboradas para personas con insuficiencia
            renal y encontrar formas saludables de agregar calorías a la dieta porque es posible que se sienta con mucho
            apetito.</li>
        </ul>

        <p class="descanso" style="color: var(--rojo);">¿Cómo sabe el paciente si la hemodiálisis está funcionando?</p>
        <p>El paciente sabrá si los tratamientos de hemodiálisis están funcionando dependiendo de cómo se sienta. El
          nivel de energía puede aumentar y es posible que tenga más apetito. La hemodiálisis reduce la acumulación de
          sal y líquidos, por lo tanto debería tener menos dificultad respiratoria y menos hinchazón, así como control
          de su presión arterial.</p>
        <p>Para aprovechar al máximo el tratamiento de hemodiálisis, el paciente debe mantener su “peso seco” ideal. El
          peso seco ideal es el peso cuando no se tiene exceso de líquido en el organismo. <i class="fas fa-square"></i>
        </p>
