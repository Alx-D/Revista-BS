      <div class="texto">
        <p>La hemodiálisis es un tratamiento para filtrar las toxinas y el agua de la sangre como lo hacían los riñones
          cuando estaban sanos. Ayuda a controlar la presión arterial y a equilibrar los minerales importantes en la
          sangre como el potasio, el sodio y el calcio.</p>

        <p class="descanso" style="color: var(--rojo);">¿Qué sucede durante la hemodiálisis?</p>
        <p>Durante la hemodiálisis, se bombea la sangre a través de un filtro, conocido como dializador, fuera del
          organismo. El dializador también se conoce como “riñón artificial”.</p>
        <p>Al comienzo de un tratamiento de hemodiálisis, una enfermera colocará dos agujas en el brazo del paciente o
          conectará a un catéter venoso (previamente colocado por un médico nefrólogo en una vena grande) unas mangueras
          que van a la máquina.</p>
        <p>El dializador bombea la sangre a través del filtro y la devuelve al organismo. Durante el proceso, la máquina
          de diálisis verifica la presión arterial y controla qué tan rápido fluye la sangre a través del filtro así
          como la velocidad con que se extrae el líquido del organismo.</p>

        <p class="descanso" style="color: var(--rojo);">¿Qué pasa con la sangre del paciente mientras está en el filtro?
        </p>
        <p>La sangre pasa por un extremo del filtro y entra a muchas fibras huecas muy delgadas. A medida que la sangre
          pasa a través de las fibras huecas, la solución de diálisis pasa en dirección opuesta en el exterior de las
          fibras. Las toxinas de la sangre pasan a la solución de diálisis. La sangre filtrada permanece en las fibras
          huecas y regresa al cuerpo.</p>
        <p>El nefrólogo recetará al paciente una solución de diálisis para satisfacer sus necesidades. La solución de
          diálisis contiene agua y sustancias químicas que se agregan para eliminar de manera segura las toxinas, el
          exceso de sal y el líquido de la sangre.</p>
        <p>El médico puede ajustar el equilibrio de los productos químicos en la solución si los análisis de sangre
          muestran que la sangre tiene demasiada o muy poca cantidad de ciertos minerales como potasio o calcio, o si el
          paciente tiene problemas como presión arterial baja o calambres musculares durante la diálisis.</p>

        <div class="foto-cnt-pie">
