          <p>La alta frecuencia de la diabetes, así como la severidad y diversidad de sus complicaciones determinan que
            esta enfermedad sea uno de los principales problemas de salud púbica en México, explicó la doctora <b>Rosa María Acuña Ortiz</b> a Revista BS.</p>
          <p>“La diabetes impacta en toda la micro y macro circulación, lo cual hace que su descontrol sea la principal causa de infarto, enfermedad cerebral vascular, muerte prematura, ceguera y amputación no traumática de alguna extremidad”, señaló la especialista en medicina interna.</p>
          <p>Para hacer frente a este padecimiento y evitar el desarrollo de sus complicaciones, su adecuado seguimiento por parte de los profesionales de la salud es crucial.</p>
          <p>“Es importante el seguimiento mensual cuando se está iniciando el tratamiento y, posteriormente, consulta trimestral para valorar que las dosis sean las adecuadas”, detalló, “además de la vigilancia de la función orgánica en ojos, riñón, corazón e hígado para la detección oportuna de las alteraciones en los mismos”.</p>
          <p>Otro punto clave en la consulta periódica del paciente con diabetes, comentó la especialista, es la revisión de las extremidades inferiores para la detección y tratamiento del pie diabético.</p>

          <p style="color: #ff6b5b;" class="descanso-esp">Hacer frente</p>
          <p>En el ámbito individual y familiar, practicar hábitos saludables es la forma de contribuir a prevenir la diabetes y así frenar sus estragos en la salud pública.</p>
          <p>“Adoptando un estilo de vida saludable y sobre todo mantenerlo a largo plazo. Con esto me refiero a un hábito de alimentación balanceado, aumento de la actividad física, evitar aumento de peso, además de evitar o abandonar adicciones, principalmente el tabaquismo”, señaló.</p>
          <p>Desde una perspectiva social más amplia, la doctora Acuña Ortiz reconoce que es necesario reforzar las campañas de tamizaje y detección de complicaciones, fomentar el automonitoreo y el apego al tratamiento farmacológico, nutricional y de actividad física.</p>
          <p>“Es importante saber que en las instituciones se cuenta con el recurso humano, es decir con la atención del personal de salud necesario, incluso con la atención multidisciplinaria de todos ellos, pero en general se requiere del recurso económico, que a veces no es suficiente para lograr abastecer al 100% de la población que se atiende”, finalizó la entrevistada. &nbsp;<i class="fas fa-square remate-esp"></i></p>
