        <p>Ante un auditorio compuesto por estudiantes, médicos y público en general, el congreso organizado de forma tripartita por Agrupación George Papanicolaou A. C., Oncología San José y Tecnológico de Monterrey Campus Sonora Norte fue un foro activo y enriquecedor en el que reconocidos especialistas hablaron sobre distintas aristas de la detección y tratamiento, bajo el tema “Desafíos actuales en cáncer”.</p>
