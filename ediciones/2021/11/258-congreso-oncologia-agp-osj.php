        <p>En entrevista con Revista BS el doctor Ávila Monteverde comentó: “El compromiso es tratar de bajar la incidencia en mortalidad por cáncer. Esto implica básicamente la educación. Tratar de hacer llegar a la mayor cantidad de personas las características y condiciones para una detección oportuna de las enfermedades tumorales”.</p>
        <p>Así también, Andrea Contreras, directora de AGP, subrayó la importancia del congreso para llevar información a la población en general, ya que se trató de un evento gratuito dirigido a todo público, no únicamente a los especialistas en salud.</p>
