        <p>“Recordarles que prevenir y detectar oportunamente el cáncer es la diferencia entre la vida y la muerte”, enfatizó. “Mientras haya vida, hay esperanza”.</p>
        <p>Presidente del consejo médico de AGP y director médico de Oncología San José, el doctor Enrique Ávila Monteverde comentó que los temas a exponer se enmarcaron en dos días de conmemoración internacionales: Día Mundial de Cáncer de Pulmón (17 de noviembre) y Día Mundial del Cáncer de Próstata (29 de noviembre).</p>
        <p>El también cirujano oncólogo dijo que los tres tipos de cáncer que se trataron en el evento ocupan los primeros lugares de incidencia y de mortalidad dentro de las enfermedades oncológicas, tanto en el mundo como en México.</p>
