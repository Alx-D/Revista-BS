      <div class="texto">
        <p>El trastorno por déficit de atención e hiperactividad (TDAH) es un trastorno neuroconductual de origen neurobiológico, que se inicia en la infancia y afecta a entre 3% y 7% de los niños en edad escolar. Se caracteriza por un patrón persistente de inatención, hiperactividad e impulsividades que afectan significativamente el funcionamiento del niño en la escuela, en la familia y en lo social.</p>
        <p>Como este conjunto de síntomas puede aparecer en una amplia variedad de trastornos, se recomienda que sea un médico experto quien realice el diagnóstico, apoyándose en pruebas complementarias destinadas a conocer el funcionamiento cognitivo del niño.</p>
        <p>Desde la neuropsicología, los déficits cognitivos presentes en el TDAH se producen a consecuencia de un déficit primario en el desarrollo de las funciones ejecutivas. Las más afectadas son el control inhibitorio (detener una acción una vez que se ha iniciado), la flexibilidad cognitiva (perseverancia excesiva y fracaso ante tareas nuevas), la memoria de trabajo (retener información que debe usar: habilidad académica) y la velocidad de procesamiento (dificultad para ejecutar tareas de manera simultánea).</p>
