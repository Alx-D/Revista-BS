        <p>A consecuencia de estas dificultades, es común que el afectado obtenga bajas puntuaciones en pruebas psicométricas (medida de coeficiente intelectual).</p>
        <p>Sin embargo, en ningún caso se debe considerar al TDAH como una discapacidad. Estas fallas a nivel de funciones ejecutivas se relacionan con una alteración neurobiológica de los sistemas dopaminérgicos y noradrenérgicos en los circuitos frontoestriatales. Por tanto, con el debido tratamiento, el afectado por TDAH puede llevar una vida completamente normal.</p>

        <p class="descanso">Impacto a largo plazo</p>
        <p>Al ser un trastorno del neurodesarrollo, es común que comparta síntomas con otras entidades. La presencia de uno o más cuadros asociados se denomina comorbilidad, siendo los trastornos de aprendizaje (33%), el trastorno del desarrollo de la coordinación (50%) y los trastornos del lenguaje (30%) los que frecuentemente acompañan al TDAH en la etapa infantil. En adolescencia, el TDAH suele estar acompañado de trastorno negativista-desafiante (40%) y disocial (14%).</p>
        <p>El TDAH puede generar amplias complicaciones en el desarrollo del niño y del adolescente, especialmente en el ámbito escolar. Las fallas ejecutivas se traducen en problemas para establecer estrategias de lectura (codificación y errores de anticipación), retener información y recordarla ante un examen, mala planificación y organización del tiempo. Por ello, son comúnmente reportados, derivados a consulta con el psicólogo o a asesorías extras, con el consiguiente perjuicio familiar (especialmente el estrés experimentado por las madres).</p>
        <p>Los adolescentes y los adultos con una historia de TDAH no tratado presentan más riesgos de una conducta antisocial como son el abuso de alcohol, drogas, comportamientos criminales, dificultades académicas y dificultades de empleo y obesidad.</p>

