        <p class="descanso">Tratamiento necesario</p>
        <p>Al ser el TDAH un padecimiento de origen neurobiológico, el tratamiento debe ser prioritariamente médico, bajo una perspectiva multimodal. El tratamiento de los síntomas nucleares y comorbilidades debe incluir tratamiento farmacológico y terapia cognitivo-conductual.
          Dependiendo de la comorbilidad descrita, se deben incluir tratamientos psicopedagógicos, del lenguaje, de la motricidad y de las emociones, según proceda.
        </p>
        <p>En la actualidad, se sabe que el tratamiento farmacológico es sumamente seguro y efectivo, siendo el metilfenidato el de primera elección. La evidencia recabada permite establecer que las dificultades relacionadas con la atención sostenida, la memoria de trabajo y la velocidad de procesamiento responden favorablemente al metilfenidato de acción corta (duración media de 3 a 6 horas).</p>
        <p>Los problemas relacionados con el control inhibitorio, la flexibilidad cognitiva y la planificación responden mejor al tratamiento con metilfenidato de liberación prolongada (duración media 10 a 12 horas).</p>
        <p>A modo de conclusión, debemos comprender que el diagnóstico temprano y oportuno por parte de un profesional médico especializado es nuestra mejor herramienta para proveer al afectado por TDAH de mayores expectativas y oportunidades para su futuro. <i class="fas fa-square"></i></p>
