        <p class="descanso">Descanso adecuado</p>
        <p>El hábito del sueño es fundamental en los niños ya que les permitirá una recuperación física y psicológica
          para comenzar un nuevo día lleno de energía. El descanso adecuado contribuye a un buen crecimiento y
          desarrollo, además, mejora el rendimiento académico, memoria y estado de ánimo.</p>
        <p>Para muchas familias el volver a la rutina escolar representa un reajuste también en sus horas de sueño. La
          recomendación de sueño en los preescolares es de 10 a 13 horas al día y los escolares de 9 a 12 horas.</p>
        <p>Se puede comenzar por establecer un horario para dormir y despertar (siempre las mismas horas), realizar
          actividad física durante el día y retirar distractores del cuarto cuando se llegue la hora de dormir.</p>
        <p>Puede suceder que los niños sientan más cansancio o sueño al llegar de la escuela, debido a que se están
          volviendo a acostumbrar a mayores actividades durante el día. Esto es buena oportunidad para el descanso.</p>

        <p class="descanso">Salud mental</p>
        <p>Luego de vivir un año y medio en casa sin ver a sus compañeros y maestros, tal vez para los niños represente
          un cóctel de emociones. Es importante escuchar cómo se sienten, hablar con ellos y brindarles afirmaciones
          positivas y seguridad.</p>
        <p>Para muchas familias y niños la pandemia representa un gran miedo y es normal. Sin embargo, tratemos de
          infundir confianza y, claro, educarlos desde casa para que sigan todas las recomendaciones de higiene.</p>
        <p>Creo que el volver a la escuela no sólo representará una recuperación académica sino también de salud mental
          para los niños.</p>
        <p>No sabemos lo que pueda suceder mañana, pero hoy podemos comenzar a realizar cambios de estilo de vida que
          mejorarán la salud de los niños y la familia para hacerle frente de la mejor manera a los retos de la vida. <i class="fas fa-square"></i></p>
