        <p>Si bien, el regreso a clases presenciales (o en cualquier modalidad) representa un reto en todos los ámbitos,
          también es una buena oportunidad para fomentar buenos hábitos en los niños, iniciando desde el hogar. Se puede
          comenzar a hacer cambios de manera paulatina. A continuación comento algunos puntos importantes a considerar.
        </p>

        <p class="descanso">Alimentación saludable</p>
        <p>Tal vez se esté preguntado “¿tenemos que ponernos a dieta?, ¿debo mandar de lonche sólo tiritas de pepino y
          zanahoria?”. Pero no, realmente llevar una alimentación saludable no tiene que verse como algo restrictivo o
          aburrido. Se trata de apegarnos a ciertas recomendaciones de alimentación que al final del día traerán grandes
          beneficios a nuestra salud.</p>
        <p>Se puede comenzar con tener más consciencia de nuestro plato. Es decir, cuidar las porciones y que no falte
          la fuente de proteína animal o vegetal (pescado, pollo, carne, leche, yogurt, lentejas, frijoles, soya),
          carbohidratos complejos (arroz, avena, pan integral, pasta), verduras de todos los colores y frutas. Además,
          las grasas saludables: aguacate, aceite de oliva y frutos secos y el agua simple. Recordemos, siempre la mejor
          opción serán los alimentos naturales en vez de los ultraprocesados.</p>
        <p>Otra cuestión importante, y que suele causar un poco de estrés a los padres, es el tema de los lonches.
          Muchas veces, por falta de tiempo es común brindar a los niños alimentos empaquetados (galletas, frituras,
          jugos, caramelos) de fácil acceso en las tiendas. Sin embargo, éstos en su mayoría son alimentos altos en
          azúcares simples, grasas, sodio y exceso de calorías.</p>
        <p>Realmente, vale la pena ponerse creativos con el almuerzo de los niños. Una lonchera saludable está compuesta
          de frutas, verduras, carbohidratos complejos, proteína y agua natural.</p>
        <p>Además, debemos asegurar que los pequeños tomen su desayuno saludable antes de salir de casa, en ningún
          momento el lonche remplazará el desayuno. Estos dos tiempos de comida le permitirán al niño tener un mejor
          desempeño escolar. La clave para adoptar una alimentación saludable es la organización.</p>

        <p class="descanso">Actividad física</p>
        <p>El hecho de que los niños asistan a la escuela representa una oportunidad para incrementar su actividad
          física a través del recreo, clases de educación física y deportes. Podemos encontrar programas académicos que
          de primera instancia no impartan estas clases, pero es importante fomentar este hábito en el hogar también.
        </p>
        <p>Actualmente, ya se tiene un poco más de libertad para salir a espacios al aire libre, por lo que se pueden
          realizar actividades recreativas en familia como pasear al perro, andar en bicicleta y jugar en el parque, o
          tener juego o rutinas en casa.</p>
        <p>Se recomienda que los niños realicen 60 minutos al día de actividad física moderada a intensa. Con esto
          podemos contribuir a un mejor crecimiento, desarrollo, estado físico y emocional de los niños.</p>

