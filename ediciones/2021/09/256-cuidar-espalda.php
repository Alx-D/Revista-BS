          <ol class="rc-color-lista">
            <li style="list-style-type: decimal;">Tirantes gruesos y acolchados</li>
            <li style="list-style-type: decimal;">Diversos compartimentos para distribuir el peso</li>
            <li style="list-style-type: decimal;">Cinturón abdominal para ajustarla al cuerpo</li>
            <li style="list-style-type: decimal; font-weight: 500;">La carga no debe superar el 15% del peso del niño</li>
          </ol>
        </div>
        <p>De los dolores, la mayoría pasarán en el trascurso de horas o días, pero en algunas ocasiones requerirán
          manejo médico, por lo cual es importante prevenir los factores modificables para que se presenten con menor
          frecuencia.</p>
        <p> En fechas recientes se ha aumentado el uso de mochilas con llantas y jaladeras, las cuales evitan que el
          estudiante cargue peso en el cuerpo. No obstante, si la distancia que camina jalando la mochila es muy larga,
          el cambio en el patrón de marcha facilita la aparición de contracturas musculares y dolor en la espalda baja,
          así como la aparición de dolores de hombro y muñeca.</p>
        <p>Por ello es recomendable usar una mochila de tirantes que incluya un cinturón para atarla a la cintura, esto
          mejorara la distribución de carga, haciendo más ergonómica la carga de la misma.</p>
        <p class="descanso">Postura y lugar de trabajo</p>
        <p>La estación de trabajo es otro punto importante, ya que en estos tiempos de pandemia la educación se ha
          vuelto híbrida con muchas horas en el escritorio de la casa. Así pues, malas posturas ocasionan contracturas
          musculares y desarrollo de dolor, así como aumento en la incidencia de otros males como el túnel de carpo y
          una variedad de tendinitis, por lo cual es importante tener en cuenta las siguientes recomendaciones:</p>
        <ol class="lista-num-nota">
          <li>El borde superior del monitor debe de estar a nivel de los ojos, incluso a 5
            centímetros por debajo de éste.</li>
          <li>La distancia del teclado debe de ser de 10 centímetros del borde de la mesa para
            poder reposar los antebrazos.</li>
          <li>Los antebrazos deben estar a 90 grados.</li>
          <li>Las rodillas deben estar a flexionadas a 90 grados o más y los pies pegados al
            piso.</li>
          <li>Priorizar el uso de silla ergonómica.</li>
        </ol>
        <p>Además de estas medidas, la relación de una rutina de ejercicio diaria disminuye el estrés así como la
          posibilidad de dolor por cuestiones osteomusculares. <i class="fas fa-square"></i></p>
