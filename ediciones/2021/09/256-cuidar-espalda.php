      <div class="texto">
        <p> El dolor de espalda en el estudiante es una causa frecuente de consulta osteomuscular, se estima que 20 a
          30% de los estudiantes tendrán dolor de espada o cuello en el trascurso del año escolar.</p>
        <p>Los factores relacionados con esto son múltiples, desde cuestiones sociodemográficas como traslados largos a
          la escuela, estaciones de trabajo inadecuadas, mala posturas y el estrés.</p>

        <div class="rc-color naranjao rc-der fit680">
          <img src="/ediciones/2021/09/imgs/256-dr-guillermo-vargas-dolor-osteomuscular-2-nt.png" style="margin: 0 auto; display: block; margin-bottom: 5px;">
