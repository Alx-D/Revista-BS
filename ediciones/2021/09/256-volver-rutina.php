      <div class="texto">
        <p>Desde preescolares hasta universitarios, y sin muchas opciones ante la necesidad de continuar con la
          educación, nos vimos obligados a adoptar una nueva forma de enseñar y por tanto una nueva forma de aprender.
        </p>
        <p>En este nuevo proceso de enseñanza-aprendizaje virtual con el cierre de escuelas ha afectado
          desproporcionadamente a niños y niñas, ya que no todos tuvieron los mismos recursos y apoyos para darle
          seguimiento efectivo a las clases virtuales.</p>
        <p>La pandemia ha impactado a la educación en diferentes áreas del desarrollo de niños y adolescentes, los
          comentaremos a continuación.</p>

        <p class="descanso">Aprendizaje</p>
        <p>En el aspecto cognitivo, con el cierre de escuelas, se ha registrado una afectación en la estimulación
          multisensorial, en algunos casos se ha observado una afectación en la estimulación cognitiva y retrocesos en
          los procesos de lectoescritura, operaciones básicas y solución de problemas, sobre todo en menores con
          necesidades educativas especiales.</p>
        <div class="rc-color azulc rc-der">
          <div class="rc-foto-bx">
