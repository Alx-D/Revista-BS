        <p>El uso de dispositivos electrónicos se ha disparado de una manera exorbitante, generando una gran dependencia
          a ellos, y afectando directamente en el tiempo y la calidad del sueño, en la concentración, memorización, las
          intenciones de comunicación social y poca tolerancia a la frustración.</p>
        <p>La falta de sueño reparador, repercute en el crecimiento de los niños, así como en la consolidación del
          aprendizaje cognitivo, ya que la infancia es un período crítico para el desarrollo cognitivo y psicomotor.</p>

        <p class="descanso">Recomendaciones</p>
        <p>Con el inminente regreso a clases presenciales, será importante mantener una comunicación abierta y continua
          con el centro escolar. Es importante estar abiertos a la retroalimentación sobre la parte académica,
          emocional, social y la adaptación de nuestros hijos sobre todo las primeras semanas.</p>
        <p>Es probable que veamos a nuestros hijos cansados, irritables o con cambios de humor, a pesar de ir contentos
          a la escuela, pero debemos de recordar que es parte del proceso de adaptación a nuevos horarios, a nuevas
          rutinas y a nuevo retos.</p>
        <p>Hay que evitar compararse con otros líderes de familia y, sobre todo, comparar a sus hijos. Cada familia
          tiene sus necesidades y posibilidades diferentes, y cada niño tiene su propio proceso de aprendizaje, sus
          propias habilidades, talentos y áreas de oportunidad. Eviten juzgarse por cómo manejaron las cosas en el
          pasado, recuerden que todos estuvimos en una situación sin precedentes.</p>
        <p>Lo más importante es volver a las rutinas, tener un horario y limitar los excesos que surgieron en la
          pandemia, y sobre todo buscar ayuda cuando una situación sobrepasa nuestro entendimiento o nuestra capacidad
          para abordarlo.</p>

        <p class="descanso">Busque asesoría</p>
        <p>Para líderes de familia de niñas y niños en preescolar y primaria independientemente si deciden que sus hijos
          tomen clases desde casa o en la escuela, será importante considerar buscar apoyo de profesionales para poder
          evaluar su lenguaje, habla, motricidad, su avance académico y estimular aquellas áreas en las que
          identifiquemos desfases.</p>
        <p>Como psicólogos podemos también darles apoyo socioemocional a sus hijos y orientarlos a ustedes sobre el
          mejor manejo conductual en casa dependiendo el estilo de crianza y dinámica de cada familia. <i class="fas fa-square"></i></p>
