        <p>Niñas y niños con trastorno de espectro autista (TEA) y trastorno por déficit de atención e hiperactividad
          (TDAH) muestran una importante alteración en sus rutinas, privación social y se ha entorpecido el trabajo para
          la estimulación de las funciones ejecutivas.</p>
        <p>Se registran notables retrocesos académicos, a la vez que los niños tienen pérdida por la motivación y el
          aprendizaje vía online. La falta de actividad física y falta de estimulación en la motricidad gruesa y fina se
          ve reflejado en los productos gráficos de los niños.</p>

        <p class="descanso">Salud física</p>
        <p>Se ha observado una pérdida de hábitos saludables y un decremento significativo en la actividad física de
          niños y jóvenes. El decremento en el juego con pares ha ocasionado que haya un mayor sedentarismo aunado al
          uso excesivo de electrónicos, empobrecimiento dietético y facilidad de adquirir alimentos procesados.</p>
        <p>Se registran cambios importantes en rutinas y hábitos de vida saludable. Por lo cual, es recomendable
          implementar una nueva rutina haciendo uso de un horario visual en casa, temporizadores (sobre todo en casos
          con TEA o TDAH), respetar horas de comida y sueño, ya que hay una importante relación entre la actividad
          física y actividad cognitiva sobre todo en la etapa infantil.</p>

        <p class="descanso">Salud mental</p>
        <p>Hay un incremento de síntomas depresivos y de ansiedad debido a la reducción de interacción social, además,
          se han detectado ciertos factores estresantes en la infancia y adolescencia como el confinamiento prolongado,
          miedo al contagio, duelo, pérdidas familiares, pérdidas de poder adquisitivo, sentimientos de soledad, falta
          de juego y falta de actividad física.</p>
        <p>El confinamiento ha afectado la relación entre iguales, ya que la escuela juega un papel importante en la
          socialización de los niños, la comunicación social, el lenguaje, la empatía, la tolerancia, la solución de
          conflictos. Es en las escuelas donde se favorece el desarrollo de las habilidades interpersonales sobre todo
          en niños con TEA o TDAH. El juego es el medio por el cual los niños aprenden en la infancia y por tanto básico
          para el desarrollo integral de los niños para interiorizar roles, normas, valores, así como desarrollar el
          lenguaje y comunicación en edades preescolares.</p>
