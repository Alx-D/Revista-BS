          <p class="f2 rc-color-cbz-1r" style="color: white;">Las claves
          </p>
          <ul class="rc-color-lista">
            <li>Volver a las rutinas lo antes posible</li>
            <li>Mantener una comunicación con la escuela</li>
            <li>Evitar compararse con otros líderes de familia</li>
            <li>Buscar ayuda cuando una situación sobrepasa</li>
