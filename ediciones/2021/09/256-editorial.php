      <div class="texto">
        <p>La emergencia sanitaria por Covid-19 ha impactado de manera negativa los sistemas de salud, la economía
          nacional y amenaza con provocar una crisis educativa ante el gran número de alumnos que han abandonado sus
          estudios, advirtió la diputada Adriana Teissier Zavala, de la comisión permanente del Senado de la República.
        </p>
        <p>Abundó que la encuesta para la Medición del Impacto de la Covid-19 en la Educación 2020, del Instituto
          Nacional de Estadística y Geografía (INEGI) reveló que 33.6 millones de personas entre los 3 y 29 años
          estuvieron inscritas en el ciclo escolar 2019-2020, de ellas, 740 mil no lo concluyeron.</p>
        <p>El ciclo 2020-2021 en nuestro país fue frente a televisores, computadoras, teléfonos y radios en casa,
          obligados por la pandemia.
          Estatista estima que entre abril y agosto de 2020 cerca de tres millones de estudiantes en México abandonaron
          la escuela como resultado de la pandemia. En su encuesta realizada a finales de 2020, y publicada en julio de
          2021, un 26.6% de los estudiantes que decidieron no inscribirse al ciclo escolar 2020-2021 consideran que las
          clases a distancia son poco funcionales para el aprendizaje.</p>
        <p>La segunda razón más mencionada para justificar por qué no continuaron sus estudios es que los padres o
          tutores se quedaron sin trabajo (25.3%). Otras razones son: carece de computadora u otro dispositivo o
          conexión de internet (21.9%), la escuela cerró definitivamente (19.3 %), el padre o tutor no puede estar al
          pendiente (4.4%), y un 2.6% porque un familiar enfermó o falleció por Covid-19.</p>
        <p>Afortunadamente, para este año se planteó por la Secretaría de Educación Pública (SEP) un calendario del
          ciclo escolar 2021-2022 para alumnos de preescolar, primaria y secundaria que contempla 200 días de clases;
          que inició el 30 de agosto y finalizará el 28 de julio de 2022.</p>
        <p>De acuerdo a México Evalúa, la titular de la SEP, Delfina Gómez Álvarez, destacó que el calendario deberá
          ayudar a retomar con mayor vigor “la enseñanza presencial que tanta falta le hace a nuestras queridas alumnas
          y alumnos”.</p>
        <p>Propuso tres áreas para orientar las actividades de las comunidades escolares en los próximos meses:</p>
        <ol class="lista-letra-nota">
          <li>Apoyo emocional para que niñas, niños y adolescentes recuperen la
            confianza ante los efectos de la pandemia.</li>
          <li>Análisis de los resultados de aprendizaje del ciclo escolar que
            termina, para ofrecer medidas y acciones que regularicen el nivel en los aprendizajes esperados.</li>
          <li>Análisis de los avances del programa escolar de mejora continua,
            para reconocer el logro de los aprendizajes de estudiantes y la mejora continua del servicio que brinda la
            escuela.</li>
        </ol>

        <p class="f1 descanso">Más salones, menos pantallas</p>
        <p>El regreso a clases es el gran reinicio de la sociedad, según Juan Antonio Lozano Diéz, Director de IPADE, ya
          que hace falta estimular la inteligencia emocional, el análisis y reflexión, para el desarrollo cognitivo y la
          convivencia social.</p>
        <p>Hace alusión al experto en neurociencia Michel Desmurget, quien alerta y denuncia en su libro “La fábrica de
          cretinos digitales” los peligros reales de las pantallas y las graves consecuencias que conlleva seguir
          promoviendo de forma acrítica el uso de estas tecnologías.</p>
        <p>Señala en una síntesis que el uso de tecnología (smartphones, ordenadores, tabletas) por parte de las nuevas
          generaciones es absolutamente desproporcionado. Con sólo dos años de edad, el consumo medio se sitúa en torno
          a las tres horas. De los ocho a los doce, la media se acerca a las cinco horas. En la adolescencia, la cifra
          se dispara casi a siete horas, lo que supone más de dos mil cuatrocientas horas al año en pleno desarrollo
          intelectual.</p>
        <p>En contra de lo que la prensa y la industria han difundido hasta ahora, el uso de la tecnología, lejos de
          ayudar al desarrollo de los niños y estudiantes, produce graves complicaciones de toda índole: sobre el cuerpo
          (obesidad, problemas cardiovasculares, reducción de la esperanza de vida), sobre las emociones (agresividad,
          depresión, comportamientos de riesgo) y sobre el desarrollo intelectual (empobrecimiento del lenguaje,
          concentración, memoria).</p>
        <div class="texto-resaltado-bx">
          <p>Por lo anterior se recomienda:</p>
          <ul class="lista-disco-nota">
            <li>Disminuir el número de horas de exposición a las pantallas.</li>
            <li>Convivencia familiar para limitar el aislamiento de los niños, teniendo un
              genuino interés por ellos y su desarrollo.</li>
            <li>Entretenimientos diversos: lectura, ver películas donde haya más diálogo
              que violencia, etc.</li>
            <li>Cuidado del sueño; se ha visto que los niños y jóvenes duermen menos por la
              exposición a la pantalla y por cambios de horario debido al trabajo en casa.</li>
            <li>Encontrar y enseñar valores, promover sobre todo las cosas no materiales
              que nos pueden hacer felices, como ayudar a los demás, cuidar la naturaleza, etcétera.</li>
            <li class="lista-disco-nota">Fomentar la inteligencia emocional y expresar los sentimientos.</li>
          </ul>
        </div>
