      <div class="texto">
        <p>La podología, como parte de las ciencias médicas, estudia la biomecánica y la marcha del ser humano desde los primeros pasos de la infancia y el desarrollo hasta la edad adulta. </p>
        <p>Hoy vamos a hablar sobre cómo caminan nuestros hijos, los problemas más comunes que como padres notamos en ellos, tanto al caminar como al estar de pie.</p>
        <p>Conforme el niño va creciendo, en cada etapa de su vida vamos a notar características estructurales y en la postura, propias de su edad, las cuales no deben de preocuparnos, siempre y cuando no les causen dolor o alguna molestia que les impida una correcta deambulación.</p>
        <p><span style="color: var(--rojo);">0 a 1 año.</span> Comenzamos a ver la sensibilidad de los niños por medio de cosquillas, la forma es de un piecito mofletudo, esto quiere decir que tiene exceso de grasa en la parte de la planta, por lo cual vamos a encontrar un pie completamente redondo y regordete. En esta etapa de la vida no recomendamos que utilicen ningún tipo de zapato, únicamente cuando es temporada invernal, utilizar prendas para cubrirlo del frío.</p>

        <p><span style="color: var(--rojo);">1 a 2 años.</span> Notamos que el niño comienza a tener una posición de las piernas arqueadas, coloquialmente llamado zambo. Esto se debe a que la musculatura aún no está correctamente fortalecida, si a esto le sumamos el uso del pañal, se va a marcar una posición en paréntesis de sus piernas.</p>

        <p><span style="color: var(--rojo);">2 a 5 años.</span> Comienzan a tener una postura totalmente contraria a las piernas arqueadas. En el desarrollo y crecimiento las piernas empiezan a juntarse a la altura de las rodillas, se denomina genu valgo, la mayoría de los niños comienza a deambular de puntillas.</p>

