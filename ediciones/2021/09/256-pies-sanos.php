        <p><span style="color: var(--rojo);">5 años en adelante.</span> Ya que el menor tiene una pisada firme y estable, es cuando se puede determinar si el niño presenta algún tipo de problema como: pies con mucho arco (pie cavo), pies con pérdida total de arco (pie plano), niños que caminan con las puntillas (pie equino), niños que caminan con las puntas hacia afuera (pie aducto), menores que presentan la pisada con la punta del pie hacia adentro (pie abducto), lo que muchas veces va ocasionar dolores, una postura anormal y caídas al correr.</p>
        <p>Los especialistas en el cuidado del pie realizamos un estudio de biomecánica, el cual se recomienda entre los tres y los tres años y medio de edad, es decir, cuando ya dejó el pañal y su pisada es más firme.</p>
        <p>Este estudio es para ver la postura, cómo camina, cómo se para, cómo desgasta su calzado, para así llegar a un diagnóstico certero, y, en su caso, poder brindar un tratamiento adecuado, el cual puede ser por medio de plantillas ortopédicas, algún tipo de ortesis u ortótico o zapato especial para el problema estructural que presente el niño.</p>

        <p class="descanso">Recomendaciones</p>
        <p>En el desarrollo del menor vamos a encontrar problemas en la postura y problemas en la marcha, niños que presentan supinación, esto quiere decir que pisan con el arco externo del pie y sus rodillas están separadas, vamos a encontrar menores con pisadas en valgo, la característica del menor es que sus rodillas se van a unir cuando ellos estén caminando (piernas en forma de x), también encontramos niños con recurvatum, la característica de estos pacientes es que tienen una hiperlaxitud (exceso de flexibilidad en las articulaciones) tanto en las piernas como en articulaciones superiores brazos, muñeca y dedos de las manos.</p>
