      <div class="texto">
        <p>En recientes meses, la “tercera ola” ha tomado gran importancia en el tema de la pediatría ya que se ha visto
          un incremento acelerado en el contagio de los niños, esto conlleva no sólo al aumento de casos, sino también
          al aumento de complicaciones, de secuelas y muertes.</p>
        <p>Los principales factores de riesgo que se han destacado en esta tercera ola son la falta de vacunación a
          menores, así como la variante delta. Esto sumado a que las medidas de sanidad han empobrecido y la menor
          preocupación por contagiarse de las personas ya vacunadas, a pesar de que éstas son capaces de transmitir el
          virus.</p>

        <div class="rc-color morado rc-der">
          <p class="f2 rc-color-cbz-1r" style="color: white;">Cuidados en casa
          </p>
          <ul class="rc-color-lista">
            <li>Fomentar la responsabilidad de cuidarse</li>
            <li>Enseñar la importancia de acatar las normas</li>
            <li>Evitar acudir a lugares conglomerados</li>
            <li>Vigilar la presencia de síntomas</li>
            <li>Quedarse en casa y reportar síntomas</li>
          </ul>
        </div>

        <p>En otros países ya se comenzó con la vacunación en menores desde los 12 años. En México no hemos logrado
          cubrir a toda la población adulta con el esquema completo. Los mayores de 18 años llevan meses vacunándose,
          los menores de edad mientras no inicien su vacunación seguirán con predisposición a los contagios. Y aunque se
          sabe que la mayoría de los niños son asintomáticos o presentan síntomas leves, ha aumentado la hospitalización
          y muertes en este grupo de edad.</p>
        <p>Hablando sobre la variante delta, no es que los niños sean más propensos a adquirir la enfermedad por tal
          variante, en general se ha visto un aumento en todos los grupos de edad por ésta.</p>
        <p>La diferencia es que esta variante es más contagiosa que otras cepas, esto sumado a que los niños no están
          vacunados y que los padres han disminuido las medidas de protección debido a que con más frecuencia acuden a
          lugares donde hay acumulación de personas y no se cuidan las medidas de seguridad, los hace más susceptibles a
          contraer la enfermedad.</p>

        <p class="descanso">Inicio de ciclo escolar</p>
        <p>Con el regreso a clases, es lógico pensar que la propagación del virus, los contagios y todo lo que conlleva
          van a aumentar. En este momento ya no podemos frenar el regreso a clases, pero las escuelas deben de cumplir
          con las normas sanitarias, recomendaciones dadas por organismos internacionales y nacionales y que se esté en
          constante vigilancia el cumplimiento de éstas para reducir lo más posible los contagios y poder crear un
          hábito en los niños de seguir las normas de sanidad.</p>
        <p>Entre las recomendaciones que se han dado para este regreso a clases son tres las estrategias principales que
          se han
          sugerido como pautas a seguir en lugares donde se reúnen muchas personas, en este caso en un aula de la
          escuela.
          La primera es el correcto uso del cubrebocas, siendo éste de uso permanente en el interior de las aulas. En
          exteriores
          se recomienda usar mascarilla si hay acumulación de personas.
        </p>

        <div class="rc-gris rc-izq fit680">
          <p class="f2 rc-gris-cbz-1r">Medidas en las escuelas</p>
          <ul class="rc-gris-lista">
            <li>Correcto uso del cubrebocas en todo momento</li>
            <li>Sana distancia por lo menos un metro</li>
            <li>Aseo adecuado y frecuente de manos</li>
            <li>Reducir el número de alumnos por salón</li>
            <li>Ventilación adecuada en los salones</li>
            <li>Procurar que todo el personal esté vacunado</li>
          </ul>
        </div>
        <p>Sana distancia sería la segunda estrategia, se recomienda por lo menos un metro de distancia entre cada
          persona, siempre en presencia de mascarilla. Si ésta no se va a utilizar en espacios cerrados, la distancia
          debe ser de dos metros.</p>
        <p>Aseo adecuado y frecuente de manos, en este caso implica ya sea lavado con agua y jabón o gel antibacterial.
          Deben de proveerse los suficientes lavabos o dispensadores para cubrir las entradas a los colegios, aulas,
          baños y puntos estratégicos.</p>
        <p>Existen muchas más recomendaciones para este regreso a clases como puede ser el reducir el número de alumnos
          por salón para cumplir con la distancia de un metro, la ventilación que deben tener los salones, mantener
          puertas y ventanas abiertas.</p>
        <p>Para las actividades al aire libre, como en los recreos, la recomendación es cuidar que los niños no
          compartan sus alimentos e indicar cómo se deben quitar el cubrebocas, la distancia entre alumnos debe de ser
          de dos metros, no se recomiendan las actividades extracurriculares.</p>

        <p class="descanso">Responsabilidad de todos</p>
        <p>Toda persona, ya sea personal del colegio, padre o alumno, debe de concientizarse sobre la responsabilidad
          que tiene ante el colegio de cuidarse y de acatar las normas, de no acudir a lugares donde haya
          aglomeraciones, si hay presencia de síntomas, aunque sean leves, resguardarse en casa y reportar el caso para
          dar seguimiento al mismo.</p>
        <p>El total del personal del colegio debe estar vacunado, no se recomienda la medición de temperatura en las
          entradas a los colegios, pero sí que los padres la monitoreen en casa, así como presencia de síntomas. </p>
        <p>Al final, la responsabilidad está en cada uno de nosotros y la decisión de llevar a los hijos a las escuelas
          corresponde a cada familia, ya que influyen muchas variables que se tienen que considerar dependiendo de la
          situación de cada familia.</p>
        <p>Por recomendación hay que mantenerse informado, vigilar que en las escuelas se estén cumpliendo los
          protocolos y denunciar cualquier omisión de los mismos, si existen dudas sobre volver o no es importante
          acercarse al personal de salud para tener mejor información y poder individualizar cada caso. <i
            class="fas fa-square"></i></p>
