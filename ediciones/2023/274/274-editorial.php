      <div class="nt-txt">
        <p>La alta prevalencia, efectos nocivos e incluso fatales de la violencia familiar y sexual es un problema de salud pública que representa un obstáculo fundamental para la consolidación efectiva de la convivencia social, democrática y con pleno ejercicio de los Derechos Humanos.</p>
        <p>Cada 8 de marzo se conmemora la lucha de las mujeres por la igualdad, el reconocimiento y ejercicio efectivo de sus derechos. La fecha se oficializó en 1975 por la Organización de las Naciones Unidas. En los antecedentes de este día destacan las manifestaciones de las mujeres que alrededor del mundo alzaron la voz por una vida digna, particularmente en Europa.</p>
        <p>Los datos sobre violencia contra la mujer muestran que nos falta mucho para lograr una vida digna y equidad para la mujer en muchos ámbitos. La violencia contra las mujeres y las niñas constituye una violación de los Derechos Humanos que ha sido perpetuada durante décadas. Es omnipresente, pero no es inevitable, a menos que permanezcamos en silencio.</p>
        <p>La Encuesta Nacional sobre la Dinámica de las Relaciones en los Hogares (ENDIREH), indica que en México en 2021 vivían 128 millones de personas, 65.5 millones eran mujeres (51.2%), de las cuales más de 50.5 millones (77.1%) tenían 15 años y más de edad. Justamente sobre ellas se muestra que 70.1% a nivel nacional en el año 2021 habían experimentado al menos un incidente de violencia, ejercida por cualquier persona agresora a lo largo de su vida.</p>

        <p class="nt-descanso">Un problema palpable</p>
        <p>La violencia psicológica es la que presenta mayor prevalencia (51.6%), seguida de la violencia sexual (49.7%), la violencia física (34.7%) y la violencia económica, patrimonial y/o discriminación (27.4%).</p>
        <p>Las entidades federativas donde las mujeres de 15 años y más han experimentado mayor violencia a lo largo de su vida son: Estado de México (78.7%), Ciudad de México (76.2%) y Querétaro (75.2%). La media nacional fue de 70.1% y Sonora de se encuentra en el rango de más de 71.2%.</p>
        <p>Al revisar las cifras sobre violencia contra las mujeres que reporta el Secretariado Ejecutivo del Sistema Nacional de Seguridad Pública (SESNSP), quien sistematiza los datos de las 32 fiscalías del país desde 2015, de entonces y hasta enero del 2023, suman 6 mil 546 muertes violentas de mujeres clasificadas como feminicidios.</p>
        <p>En el año 2022 se cometieron 948 delitos de este tipo, lo que nos da una incidencia promedio de 79 feminicidios mensuales. En enero de 2023 se reportaron 68 casos. Durante 2022, los estados con más feminicidios fueron Estado de México con 138, Nuevo León con 102, Ciudad de México con 75, Veracruz con 68 y Chiapas con 44. En Sonora se presentaron 31 casos.</p>
        <p>Además de las cifras de feminicidio del 2022, SESNSP reporta que 2 mil 807 mujeres fueron asesinadas de manera violenta; con estos datos suman 3 mil 755 mujeres, de esta forma se mantiene el promedio de 10 mujeres asesinadas al día en México.</p>

        <p class="nt-descanso">Prevención desde el sector público</p>
        <p>Las y los prestadores de servicios del sector público deberán participar en el diseño y ejecución de programas educativos para la prevención y detección temprana de la violencia familiar o sexual, dirigidos a la población en general.</p>
        <p>Promover estilos de vida saludables que incluyan el desarrollo de las responsabilidades compartidas al interior de las familias, con el fin de lograr un desarrollo integral y mantener un ambiente familiar armónico y libre de violencia, con perspectiva de género.</p>

        <div class="nt-foto-bx">
          <figure>
