        <p>Las y los prestadores de servicios de salud deben realizar el procedimiento de detección de manera rutinaria y en los casos probables, llevar a cabo procedimientos necesarios para su confirmación, en base a la Norma Oficial Mexicana NOM-046-SSA2-2005, sobre Violencia familiar, sexual y contra las mujeres.</p>
        <p>Para ello, se realiza una entrevista dirigida a la o el usuario afectado por violencia familiar o sexual, en un clima de confianza, sin juicios de valor ni prejuicios, con respeto y privacidad, garantizando la confidencialidad.</p>
        <p>Debe considerar las manifestaciones del maltrato físico, psicológico, sexual, económico o abandono, los posibles factores desencadenantes del mismo y una valoración del grado de riesgo en el que viven las o los usuarios afectados por esta situación.</p>
        <p>Consulte a su médico, busque información, forme su red de apoyo en contra de la violencia y procure su Buena Salud.</p>
