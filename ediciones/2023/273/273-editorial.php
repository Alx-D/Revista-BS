        <p class="nt-descanso">Enfermedades no transmisibles</p>
        <p>A partir de muestras de sangre venosa colectadas en la Encuesta Nacional de Nutrición y Salud (Ensanut) 2021, la prevalencia de diabetes en adultos fue de 15.8%; siendo de 5.4% el hallazgo en la encuesta. La de hipertensión arterial (considerando el hallazgo en encuesta más el diagnóstico previo) fue de 26.4% para mujeres y 30.5% para hombres.</p>
        <p>Se considera glucosa elevada a la glucosa en ayuno mayor o igual a 100 y menor o igual a 125 mg/dl. Se recomienda utilizar la hemoglobina glicosilada (HbA1c) para confirmar el diagnóstico de diabetes mellitus tipo 2 en pacientes que dieron positivo en alguna prueba de tamizaje y que presenten una glucemia plasmática (glucosa en sangre) en ayunas entre 100 y 125 mg/dL. Un valor mayor a 6.5% confirma el diagnóstico.</p>
        <p>La diabetes mellitus 2 es una grave enfermedad crónica que se desencadena cuando el páncreas no produce suficiente insulina (una hormona que regula el nivel de azúcar, o glucosa, en la sangre), o cuando el organismo no puede utilizar con eficacia la insulina que produce. Se observa más comúnmente en adultos mayores, no obstante, recientemente es cada vez más frecuente en niños, adolescentes y adultos jóvenes por el incremento en los niveles de obesidad, sedentarismo y una dieta inadecuada.</p>
        <p>La hipertensión arterial sistémica (HAS), se caracteriza por niveles elevados y sostenidos de presión arterial (PA), teniendo como criterio clínico individuos con más de 18 años y niveles de tensión arterial iguales o mayores a 140 mmHg (sistólica) sobre 90mmHg (diastólica).</p>
        <p>La HAS es una enfermedad crónica multifactorial que afecta a casi un tercio de la población mundial, la cual es asociada a estilos de vida que conducen a obesidad, sobrepeso y sedentarismo, así como consumo excesivo de alcohol, sal, tabaco y mal manejo de tensiones que causan estrés.</p>
