        <p class="nt-descanso">Vigile su peso</p>
        <p>El IMC elevado se traduce en sobrepeso y obesidad, en la Ensanut 2021, la prevalencia de sobrepeso y obesidad en población de 20 o más fue de 72.4% (75% en mujeres y 69.6% en hombres). Al comparar sólo la prevalencia de obesidad, ésta fue 22.6% más alta en mujeres (41.1%) que en hombres (31.8%), mientras que la prevalencia de sobrepeso fue 10.3% mayor en hombres (37.8%) que en mujeres (33.9%).</p>
        <p>El índice de masa corporal es un indicador antropométrico del estado de nutrición que se obtiene dividiendo el peso de una persona en kilogramos entre su talla al cuadrado en metros (kg/m<sup>2</sup>). Permite determinar peso bajo, peso normal, sobrepeso y obesidad. De acuerdo con los criterios de la OMS, el IMC en adultos de 20 años y más normal es de 18.5 a 24.9 kg/m<sup>2</sup>, se considera sobrepeso si se encuentra entre 25.0 a 29.9 kg/m2, y se trata de obesidad si es mayor o igual a 30.0 kg/m<sup>2</sup>.</p>
        <p>El sobrepeso y la obesidad son condiciones que se relacionan con la susceptibilidad genética, con trastornos psicológicos, sociales y metabólicos que incrementan el riesgo para desarrollar comorbilidades tales como hipertensión arterial, diabetes mellitus tipo 2, enfermedades cardiovasculares y cerebrovasculares, así como algunas neoplasias en mama, endometrio, colon, próstata, entre otros padecimientos.</p>
        <p>Actuemos en consecuencia para evitar los factores de riesgo y las causas que los condicionan. Consuma agua pura, evite refrescos, bebidas azucaradas, pastelillos y alimentos ultraprocesados, realice actividad física al menos 30 minutos diarios 5 días a la semana, evite el tabaco y el abuso del alcohol.</p>
        <p>Cuide su salud y consulte a su médico, para vivir con sentido.</p>
