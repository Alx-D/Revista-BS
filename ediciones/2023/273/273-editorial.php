      <div class="nt-txt">
        <p>La elevación de la glucosa, la presión arterial y el índice de masa corporal (IMC) son los principales factores de riesgo que determinan las mayores pérdidas de salud en México. De acuerdo al Instituto Nacional de Salud Pública (INSP), en la Síntesis de Políticas de Salud, los factores de riesgo más relevantes en la salud de los mexicanos son:</p>
        <ul class="lista-disco-num">
          <li>Glucosa en ayuno elevada</li>
          <li>IMC elevado</li>
          <li>Riesgos por la dieta</li>
          <li>Presión sistólica elevada</li>
          <li>Consumo de alcohol</li>
          <li>Insuficiencia renal</li>
          <li>Desnutrición</li>
          <li>Consumo de tabaco</li>
          <li>Contaminación del aire</li>
          <li>Nivel de lipoproteínas de baja densidad (LDL) alto</li>
        </ul>
        <p>Un factor de riesgo (FR) es cualquier rasgo, característica o exposición de un individuo que aumente su probabilidad de sufrir una enfermedad o lesión. Conocer estos FR permite dirigir intervenciones basadas en evidencias, para cobrar conciencia y actuar con oportunidad en la prevención y control de daños a la salud, lo cual tendrá mayores beneficios en la salud poblacional.</p>

