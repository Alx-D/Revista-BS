            <figcaption>
            Las enfermedades endocrinas más comunes son: diabetes, hipotiroidismo, hipertiroidismo, enfermedad de Addison, enfermedad de Cushing, acromegalia, gigantismo, enanismo, hipogonadismo y síndrome de ovario poliquístico.
            </figcaption>
          </figure>
        </div>

        <p>Las alteraciones pueden ser resultado de trastornos congénitos y adquiridos como enfermedades autoinmunitarias, tumores, infecciones, trastornos vasculares y toxinas.</p>
        <p>Por su parte, las enfermedades autoinmunes consisten en condiciones en las que el sistema inmunitario ataca por error a las células sanas del cuerpo, las más comunes son: esclerosis múltiple, artritis reumatoide y lupus eritematoso sistémico.</p>
        <p>Como podemos apreciar, el sistema endocrino es muy complejo por todas las funciones que regula de los diferentes órganos y tejidos corporales, por ello se requiere mantener la visita médica periódica, para detectar desviaciones o trastornos metabólicos desde la infancia. Así como mediciones mediante exámenes de laboratorio en etapas críticas de la vida como la adolescencia, la edad reproductiva, el embarazo, la vida adulta y los adultos mayores.</p>
        <p>De estos controles y valoraciones dependerá la detección temprana, el manejo oportuno y el tratamiento adecuado para el control de los padecimientos endocrinológicos más comunes, los que se abordarán por distinguidos especialistas, en este número, así como algunos trastornos autoinmunes.</p>
        <p>Para vivir con sentido y lo más sanamente posible, aliméntese saludablemente, tome agua pura, evite refrescos, bebidas azucaradas y alimentos ultraprocesados. Haga ejercicio al menos 30 minutos diarios cinco días a la semana, evite el estrés y conviva sanamente con su familia o amigos, evite el tabaco y el alcohol.</p>
        <p>Consulte a su médico de manera regular y oportuna para mantener una Buena Salud.</p>
