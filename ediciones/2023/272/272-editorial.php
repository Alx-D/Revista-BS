      <div class="nt-txt">
        <p>Las glándulas endocrinas representan, junto con el sistema nervioso, los dos grandes sistemas coordinadores del organismo. Las glándulas endocrinas propiamente dichas incluyen la hipófisis, glándula pineal, tiroides, glándulas paratiroides, páncreas, glándulas suprarrenales y los ovarios o testículos, según sea el caso.</p>
        <p>El sistema endocrino coordina el funcionamiento de los diferentes órganos, aparatos y sistemas a través de hormonas, que son compuestos químicos que se secretan a la circulación sanguínea desde células especificas ubicadas dentro de glándulas endocrinas.</p>
        <p>Una vez en la circulación, las hormonas afectan la función de los tejidos diana (susceptibles), que puede ser una glándula endocrinológica o un órgano terminal. Las hormonas se unen selectivamente a receptores localizados dentro o en la superficie de las células blanco e interactúan con ellas para regular las distintas funciones, como el crecimiento y desarrollo; el metabolismo que incluye digestión, eliminación, respiración, circulación sanguínea y mantenimiento de la temperatura corporal; función sexual; reproducción y estado de ánimo.</p>
        <p>El hipotálamo, que es parte del sistema nervioso central, produce hormonas excitadoras de la hipófisis; ésta a su vez produce en su parte anterior, o adenohipófisis, hormonas estimulantes, como:</p>
        <ul class="lista-disco-nota">
          <li>Hormona estimulante de las glándulas suprarrenales (ACTH), a su vez las suprarrenales producen aldosterona, cortisol y adrenalina.</li>
          <li>Hormona estimulante de la tiroides (TSH), la tiroides produce tiroxina y calcitonina.</li>
          <li>Hormona estimulante del crecimiento de los huesos.</li>
          <li>Hormonas estimulantes de testículos y ovarios (FSH), estos órganos producen hormonas sexuales, estrógenos y progesterona en ovario, y testosterona en testículos.</li>
          <li>Hormonas estimulantes de las mamas (LTH), éstas producen leche.</li>
          <li>El hipotálamo a su vez produce la vasopresina y oxitocina, mismas que se depositan en la neurohipófisis o hipófisis posterior. La vasopresina favorece la reabsorción de agua en los riñones, lo cual eleva la presión arterial y la oxitocina regula la contracción del útero durante el parto.</li>
          <li>Las glándulas paratiroides son cuatro pequeños cuerpos ovales localizados sobre la parte posterior de la glándula tiroides, secretan hormona paratiroidea (PTH) que regula la concentración sanguínea de calcio y la resorción ósea.</li>
          <li>El páncreas produce insulina que regula el metabolismo de la glucosa que se ingiere en la dieta.</li>
          <li>La glándula pineal es un órgano aplanado, unido al techo del tercer ventrículo del cerebro, responsable de producción de melatonina.</li>
        </ul>

        <p class="nt-descanso">Desequilibrios</p>
        <p>Si los niveles hormonales están demasiado elevados o disminuidos, es posible que haya un trastorno hormonal. Las enfermedades hormonales también ocurren si el cuerpo no responde a las hormonas como debería hacerlo.</p>
        <p>El estrés, las infecciones y los cambios en el equilibrio de líquidos y electrolitos de la sangre también pueden afectar los niveles hormonales.</p>

        <div class="nt-foto-bx">
          <figure>
